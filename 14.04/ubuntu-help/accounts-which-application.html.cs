<div class="hgroup"><h1 class="title"><span class="title">Které aplikace používají online účty?</span></h1></div>
<div class="region">
<div class="contents"><p class="p"><span class="app">On-line účty</span> mohou být používány externími aplikacemi k vlastnímu nastavení.</p></div>
<div id="accounts-google-services" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">S Google účtem</span></h2></div>
<div class="region"><div class="contents"><div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p"><span class="app">Evolution</span>, emailová aplikace. Váš emailový účet bude do <span class="app">Evolution</span> přidán automaticky, tak dojde ke stažení pošty, přidání přístupu ke kontaktům a ke zobrazení Google kalendáře.</p></li>
<li class="list"><p class="p"><span class="app">Empathy</span>, aplikace pro zasílání rychlých zpráv. Váš online účet bude přidán a budete schopni komunikovat se svými přáteli.</p></li>
<li class="list"><p class="p"><span class="app">Kontakty</span>, vám dovolí zobrazit a upravit jednotlivé kontakty.</p></li>
<li class="list"><p class="p"><span class="app">Dokumenty</span> zpřístupní vaše online dokumenty pro zobrazení.</p></li>
</ul></div></div></div></div></div>
</div></div>
<div id="accounts-windows-services" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">S Windows Live, Facebookem nebo Twitter účtem</span></h2></div>
<div class="region"><div class="contents"><p class="p"><span class="app">Empathy</span> může tyto účty použít k online připojení a k zasílání zpráv vašim kontaktům, kamarádům a následujícím osobám.</p></div></div>
</div></div>
<div id="account-windows-skydrive" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Se SkyDrive účtem</span></h2></div>
<div class="region"><div class="contents"><p class="p">Aplikace <span class="app">Dokumenty</span> muže přistupovat k online dokumentům v úložišti Microsoft SkyDrive a zobrazit je.</p></div></div>
</div></div>
<div id="account-exchange" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">S Exchange účtem</span></h2></div>
<div class="region"><div class="contents"><p class="p">Jakmile vytvoříte Exchange účet, <span class="app">Evolution</span> začne pracovat s emaily na tomto účtu.</p></div></div>
</div></div>
<div id="accounts-ownCloud" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">S ownCloud účtem</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Jestli že je nastaven účet ownCloud, <span class="app">Evolution</span> je schopen přistupovat ke kontaktům a ke schůzkám v kalendáři a také je editovat.</p>
<p class="p"><span class="app">Soubory</span> a další aplikace budou schopny vypsat a zpřístupnit vaše online soubory, které jsou uloženy v instalaci ownCloud.</p>
</div></div>
