<div class="hgroup"><h1 class="title"><span class="title">Poslat soubory do Bluetooth zařízení</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Do připojených zařízení, jako jsou telefony a jiné počítače, můžete posílat soubory. Některé typy zařízení neumožňují přenos souborů, nebo jejich typů. Můžete poslílat soubory třemi různými cestami: zkrz ikonku Bluetooth v panelu, z okna nastavení Bluetooth, nebo přímo ze správce souborů.</p>
<p class="p">Pro odesílání souborů přímo ze správce souborů navštivte <span class="link"><a href="files-share.html" title="Sdílení a přenos souborů">Sdílení a přenos souborů</a></span>.</p>
<div class="note note-important" title="Důležité"><div class="inner"><div class="region"><div class="contents"><p class="p">Před tím, než začnete, se ujistěte, že máte Bluetooth povoleno. Navštivte <span class="link"><a href="bluetooth-turn-on-off.html" title="Zapnutí nebo vypnutí Bluetooth">Zapnutí nebo vypnutí Bluetooth</a></span>.</p></div></div></div></div>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Posílání souborů pomocí ikony Bluetooth</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonku Bluetooth v panelu a vyberte <span class="gui">Poslat soubory do zařízení</span>.</p></li>
<li class="steps">
<p class="p">Vyberte soubor, který chcete odeslat a klikněte na <span class="gui">Vybrat</span>.</p>
<p class="p">Pro odeslání více jak jednoho souboru vyberte všechny soubory za současného držení klávesy <span class="key"><kbd>Ctrl</kbd></span>.</p>
</li>
<li class="steps">
<p class="p">Vyberte zařízení, na nějž si přejete soubory odeslat a klikněte na <span class="gui">Poslat</span>.</p>
<p class="p">Seznam zařízení zobrazuje <span class="link"><a href="bluetooth-connect-device.html" title="Spojení počítače se zařízením s Bluetooth">úspěšně připojená zařízení</a></span> a také <span class="link"><a href="bluetooth-visibility.html" title="Co je to viditelnost?">viditelná zařízení</a></span> v dosahu. Pokud zařízení není připojeno, budete tak must učinit po kliknutí na <span class="gui">Odeslat</span>. To bude pravděpodobně vyžadovat potvrzení na druhém zařízení.</p>
<p class="p">Pokud je zde příliš mnoho zařízení, můžete je filtrovat pomocí rozbalovacího seznamu <span class="gui">Typ zařízení</span>.</p>
</li>
<li class="steps"><p class="p">Vlastník zařízení na příjmu musí obvykle přenos souborů potvrdit. Jakmile je přenos potvrzen nebo zamítnut, výsledek přenosu se objeví na obrazovce.</p></li>
</ol></div>
</div></div>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Odeslat soubory z Nastavení Bluetooth</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonku Bluetooth a vyberte <span class="gui">Nastavení Bluetooth</span>.</p></li>
<li class="steps"><p class="p">Vyberte zařízení, na které chcete soubory poslat. V seznamu jsou pouze již připojená zařízení. Viz <span class="link"><a href="bluetooth-connect-device.html" title="Spojení počítače se zařízením s Bluetooth">Spojení počítače se zařízením s Bluetooth</a></span>.</p></li>
<li class="steps"><p class="p">V informacích o zařízení klikněte na <span class="gui">Poslat soubory</span>.</p></li>
<li class="steps">
<p class="p">Vyberte soubor, který chcete odeslat a klikněte na <span class="gui">Vybrat</span>.</p>
<p class="p">Pro odeslání více jak jednoho souboru vyberte všechny soubory za současného držení klávesy <span class="key"><kbd>Ctrl</kbd></span>.</p>
</li>
<li class="steps"><p class="p">Vlastník zařízení na příjmu musí obvykle přenos souborů potvrdit. Jakmile je přenos potvrzen nebo zamítnut, výsledek přenosu se objeví na obrazovce.</p></li>
</ol></div>
