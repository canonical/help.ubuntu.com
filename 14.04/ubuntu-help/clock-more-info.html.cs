<div class="hgroup"><h1 class="title"><span class="title">Změna počtu informací zobrazených v hodinách</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Ve výchozím nastavení je v hodinách v Ubuntu zobrazen jen čas. Máte ale možnost nastavit si další informace dle svého výběru.</p>
<p class="p">Klikněte na hodiny a vyberte <span class="gui">Nastavení data a času...</span>. Přepněte se na kartu <span class="gui">Hodiny</span>. Vyberte jednotlivé volby data a času, které chcete zobrazit.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents">
<p class="p">Hodiny můžete také zcela vypnout odznačením <span class="gui">Zobrazovat hodiny v liště nabídek</span>.</p>
<p class="p">Pokud si to později rozmyslíte, můžete hodiny vrátit tak, že kliknete na ikonu umístěnou zcela vpravo na horním panelu a vyberete <span class="gui">Nastavení systému</span>. V části Systém vyberte <span class="gui">Datum a čas</span>.</p>
</div></div></div></div>
</div>
<div id="change-date-format" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Změna formátu data</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Máte možnost nastavit si formát data tak, aby odpovídal obvyklým standardům vaší země.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na horním panelu a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">V části Osobní klikněte na <span class="gui">Jazyková podpora</span>.</p></li>
<li class="steps"><p class="p">Přepněte na kartu <span class="gui">Místní formáty</span>.</p></li>
<li class="steps"><p class="p">Z rozbalovací nabídky vyberte vámi preferované umístění.</p></li>
<li class="steps"><p class="p">Aby se změna projevila, bude potřeba se <span class="link"><a href="shell-exit.html" title="Odhlášení, vypínání a přepínání uživatelů">odhlásit</a></span> a přihlásit zpět.</p></li>
</ol></div></div></div>
