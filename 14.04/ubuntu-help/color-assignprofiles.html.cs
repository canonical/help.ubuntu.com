<div class="hgroup"><h1 class="title"><span class="title">Jak přiřadím profily zařízením?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Otevřete <span class="guiseq"><span class="gui">Nastavení systému</span> ▸ <span class="gui">Barvy</span></span> a klikněte na zařízení, pro něž si přejete přidat profil.</p>
<p class="p">Kliknutím na <span class="gui">Přidat profil</span> můžete zvolit existující profil nebo ho importovat ze souboru.</p>
<p class="p">Každé zařízení může mít více přiřazených profilů, ale pouze jeden profil může být <span class="em">výchozím</span> profilem. Výchozí profil je použit, když nejsou přítomny žádné další informace, který profil zvolit automaticky. Příkladem této automatické volby může být situace, kdy byl jeden profil vytvořen pro lesklý papír a druhý pro matný papír.</p>
<div class="figure"><div class="inner">
<a href="#" class="zoom" data-zoom-in-title="Zobrazit obrázky v normální velikosti" data-zoom-out-title="Zmenšit obrázky"></a><div class="region">
<div class="contents"><div class="media media-image"><div class="inner"><img src="figures/color-profile-default.png" class="media media-block" alt=""></div></div></div>
<div class="desc">Můžete zvolit výchozí profil výběrem mezi přepínacími tlačítky.</div>
</div>
</div></div>
<p class="p">Pokud je kalibrační hardware připojen, tlačítkem <span class="gui">Kalibrovat...</span> vytvoříte nový profil.</p>
