<div class="hgroup"><h1 class="title"><span class="title">Nastavení jasu obrazovky</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete změnit jas obrazovky pro úsporu energie, respektive pro učinění obrazovky více čitelnou za jasného světla. Dále lze ztlumit obrazovku automaticky, když počítač využívá napájení z baterie, v opačném případě se obrazovka neztlumí.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Nastavení jasu</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Vyberte <span class="gui">Jas a zamykání</span>.</p></li>
<li class="steps"><p class="p">Pomocí posuvníku <span class="gui">Jas</span> nastavte vyhovující úroveň jasu.</p></li>
</ol></div>
</div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Mnoho notebooků má speciální klávesy pro úpravu jasu. Tyto klávesy jsou obvykle označeny jako slunce a jsou umístěny mezi funkčními klávesami nahoře. Držte klávesu <span class="key"><kbd class="key-Fn">Fn</kbd></span> pro použití těchto speciálních kláves.</p></div></div></div></div>
<p class="p">Zvolte <span class="gui">Ztlumit obrazovku kvůli úspoře energie</span>, tím bude jas automaticky snížen, když je počítač napájen z baterie. Podsvícení obrazovky může spotřebovat mnoho energie a výrazně snížit dobu provozu na baterii.</p>
<p class="p">Obrazovka se automaticky po chvíli nepoužívání vypne. To se týká pouze obrazovky, počítače se to nijak nedotkne. Můžete nastavit, po jak dlouhé době nepoužívání se obrazovka vypne.</p>
</div>
