<div class="hgroup"><h1 class="title"><span class="title">Automatické uzamykání obrazovky</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Když opustíte počítač, měli byste <span class="link"><a href="shell-exit.html#lock-screen" title="Uzamčení obrazovky">uzamknout obrazovku</a></span> abyste ostatním lidem znemožnili manipulaci s vaším profilem a s vašimi soubory. Při zamčené obrazovce je uživatel stále přihlášen a všechny aplikace jsou ponechány spuštěné. Pro další práci s počítačem je ale zapotřebí zadat znovu heslo. Uzamknout obrazovku můžete ručně, anebo si můžete nechat obrazovku uzamykat automaticky.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Vyberte <span class="gui">Jas a zamykání</span>.</p></li>
<li class="steps"><p class="p">Ujistěte se, že je <span class="gui">Zamknout</span> zašktnuto a poté vyberte odpočet z rozbalovacího seznamu. Obrazovka se automaticky zamkne po vybrané době nečinnosti. Vybráním <span class="gui">Při vypnutí monitoru</span> se bude obrazovka zamykat při automatickém vypnutí. Odpočet pro automatické vypnutí lze změnit v rozbaloacím seznamu <span class="gui">Vypnout display po době nečinnosti</span>.</p></li>
</ol></div></div></div>
