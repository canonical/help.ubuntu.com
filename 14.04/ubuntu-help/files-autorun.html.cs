<div class="hgroup"><h1 class="title"><span class="title">Otevření aplikace pro zařízení nebo disky</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Je možné nastavit, aby se po připojení zařízení nebo vložení disku nebo paměťové karty spustila určitá aplikace. Například můžete nastavit, aby se po připojení digitálního fotoaparátu spustil správce fotografií. Můžete také tuto funkci vypnout, aby se v případě připojení nějakého zařízení nic nestalo.</p>
<p class="p">Pro rozhodnutí, které aplikace by měly být spuštěny, když připojíte patřičné zařízení:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Vyberte <span class="guiseq"><span class="gui">Podrobnosti</span> ▸ <span class="gui">Vyměnitelná média</span></span>.</p></li>
<li class="steps">
<p class="p">Find your desired device or media type, and then choose an application
    or action for that media type. See below for a description of the different
    types of devices and media.</p>
<p class="p">Instead of starting an application, you can also set it so that the device
    will be shown in the file manager. When that happens, you will be asked
    what to do, or nothing will happen automatically.</p>
</li>
<li class="steps"><p class="p">Volba <span class="gui">Programy</span> je mírně odlišná od ostatních - pokud počítač detekuje na disku software, pokusí se ho automaticky spustit. To se hodí, pokud máte na CD aplikaci a chcete jí spustit hned po vložení disku do mechaniky (například prezentace).</p></li>
<li class="steps"><p class="p">Pokud v seznamu nevidíte zařízení či typ média které chcete nastavit (např. Blu-ray disky nebo e-čtečky), klikněte na <span class="gui">Další média</span> a vyberte si z rozbalovacího seznamu <span class="gui">Typ</span> a aplikaci nebo akci ze seznamu <span class="gui">Akce</span>.</p></li>
</ol></div></div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Nechcete-li automaticky otevírat žádnou aplikaci nezávisle na médiu, vyberte <span class="gui">Nikdy se nedotazovat nebo nespouštět programy při vložení</span> na konci okna Vyměnitelná média.</p></div></div></div></div>
</div>
<div id="files-types-of-devices" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Druhy zařízení a médií</span></h2></div>
<div class="region"><div class="contents"><div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Audio disky</dt>
<dd class="terms"><p class="p">Choose your favorite music application or CD audio extractor to handle
    audio CDs. If you use audio DVDs (DVD-A), select how to open them under
    <span class="gui">Other Media</span>. If you open an audio disc with the file manager,
    the tracks will appear as WAV files that you can play in any audio player
    application.</p></dd>
<dt class="terms">Video disky</dt>
<dd class="terms"><p class="p">Vyberte si vaši nejoblíbenější aplikaci pro práci s DVD disky. Použijte tlačítko <span class="gui">Ostatní média</span> k nastavení aplikace pro Bru-ray, HD DVD, video CD (VCD) a super video CD (SVCD). Jestliže DVD disky nebo ostatní média nefungují po vložení do mechaniky správně, podívejte se na <span class="link"><a href="video-dvd.html" title="Proč nemohu přehrát DVD?">Proč nemohu přehrát DVD?</a></span>.</p></dd>
<dt class="terms">Prázdný disk</dt>
<dd class="terms"><p class="p">Use the <span class="gui">Other Media</span> button to select a disc-writing
    application for blank CDs, blank DVDs, blank Blu-ray discs, and blank HD
    DVDs.</p></dd>
<dt class="terms">Kamery a fotoaparáty</dt>
<dd class="terms">
<p class="p">Use the <span class="gui">Photos</span> drop-down to choose a photo-management
    application to run when you plug in your digital camera, or when you insert
    a media card from a camera, such as a CF, SD, MMC, or MS card. You can also
    simply browse your photos using the file manager.</p>
<p class="p">Under <span class="gui">Other Media</span>, you can select an application to open Kodak
    picture CDs, such as those you might have made in a store. These are regular
    data CDs with JPEG images in a folder called <span class="file">PICTURES</span>.</p>
</dd>
<dt class="terms">Hudební přehrávače</dt>
<dd class="terms"><p class="p">Vyberte aplikaci pro správu hudební knihovny na přenosném přehrávači, nebo soubory spravujte sami pomocí souborového správce.</p></dd>
<dt class="terms">Elektronické čtečky knih</dt>
<dd class="terms"><p class="p">Use the <span class="gui">Other Media</span> button to choose an application to manage the
    books on your e-book reader, or manage the files yourself using the file manager.</p></dd>
<dt class="terms">Software</dt>
<dd class="terms">
<p class="p">Some discs and removable media contain software that is supposed to be run
    automatically when the media is inserted. Use the <span class="gui">Software</span> option
    to control what to do when media with autorun software is inserted. You will
    always be prompted for a confirmation before software is run.</p>
<div class="note note-warning" title="Varování"><div class="inner"><div class="region"><div class="contents"><p class="p">Nikdy nespouštějte software z neznámého média.</p></div></div></div></div>
</dd>
</dl></div></div></div></div></div>
