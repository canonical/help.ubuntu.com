<div class="hgroup"><h1 class="title"><span class="title">Kopírování a přesouvání souborů a složek</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Soubor nebo adresář může být kopírován nebo přesunut do nového umístění pomocí přetažení a upuštění myši, použitím příkazů na kopírování a vložení nebo použitím klávesových zkratek.</p>
<p class="p">Můžete například chtít zkopírovat svou prezentaci na flash disk tak, abyste si ji mohli odnést do práce. Nebo si můžete vytvořit záložní kopii dokumentu před tím, než jej změníte (a poté, když se vám nebudou změny líbit, použít kopii s původním obsahem).</p>
<p class="p">Tyto instrukce jsou stejné pro soubory i složky. Soubory i složky můžete přesouvat a kopírovat úplně stejně.</p>
<div class="steps ui-expander">
<div class="yelp-data yelp-data-ui-expander" dir="ltr" data-yelp-expanded="false"></div>
<div class="inner">
<div class="title title-steps"><h2><span class="title">Kopírování souborů</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Jedním kliknutím vyberte soubor, který si přejete zkopírovat.</p></li>
<li class="steps"><p class="p">Pravé kliknutí a vyberte <span class="gui">Kopírovat</span>, nebo stiskněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>C</kbd></span></span>.</p></li>
<li class="steps"><p class="p">Přejděte do složky do níž si přejete uložit kopii tohoto souboru.</p></li>
<li class="steps"><p class="p">Click the gear icon and pick <span class="gui">Paste</span> to finish copying the
 file, or press <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>V</kbd></span></span>. There
 will now be a copy of the file in the original folder and the other
 folder.</p></li>
</ol></div>
</div>
</div>
<div class="steps ui-expander">
<div class="yelp-data yelp-data-ui-expander" dir="ltr" data-yelp-expanded="false"></div>
<div class="inner">
<div class="title title-steps"><h2><span class="title">Přesunutí souborů pomocí vyjmout a vložit</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Jedním kliknutím vyberte soubor, který si přejete přesunout.</p></li>
<li class="steps"><p class="p">Pravý klik a vyberte <span class="gui">Vyjmout</span> nebo stiskněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>X</kbd></span></span>.</p></li>
<li class="steps"><p class="p">Přejděte do složky do níž si přejete přesunout tento soubor.</p></li>
<li class="steps"><p class="p">Click the gear button in the toolbar and pick <span class="gui">Paste</span> to
 finish moving the file, or press <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>V</kbd></span></span>.
 The file will be taken out of its original folder and moved to the other
 folder.</p></li>
</ol></div>
</div>
</div>
<div class="steps ui-expander">
<div class="yelp-data yelp-data-ui-expander" dir="ltr" data-yelp-expanded="false"></div>
<div class="inner">
<div class="title title-steps"><h2><span class="title">Kopírování a přesouvání souborů přetažením</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete správce souborů a přejděte do složky obsahující soubor, který si přejete zkopírovat.</p></li>
<li class="steps"><p class="p">Click <span class="gui">Files</span> in the top bar, select <span class="gui">New Window</span> (or
 press <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>N</kbd></span></span>) to open a second window. In
 the new window, navigate to the folder where you want to move or copy the file.
 </p></li>
<li class="steps">
<p class="p">Přetáhněte soubor z jednoho okna do druhého. Pokud je cíl na <span class="em">stejném</span> zařízení, soubor bude <span class="em">přesunut</span>. Pokud je cíl na <span class="em">jiném</span> zařízení, soubor bude <span class="em">zkopírován</span>.</p>
<p class="p">For example, if you drag a file from a USB memory stick to your Home folder,
 it will be copied because you're dragging from one device to another.</p>
<p class="p">Pokud budete chtít, můžete si vynutit kopírování respektive přesun souboru držením <span class="key"><kbd>Ctrl</kbd></span> respektive <span class="key"><kbd>Shift</kbd></span> v průběhu přetahování souboru.</p>
</li>
</ol></div>
</div>
</div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Do složek, které jsou <span class="em">pouze pro čtení</span>, nemůžete přesouvat ani kopírovat soubory. Aby se zabránilo změnám, jsou některé složky nastaveny jako pouze pro čtení. Pomocí <span class="link"><a href="nautilus-file-properties-permissions.html" title="Nastavení oprávnění souborů">změny přístupových práv </a></span> u nich můžete stav pouze pro čtení změnit.</p></div></div></div></div>
