<div class="hgroup"><h1 class="title"><span class="title">Hledání ztraceného souboru</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud jste vytvořili nebo stáhli soubor a nyní jej nemůžete najít, postupujte podle následujících tipů.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Pokud si nepamatujete, kam jste soubor uložili, ale máte představu o tom, jak jste jej pojmenovali, můžete jej vyhledat podle názvu. Pro více informací o této možnosti navštivte <span class="link"><a href="files-search.html" title="Vyhledávání souborů">Vyhledávání souborů</a></span>.</p></li>
<li class="list"><p class="p">Pokud jste soubor stáhli, váš webový prohlížeč jej pravděpodobně uložil do některé z běžných složek. Zkontrolujte složky Plocha a Stažené ve svém domovském adresáři.</p></li>
<li class="list"><p class="p">Je možné, že jste daný soubor omylem smazali. Když smažete soubor, bude přesunut do koše, kde zůstane tak dlouho, dokud koš ručně nevyprázdníte. Pro postup pro obnovu smazaných souborů navštivte <span class="link"><a href="files-recover.html" title="Obnovení souboru z Koše">Obnovení souboru z Koše</a></span>.</p></li>
<li class="list"><p class="p">You might have renamed the file in a way that made the file hidden.
  Files that start with a <span class="file">.</span> or end with a <span class="file">~</span> are hidden
  in the file manager. Click the
  <span class="media"><span class="media media-image"><img src="figures/go-down.png" class="media media-inline" alt="down"></span></span> button in the file
  manager toolbar and pick <span class="gui">Show Hidden Files</span> to display them. See
  <span class="link"><a href="files-hidden.html" title="Skrytí souboru">Skrytí souboru</a></span> to learn more.</p></li>
</ul></div></div></div>
