<div class="hgroup"><h1 class="title"><span class="title">Vyhledávání souborů</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Soubory můžete prohledávat dle názvu přímo ve správci souborů. Dokonce si můžete výsledky vyhledávání ukládat, poté se zobrazí ve speciálním adresáři ve vašem domovském adresáři.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Hledání</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p"><span class="link"><a href="files-browse.html" title="Procházení souborů a složek">Otevřete správce souborů</a></span></p></li>
<li class="steps"><p class="p">Pokud víte, že soubory, které hledáte, jsou v určité složce, přejděte do této složky.</p></li>
<li class="steps"><p class="p">Klikněte na ikonu lupy v panelu nástrojů nebo stiskněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>F</kbd></span></span>.</p></li>
<li class="steps"><p class="p">Type a word or words that you know appear in the file name. For
    example, if you name all your invoices with the word "Invoice", type
    <span class="input">invoice</span>. Press <span class="key"><kbd>Enter</kbd></span>. Words are matched regardless
    of case.</p></li>
<li class="steps">
<p class="p">Výsledky lze zúžit pomocí umístění nebo typu souboru.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Click <span class="gui">Home</span> to restrict the search results to your
      <span class="file">Home</span> folder, or <span class="gui">All Files</span> to search
      everywhere.</p></li>
<li class="list"><p class="p">Click <span class="key"><kbd>+</kbd></span> and pick a <span class="gui">File Type</span> from the
      drop-down list to narrow the search results based on file type. Click the
      <span class="key"><kbd>x</kbd></span> button to remove this option and widen the search
      results.</p></li>
</ul></div></div></div>
</li>
<li class="steps"><p class="p">Ve správci souborů můžete nalezené soubory otevírat, kopírovat, přesouvat či jinak s nimi pracovat stejně jako s jinými adresáři.</p></li>
<li class="steps"><p class="p">Klikněte znovu na ikonu lupy v panelu nástrojů pro ukončení vyhledávání a návratu do adresáře.</p></li>
</ol></div>
</div></div>
<p class="p">Pokud často hledáte stejné věci, můžete si dané vyhledávání pro urychlení uložit.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Uložení vyhledávání</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Začněte vyhledávání stejně jako výše.</p></li>
<li class="steps"><p class="p">When you're happy with the search parameters, click the ⚙ gear
    button and select <span class="gui">Save Search As</span>.</p></li>
<li class="steps"><p class="p">Vyberte pro vyhledávání jméno a klikněte na <span class="gui">Uložit</span>. Pokud chcete, můžete vybrat jinou složku pro uložení vyhledávání. Když budete danou složku procházet, uložené vyhledávání bude vypadat jako oranžová ikona složky s lupou.</p></li>
</ol></div>
</div></div>
<p class="p">Výsledky vyhledávání můžete po skončení práce jednoduše <span class="link"><a href="files-delete.html" title="Mazání souborů a složek">smazat</a></span> jako jiné soubory. Při smazání vyhledávání se nesmažou soubory které vyhledávání odpovídají.</p>
</div>
