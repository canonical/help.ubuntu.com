<div class="hgroup"><h1 class="title"><span class="title">Sdílení a přenos souborů</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">You can share files with your contacts or transfer them to external 
devices or <span class="link"><a href="nautilus-connect.html" title="Procházení souborů na server nebo po síti">network shares</a></span> directly 
from the file manager.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete <span class="link"><a href="files-browse.html" title="Procházení souborů a složek">správce souborů</a></span>.</p></li>
<li class="steps"><p class="p">Vyberte soubor který chcete přenést.</p></li>
<li class="steps"><p class="p">Klikněte pravým tlačítkem na soubor a vyberte <span class="gui">Odeslat</span>.</p></li>
<li class="steps"><p class="p">Objeví se okno <span class="gui">Odeslat...</span>. Vyberte kam chcete soubor odeslat a klikněte na <span class="gui">Odeslat</span>. Pro více informací si prohlédněte seznam příjemců .</p></li>
</ol></div></div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Můžete odeslat více souborů najednou. Držte klávesu <span class="key"><kbd>Ctrl</kbd></span> a klikejte na vybrané soubory, poté klikněte na libovolný z nich pravým tlačítkem. Můžete soubory automaticky zkomprimovat do archivu zip nebo tar.</p></div></div></div></div>
<div class="list"><div class="inner">
<div class="title title-list"><h2><span class="title">Cíle</span></h2></div>
<div class="region"><ul class="list">
<li class="list"><p class="p">Pro odeslání souboru email jednoduše zvolte <span class="gui">E-mail</span> a zadejte adresu příjemce.</p></li>
<li class="list"><p class="p">Pro odeslání souborů nějakému IM kontaktu vyberte <span class="gui">Instant Message</span> a vyberte kontakt z rozbalovacího seznamu. Aplikace pro instant messaging by měla být nejdříve spuštěna.</p></li>
<li class="list"><p class="p">Pro zapsání soubor na CD nebo DVD vyberte <span class="gui">Tvůrce CD/DVD</span>. Pro více informací navštivte <span class="link"><a href="files-disc-write.html" title="Zápis souborů na CD nebo DVD">Zápis souborů na CD nebo DVD</a></span>.</p></li>
<li class="list"><p class="p">Pro odeslání souborů přes Bluetooth vyberte <span class="gui">Bluetooth (OBEX Push)</span> a vyberte kterému zařízení se mají soubory poslat. Je potřeba mít nejprve spárovaná zařízení. Viz <span class="link"><a href="bluetooth.html" title="Bluetooth">Bluetooth</a></span> pro více informací.</p></li>
<li class="list"><p class="p">Pro odeslání souborů na externí zařízení jako USB flashka nebo nahrání na připojený server, vyberte <span class="gui">Výměnná zařízení a sdílení</span> a vyberte zařízení nebo server na který chcete soubory nahrát.</p></li>
</ul></div>
