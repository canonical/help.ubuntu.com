<div class="title title-table"><h2><span class="title">Pohyb v pracovním prostředí</span></h2></div>
<div class="region"><table class="table" style="border-top-style: solid;border-bottom-style: solid;">
<tr>
<td><p class="p"><span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span></p></td>
<td><p class="p"><span class="link"><a href="shell-windows-switching.html" title="Přepínání mezi okny">Rychlé přepínání mezi okny.</a></span> Pro změnu směru podržte <span class="key"><kbd>Shift</kbd></span>.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>`</kbd></span></span></p></td>
<td style="border-top-style: solid;">
<p class="p">Přepínání mezi okny aktuální aplikace nebo mezi okny vybrané aplikace po použití <span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span>.</p>
<p class="p">Tato zkratka využívá klávesu <span class="key"><kbd>`</kbd></span> z amerických klávesnic, kde je <span class="key"><kbd>`</kbd></span> nad klávesou <span class="key"><kbd>Tab</kbd></span>. Na ostatních klávesnicích je tato zkratka <span class="key"><kbd>Alt</kbd></span> a jakákoli klávesa nad <span class="key"><kbd>Tab</kbd></span>.</p>
</td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>šipky</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p"><span class="link"><a href="shell-workspaces-switch.html" title="Přepínání mezi pracovními plochami">Přepínání mezi pracovními plochami.</a></span></p></td>
</tr>
</table></div>
