<div class="hgroup"><h1 class="title"><span class="title">Navigace pomocí klávesnice</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Na této stránce naleznete instrukce pro lidi, kteří nemohou používat myš nebo jiná ukazovací zařízení nebo chtějí využívat klávesnici jak je jen možné. Pro klávesové zkratky užitečné pro všechny uživatele navštivte spíše <span class="link"><a href="shell-keyboard-shortcuts.html" title="Užitečné klávesové zkratky">Užitečné klávesové zkratky</a></span>.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud nemůžete používat ukazovací zařízení jako je například myš, můžete pohybovat kurzorem myši pomocí numerického bloku klávesnice. Pro více informací navštivte <span class="link"><a href="mouse-mousekeys.html" title="Click and move mouse pointer using the keypad">Click and move mouse pointer using the keypad</a></span>.</p></div></div></div></div>
<div class="table"><div class="inner">
<div class="title title-table"><h2><span class="title">Pohyb v pracovním prostředí</span></h2></div>
<div class="region"><table class="table" style="border-top-style: solid;border-bottom-style: solid;">
<tr>
<td><p class="p"><span class="key"><kbd>Tab</kbd></span> a <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span></p></td>
<td>
<p class="p">Pomocí klávesnice můžete měnit aktivní prvky. <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span> přepíná mezi skupinami prvků, jako z postranního panelu do hlavního okna. <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span> také deaktivuje prvky pracující s klávesou <span class="key"><kbd>Tab</kbd></span> jako jsou textová pole.</p>
<p class="p">Pro pohyb v opačném pořadí stiskněte <span class="key"><kbd>Shift</kbd></span>.</p>
</td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p">Kurzorové klávesy</p></td>
<td style="border-top-style: solid;">
<p class="p">Přepíná výběr mezi prvky v jedné skupině seskupených prvků. Použijte šipky pro pohyb v panelu nástrojů, výběr položek v seznamu nebo ikonovém zobrazení nebo pro výběr možnosti z jedné skupiny.</p>
<p class="p">Ve stromovém zobrazení použijte levou a pravou šipku ke sbalení či rozbalení obsažených položek.</p>
</td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+kurzorové klávesy</span></p></td>
<td style="border-top-style: solid;"><p class="p">V seznamovém či ikonovém zobrazení se posune na jinou položku bez změny označení stávajích.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Shift</kbd></span>+kurzorové klávesy</span></p></td>
<td style="border-top-style: solid;"><p class="p">V seznamovém či ikonovém zobrazení přidá k výběru aktuální položku.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="key"><kbd>Mezerník</kbd></span></p></td>
<td style="border-top-style: solid;"><p class="p">Aktivace vybrané položky jako tlačítko, přepínač nebo položka seznamu.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Mezerník</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p">V seznamovém či ikonovém zobrazení odebere od výběru aktuální položku.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="key"><kbd>Alt</kbd></span></p></td>
<td style="border-top-style: solid;"><p class="p">Držte klávesu <span class="key"><kbd>Alt</kbd></span> pro použití urychlovačů: podržená písmena v menu, tlačítka a jiné ovládací prvky. Stisknutí <span class="key"><kbd>Alt</kbd></span> a podrženého písmena je totéž co kliknutí myší.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="key"><kbd>Esc</kbd></span></p></td>
<td style="border-top-style: solid;"><p class="p">Opuštění nabídky, vyskakovacího okna, přepínače nebo dialogu.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="key"><kbd>F10</kbd></span></p></td>
<td style="border-top-style: solid;"><p class="p">Open the first menu on the menu bar of a window. Use the arrow keys
    to navigate the menus.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Shift</kbd></span>+<span class="key"><kbd>F10</kbd></span></span> nebo klávesa Menu</p></td>
<td style="border-top-style: solid;"><p class="p">Zobrazení kontextové nabídky pro aktuálně vybrané okno. Stejná funkce jako pravý klik.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>F10</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p">Ve správci souborů vyvolá kontextovou nabídku aktuálho adresáře totožně jako pravé kliknutí do volného prostoru.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>PageUp</kbd></span></span> a <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>PageDown</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p">V okně s více kartami přepne na kartu vlevo nebo vpravo.</p></td>
</tr>
</table></div>
</div></div>
