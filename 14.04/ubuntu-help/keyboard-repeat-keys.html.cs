<div class="hgroup"><h1 class="title"><span class="title">Vypnout opakování znaků při držení</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud ve výchozím nastavení držíte klávesu, znak se bude opakovat dokud ji nepustíte. Pokud máte problém dostatečně rychle klávesu zase uvolnit, můžete tuto funkci zakázat, nebo změnit prodlevu před opakováním.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na horním panelu a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">V části Hardware klikněte na <span class="gui">Klávesnice</span>.</p></li>
<li class="steps">
<p class="p">Vypnutím volby <span class="gui">Při držení klávesy opakovat znaky</span> zakážete opakování kláves.</p>
<p class="p">Nebo můžete zvětšit čas potřebný k prvnímu opakováním znaku a zvýšit rychlost opakování.</p>
</li>
</ol></div></div></div>
