<div class="hgroup"><h1 class="title"><span class="title">Click and move mouse pointer using the keypad</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud máte potíže s používáním myši nebo podobného zařízení, můžete kurzor ovládat pomocí numerického bloku na klávesnici. Tato funkce se nazývá <span class="em">myš z klávesnice</span>.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Stiskněte klávesu <span class="key"><kbd><span class="link"><a href="windows-key.html" title='Která klávesa je "Super"?'>Super</a></span></kbd></span> pro otevření nabídky <span class="gui">Dash</span>.</p></li>
<li class="steps"><p class="p">Napište <span class="input">Usnadnění přístupu</span> a potvrďte <span class="key"><kbd>Enterem</kbd></span> pro otevření nastavení.</p></li>
<li class="steps"><p class="p">Stiskněte klávesu <span class="key"><kbd>Tab</kbd></span> k vybrání záložky <span class="gui">Dívání</span>.</p></li>
<li class="steps"><p class="p">Použijte klávesu <span class="key"><kbd>←</kbd></span> k přepnutí na záložku <span class="gui">Ukazování a klikání</span>.</p></li>
<li class="steps"><p class="p">Zmáčkněte klávesu <span class="key"><kbd>↓</kbd></span> k vybrání nabídky<span class="gui">Myš z klávesnice</span> a potvrďte <span class="key"><kbd>Enterem</kbd></span>.</p></li>
<li class="steps"><p class="p">Ujistěte se, že máte vypnutý <span class="key"><kbd>Num Lock</kbd></span>. Nyní můžete pohybovat kurzorem pomocí klávesnice.</p></li>
</ol></div></div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Tento postup popisuje nejrychlejší cestu k povolení myši z klávesnice pouze za pomoci klávesnice. Vyberte <span class="gui">Nastavení usnadnění přístupu</span> pro detailní nastavení.</p></div></div></div></div>
<p class="p">Numerický blok jsou číselné klávesy na klávesnici, obvykle uspořádané do obdélníkové mřížky. Pokud máte klávesnici bez numerického bloku (například na notebooku), bude nutné použít klávesu <span class="key"><kbd class="key-Fn">Fn</kbd></span> v kombinaci s nějakými jinými. Pokud tuto funkci používáte často, zvažte zakoupení externího numerického bloku.</p>
<p class="p">Každé číslo na numerickém bloku odpovídá směru myši. například stisknutí <span class="key"><kbd>8</kbd></span> posune kurzor nahoru a <span class="key"><kbd>2</kbd></span> dolů. Stisknutím klávesy <span class="key"><kbd>5</kbd></span> kliknete jednou, dvakrát rychle za sebou vygeneruje dvojklik.</p>
<p class="p">Většina klávesnic má zvláštní klávesu pro vyvolání pravého tlačítka myši; většinou se nachází vedle mezerníku. Ovšem vyvolá menu tam, kde je aktivní klávesnice nezávisle na pozici kurzoru myši. Viz <span class="link"><a href="a11y-right-click.html" title="Simulace kliknutí pravým tlačítkem myši">pravé tlačítko</a></span> pro podrobnosti o klikáním pravým tlačítem pomocí klávesy <span class="key"><kbd>5</kbd></span> nebo levým tlačítkem myši.</p>
<p class="p">Chcete-li psát čísla a zároveň ovládat pomocí numerického bloku myš, zapněte <span class="key"><kbd>Num Lock</kbd></span>. Pokud je <span class="key"><kbd>Num Lock</kbd></span> zapnutý, je funkce pohybu myši dočasně vypnuta.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Čísledné klávesy na horní straně klávesnice nelze použít pro ovládání myši. takto lze použít pouze čísla na numerickém bloku.</p></div></div></div></div>
