<div class="hgroup"><h1 class="title"><span class="title">Click, drag, or scroll with the touchpad</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pomocí touchpadu můžete klikat, dvojklikat, přesouvat a rolovat obsah bez pomoci jiných kláves.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Myš a touchpad</span>.</p></li>
<li class="steps">
<p class="p">In the <span class="gui">Touchpad</span> section, check <span class="gui">Tap to click</span>.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Sekce <span class="gui">Touchpad</span> se objeví pouze tehdy, je-li touchpad v systému přítomen.</p></div></div></div></div>
</li>
</ol></div></div></div>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Pro kliknutí klepněte na touchpad.</p></li>
<li class="list"><p class="p">Pro dvojklik klepněte dvakrát.</p></li>
<li class="list"><p class="p">Pro přetažení klepněte dvakrát ale po druhém klepnutí nezvedejte prst. Přetáhněte položku kam si přejete a poté zvedněte prst.</p></li>
<li class="list"><p class="p">Pokud váš touchpad podporuje gesta za použití více prstů, můžete vyvolat pravý klik klepnutím dvěma prsty najednou. Jinak budete muset pro pravé kliknutí použít hardwarová tlačítka. Pro informace o možnosti vyvolání pravého kliknutí bez použití myši navštivte <span class="link"><a href="a11y-right-click.html" title="Simulace kliknutí pravým tlačítkem myši">Simulace kliknutí pravým tlačítkem myši</a></span>.</p></li>
<li class="list"><p class="p">Pokud váš touchpad podporuje gesta za použití více prstů, můžete vyvolat <span class="link"><a href="mouse-middleclick.html" title="Prostřední kliknutí">kliknutí prostředním tlačítkem</a></span> klepnutím třemi prsty najednou.</p></li>
</ul></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Při klikání nebo přetahování pomocí více prstů se ujistěte, že jsou rozprostřeny dostatečně daleko od sebe. Pokud budou vaše prsty příliš blízko u sebe, může si počítač myslet, že se jedná o jeden prst.</p></div></div></div></div>
</div>
<div id="twofingerscroll" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Posouvání dvěma prsty</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pousouvání pomocí dvou prstů na touchpadu.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Myš a touchpad</span>.</p></li>
<li class="steps"><p class="p">V sekci <span class="gui">Touchpad</span> vyberte <span class="gui">Posunování dvěma prsty</span>.</p></li>
</ol></div></div></div>
<p class="p">When this is selected, tapping and dragging with one finger will work as
normal, but if you drag two fingers across any part of the touchpad, it will
scroll instead. If you also select <span class="gui">Enable horizontal scrolling</span>, you
can move your fingers left and right to scroll horizontally. Be careful to space
your fingers a bit apart. If your fingers are too close together, they just look
like one big finger to your touchpad.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Skrolování dvěma prsty nemusí fungovat na všech touchpadech.</p></div></div></div></div>
</div></div>
</div></div>
<div id="contentsticks" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Přilepení obsahu na prsty</span></h2></div>
<div class="region"><div class="contents">
<p class="p">You can drag content as if sliding a physical piece of paper using the
touchpad.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Myš a touchpad</span>.</p></li>
<li class="steps"><p class="p">In the <span class="gui">Touchpad</span> section, check
   <span class="gui">Content sticks to fingers</span>.</p></li>
</ol></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">This feature is also known as <span class="em">Natural Scrolling</span> or
<span class="em">Reverse Scrolling</span>.</p></div></div></div></div>
</div></div>
</div></div>
