<div class="hgroup"><h1 class="title"><span class="title">Nastavení oprávnění souborů</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pomocí oprávnění souborů můžete určit, kdo bude moci procházet a upravovat soubory, které vlastníte. Pro prohlížení a nastavení oprávnění souboru na něj klikněte pravým tlačítkem a zvolte <span class="gui">Vlastnosti</span> a poté přejděte na kartu <span class="gui">Oprávnění</span>.</p>
<p class="p">Pro více informací a možnostech nastavení oprávnění navštivte <span class="link"><a href="#files" title="Soubory">Soubory</a></span> a <span class="link"><a href="#folders" title="Složky">Složky</a></span>.</p>
</div>
<div id="files" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Soubory</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Oprávnění můžete nastavovat pro vlastníka souboru, skupinu a ostatní uživatele systému. U vašich souboru jste vždy vlastníkem vy a sám sobě můžete nastavit právo pro čtení nebo pro čtení a zápis. Práva pouze pro čtení nastavte v případě, že soubor nechcete omylem změnit.</p>
<p class="p">Každý uživatel na vašem počítači patří do nějaké skupiny. Na domácích počítačích je běžně pro každého uživatele vytvořena jeho vlastní skupina a skupinová oprávnění se obvykle nepoužívají. V podnikovém prostředí jsou skupiny někdy používány pro oddělení nebo jednotlivé projekty. Stejně jako má soubor vlastníka, patří i nějaké skupině. U souboru můžete nastavit jak jeho skupinu, tak nastavovat oprávnění pro všechny uživatele v dané skupině. Souboru můžete přiřadit jen takovou skupinu, do níž sami patříte.</p>
<p class="p">Oprávnění můžete nastavovat také pro ostatní uživatele než vlastníka a ty, kteří jsou v dané skupině.</p>
<p class="p">Pokud je soubor programem, jako například skripty, musíte vybrat <span class="gui">Povolit spouštění souboru jako programu</span>, aby jej bylo možné spouštět. I pokud je tato položka zaškrtnuta, může jej správce souborů stále spouštět v nějaké aplikaci nebo se zeptat, co má dělat. Pro více informací navštivte <span class="link"><a href="nautilus-behavior.html#executable" title="Spustitelné textové soubory">Spustitelné textové soubory</a></span>.</p>
</div></div>
</div></div>
<div id="folders" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Složky</span></h2></div>
<div class="region"><div class="contents">
<p class="p">U složek můžete nastavovat oprávnění pro vlastníka, skupinu a ostatní uživatele. Pro více informací o vlastnících, skupinách a ostatních uživatelích se podívejte na oprávnění souborů popsané výše.</p>
<p class="p">Oprávnění, která můžete nastavit pro složky se poněkud liší od těch, které můžete nastavovat pro soubory.</p>
<div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms"><span class="gui">Žádné</span></dt>
<dd class="terms"><p class="p">Uživatel nebude schopen vidět, jaké soubory jsou ve složce.</p></dd>
<dt class="terms"><span class="gui">Pouze vypsat soubory</span></dt>
<dd class="terms"><p class="p">Uživatel bude moci vidět, jaké soubory jsou ve složce, ale nebude schopen soubory otevírat, vytvářet ani mazat.</p></dd>
<dt class="terms"><span class="gui">Přístup k souborům</span></dt>
<dd class="terms"><p class="p">Uživatel bude moci otevřít soubory ve složce (za předpokladu, že má oprávnění provádět to s konkrétním souborem), ale nebude moci soubory vytvářet ani mazat.</p></dd>
<dt class="terms"><span class="gui">Vytváření a mazání souborů</span></dt>
<dd class="terms"><p class="p">Uživatel bude mít plný přístup ke složce, včetně procházení, tvorby a mazání souborů.</p></dd>
</dl></div></div></div>
<p class="p">You can also quickly set the file permissions for all the files
    in the folder by clicking <span class="gui">Change Permissions for Enclosed Files</span>.
    Use the drop-down lists to adjust the permissions of contained files or
    folders, and click <span class="gui">Change</span>. Permissions are applied to files and
    folders in subfolders as well, to any depth.</p>
</div></div>
