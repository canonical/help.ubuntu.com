<div class="hgroup"><h1 class="title"><span class="title">Instalace zásuvného modulu Flash</span></h1></div>
<div class="region">
<div class="contents">
<p class="p"><span class="app">Flash</span> je <span class="em">zásuvný modul</span> pro webový prohlížeč umožňující na některých stránkách sledování videa a prohlížení některých interaktivních webových stránek. Některé webové stránky dokonce nebudou bez Flash fungovat.</p>
<p class="p">Pokud Flash potřebujete a nemáte jej nainstalován, pravděpodobně vás o tom bude daná stránka informovat jednoduchou zprávou. Flash je dostupný zdarma (ale nemá otevřený zdrojový kód) pro většinu webových prohlížečů.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Instalace Flash</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="link"><a href="https://apps.ubuntu.com/cat/applications/flashplugin-installer" title="https://apps.ubuntu.com/cat/applications/flashplugin-installer">tento odkaz</a></span> pro spuštění <span class="app">Centra softwaru</span>.</p></li>
<li class="steps"><p class="p">Abyste se ujistili, že chcete nainstalovat Flash, přečtěte si informace a recenze.</p></li>
<li class="steps"><p class="p">Pokud se rozhodnete nainstalovat Flash, klikněte na tlačítko <span class="gui">Nainstalovat</span> v okně Centra softwaru.</p></li>
<li class="steps"><p class="p">Pokud máte otevřený nějaký webový prohlížeč, zavřete jej a znovu spusťte. Webový prohlížeč by měl automaticky detekovat, že je Flash nainstalován a měli byste být schopni prohlížet obsah pomocí technologie Flash.</p></li>
</ol></div>
</div></div>
</div>
<div id="alternatives" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Svobodné alternativy Flashe</span></h2></div>
<div class="region"><div class="contents">
<p class="p">K dispozici je několik open-source alternativ Flashe. V některých případech mohou fungovat lépe než Flash (například přehrávání zvuku), ale v jiných fungují hůře (například nezvládnou zobrazit některé komplikované webové stránky postavené na Flashi).</p>
<p class="p">Pokud nejste s přehrávačem Flash spokojeni nebo pokud byste rádi používali tolik svobodného softwaru, kolik je jen možné, možná budete chtít některou z těchto alternativ využít. Zde je několik možností:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p"><span class="link"><a href="https://apps.ubuntu.com/cat/applications/browser-plugin-gnash" title="https://apps.ubuntu.com/cat/applications/browser-plugin-gnash">Gnash</a></span></p></li>
<li class="list"><p class="p"><span class="link"><a href="https://apps.ubuntu.com/cat/applications/browser-plugin-lightspark" title="https://apps.ubuntu.com/cat/applications/browser-plugin-lightspark">LightSpark</a></span></p></li>
</ul></div></div></div>
