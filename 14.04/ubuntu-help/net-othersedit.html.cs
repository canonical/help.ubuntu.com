<div class="hgroup"><h1 class="title"><span class="title">Ostatní uživatelů nemohou upravovat síťová připojení</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud můžete upravit nastavení sítě, ale ostatní nemohou, můžete chtít aby bylo připojení <span class="gui">dostupné všem uživatelům</span>. To znamená že se všichni uživatelé počítače mohou <span class="em">připojit</span>, ale jen <span class="link"><a href="user-admin-explain.html" title="Jak funguje oprávnění správce?">administrátoři</a></span> ho mohou měnit.</p>
<p class="p">Důvod je ten, že pokud nastavení ovlivňuje všechny uživatele, jenom důvěryhodná (administrátor) osoba by měla mít možnost ho měnit.</p>
<p class="p">Pokud i ostatní uživatelé opravdu musí připojení měnit, <span class="em">nepovolujte</span> dostupnost pro všechny uživatele. Poté bude mít každý uživatel své vlastní připojení místo jednoho globálně platného nastavení.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Zrušení sdílení připojení</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="gui">nabídku sítí</span> na horním panelu a vyberte <span class="gui">Upravit připojení</span>.</p></li>
<li class="steps"><p class="p">Najděte připojení pro které chcete aby si každý uživatel nastavil sám. Vyberte ho a klikněte na <span class="gui">Upravit</span>.</p></li>
<li class="steps"><p class="p">Budete muset zadat své administrátorské heslo pro úpravu připojení. Pouze root to může udělat.</p></li>
<li class="steps"><p class="p">Zrušte volbu <span class="gui">Dostupné pro všechny uživatele</span> a klikněte na <span class="gui">Uložit</span>. Ostatní uživatelé nyní budou mít možnost si spravovat svá připojení sami.</p></li>
</ol></div>
