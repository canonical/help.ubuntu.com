<div class="hgroup"><h1 class="title"><span class="title">Bezpečnost na Internetu</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">A possible reason for why you are using Ubuntu is the robust security that Linux based
systems are known for. One reason that Linux is relatively safe from malware and
viruses is due to the lower number of people who use it.
Viruses are targeted at popular operating systems like Windows, that have an extremely large
user base. Linux based systems are also very secure due to their open source nature, which allows
experts to modify and enhance the security features included with each distribution.</p>
<p class="p">Despite the measures taken to ensure that your installation of Ubuntu is secure, there
are always vulnerabilities. As an average user on the internet you can still be susceptible to:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Phishing a scam (webové stránky a emaily, které se snaží podvodem vylákat citlivé informace)</p></li>
<li class="list"><p class="p"><span class="link"><a href="net-email-virus.html" title="Měl bych skenovat své emaily kvůli virům?">Přeposílání škodlivých emailů</a></span></p></li>
<li class="list"><p class="p"><span class="link"><a href="net-antivirus.html" title="Potřebuji antivirový software?">Aplikace se zákeřnými úmysly (viry)</a></span></p></li>
<li class="list"><p class="p"><span class="link"><a href="net-wireless-wepwpa.html" title="Co znamená WEP a WPA?">Unauthorized remote/local network access</a></span></p></li>
</ul></div></div></div>
<p class="p">Abyste zůstali v bezpečí i online byste se měli držet následujících doporučení:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Mějte se na pozoru před emaily, přílohami a odkazy, které vám poslali lidé, které neznáte.</p></li>
<li class="list"><p class="p">Pokud webová stránka nabízí víc než je uvěřitelné, nebo žádá citlivé údaje které zjevně nepotřebuje, dvakrát se rozmyslete než údaje zadáte a odešlete. Je možné že budou zneužity zloději identit a dalšími kriminálníky.</p></li>
<li class="list"><p class="p">Buďte opatrní při zadávání <span class="link"><a href="user-admin-explain.html" title="Jak funguje oprávnění správce?">rootovského oprávnění</a></span>, zejména pokud to děláte poprvé nebo u aplikace kterou dobře neznáte. Poskytnutím rootovského oprávnění se vystavujete vysokému riziku napadení.</p></li>
<li class="list"><p class="p">ujistěte se, že používáte pouze nezbytné aplikace pro vzdálený přístup. Spuštětné SSH nebo VNC může být užitečné, ale zároveň necháváte počítač náchylný k napadení, není-li ovšem dobře zabezpečen. Zvažte zapnutí <span class="link"><a href="net-firewall-on-off.html" title="Povolit nebo zakázat firewall.">firewallu</a></span> pro lepší ochranu počítače.</p></li>
</ul></div></div></div>
