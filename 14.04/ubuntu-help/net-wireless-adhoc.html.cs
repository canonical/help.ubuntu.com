<div class="hgroup"><h1 class="title"><span class="title">Vytvořte bezdrátový přístupový bod.</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Váš počítač můžete použít jako přístupový bod pro ostatní zařízení. Tato zařízení pak mohou sdílet internetové připojení, ať už po kabelu nebo bezdrátem.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na horním panelu a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Otevřte <span class="gui">Síť</span> a vlevo vyberte <span class="gui">Bezdrátové</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Vytvořit přístupový bod</span>.</p></li>
<li class="steps"><p class="p">Pokud již jste připojeni k nějaké bezdrátové síti, budete dotázání zda se od ní chcete odpojit. Jedna WiFi karta může být buď připojena, nebo může připojení nabízet, ale nemůže to dělat najednou. Potvrďte kliknutím na <span class="gui">Vytvořit přístupový bod</span></p></li>
</ol></div></div></div>
<p class="p">Název sítě (SSID) a bezpečnostní klíč budou automaticky vygenerovány. Název sítě je založen na názvu počítače. Ostatní zařízení potřebují tyto inforamce pro korektní připojení k vytvořené síti.</p>
</div>
