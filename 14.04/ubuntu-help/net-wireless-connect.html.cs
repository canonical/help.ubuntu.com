<div class="hgroup"><h1 class="title"><span class="title">Připojení k bezdrátové síti</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud máte počítač schopný připojení k bezdrátovým sítím, můžete se připojit k takovým sítím, které jsou v dosahu a přistupovat s jejich pomocí k Internetu, procházet soubory sdílené po síti a tak podobně.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Pokud na počítači máte hardwarový přepínač bezdrátové sítě, ujistěte se, že je v poloze zapnuto.</p></li>
<li class="steps">
<p class="p">Klikněte na <span class="gui">nabídku sítí</span> na <span class="gui">horním panelu</span> a vyberte síť, ke které se chcete připojit.</p>
<p class="p">Pokud není název sítě v seznamu, vyberte <span class="gui">Další sítě</span> a ujistěte se, že není ani níže v tomto seznamu. Pokud stále síť nevidíte, můžete být mimo dosah nebo se jedná o <span class="link"><a href="net-wireless-hidden.html" title="Připojení k skryté bezdrátové síti">skrytou síť</a></span>.</p>
</li>
<li class="steps">
<p class="p">Pokud je síť chráněna heslem (<span class="link"><a href="net-wireless-wepwpa.html" title="Co znamená WEP a WPA?">šifrovacím klíčem</a></span>), vložte po upozornění heslo a klikněte na <span class="gui">Připojit</span>.</p>
<p class="p">Pokud neznáte klíč, měl by být napsán na zadní straně routeru nebo v jeho manuálu, nebo se zeptejte správce bezdrátové sítě.</p>
</li>
<li class="steps"><p class="p">Ikona připojení k síti měni své chování když se počítač zrovna připojuje.</p></li>
<li class="steps"><p class="p">Pokud je připojení úspěšné, ikona se změní na několik sloupečků - čím více sloupečků, tím lepší signál. Pokud je jeich málo, může být připojení pomalé a nebo nestabilní.</p></li>
</ol></div></div></div>
<p class="p">If the connection is not successful, you <span class="link"><a href="net-wireless-noconnection.html" title="Zadal jsem správné heslo, ale stále se nemohu připojit">may be asked for your password again</a></span> or it might just tell you that the connection has been disconnected. There are a number of things that could have caused this to happen. You could have entered the wrong password, the wireless signal could be too weak, or your computer's wireless card might have a problem, for example. See <span class="link"><a href="net-wireless-troubleshooting.html" title="Řešení problémů s bezdrátovými sítěmi">Řešení problémů s bezdrátovými sítěmi</a></span> for more help.</p>
<p class="p">Silnější připojení automaticky neznamená rychlejší internet nebo stahování. Každé připojení k <span class="em">zařízení poskytujícímu internetové připojení</span> je jiné a může dosahovat jiných rychlostí.</p>
</div>
