<div class="hgroup"><h1 class="title"><span class="title">Proč se moje bezdrátová síť stále odpojuje?</span></h1></div>
<div class="region">
<div class="contents"><p class="p">Někdy se stane, že vás síť odpojí ikdyž chcete být stále pripojen(a). Počítač se okamžitě pokusí znovu připojit (ikona sítě bude pulzovat), ale může to být nepříjemné.</p></div>
<div id="signal" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Slabý bezdrátový signál</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Běžný důvod odpojení je slabý signál. Bezdrátové sítě mají určitý dosah, a pokud jste moc daleko, spojení nemusí být stabilní. Zdi a jiné objekty mezi vámi a vysílačem výrazně tlumí signál.</p>
<p class="p">Ikona Sítě indikuje sílu signálu. Pokud je slabý, zkuste se přesunout blíže k vysílači signálu.</p>
</div></div>
</div></div>
<div id="network" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Problémy při připojování do sítě</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Někdy, když se nemůžete připojit, se připojení na naváže a v okamžiku počítač zase odpojí. Toto se může stát, pokud se připojení z nějakého důvodu podařilo jen částečně a dokončení neproběhlo dle očekávání.</p>
<p class="p">Jedním z možných důvodů je špatné heslo, nebo počítači není povolen přístup do sítě (například je vyžadováno uživatelské jméno).</p>
</div></div>
</div></div>
<div id="hardware" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Nespolehlivý bezdrátový hardware/ovladače</span></h2></div>
<div class="region"><div class="contents"><p class="p">Některá síťová zařízení jsou nespolehlivá. Bezdrátové sítě jsou kompikované a občas se může stát, že zařízení kvůli nějakému problému zruší spojení. Pokud se čas od času připojení restartuje, toto může být pravá příčina. Pokud se to stává opravdu často, zvažte výměnu hardwaru.</p></div></div>
</div></div>
<div id="busy" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">zaneprázdněné bezdrátové sítě</span></h2></div>
<div class="region"><div class="contents"><p class="p">Na rušných místech s bezdrátovým připojením (kavárny, univerzity, ...) může být v jednu chvíli mnoho počítačů snažících se o přístup k internetu. Někdy je síť tak vytížená, že nemůže obsloužit všechny počítače a některé z nich odpojí.</p></div></div>
</div></div>
