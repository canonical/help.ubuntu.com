<div class="hgroup"><h1 class="title"><span class="title">Nemohu v seznamu najít svou síť</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Existuje mnoho důvodů proč vaše bezdrátová síť není v seznamu objevivším se po kliknutí na ikonu sítě.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Pokud v seznamu nejsou žádné sítě, váš hardware může být vypnutý nebo <span class="link"><a href="net-wireless-troubleshooting.html" title="Řešení problémů s bezdrátovými sítěmi">rozbitý</a></span>. Ujistěte se, že je zapnutý.</p></li>
<li class="list"><p class="p">Pokud je v okolí příliš mnoho sítí, vaše síť nemusí být na první straně seznamu. V tomto případě přesuňte myš na šipku na konci seznamu pro zobrazení ostatních bezdrátových sítí.</p></li>
<li class="list"><p class="p">Je možné, že jste mimo dosah sítě. Zkuste se přesunout blíže k routeru/vysílači a podívejte se, jestli se síť po chvíli objeví v seznamu.</p></li>
<li class="list"><p class="p">Seznamu sítí chvíli trvá, než se aktualizuje. Pokud jste právě zapnuli počítač nebo se přesunuli na jiné místo, chvíli počkejte než zkontrolujete, zda se síť objevila v seznamu.</p></li>
<li class="list"><p class="p">Síť může být skryta. Ke skrytým sítím se musíte <span class="link"><a href="net-wireless-hidden.html" title="Připojení k skryté bezdrátové síti">připojovat jiným způsobem</a></span>.</p></li>
</ul></div></div></div>
