<div class="hgroup"><h1 class="title"><span class="title">Nižší spotřeba energie a prodloužení běhu počítače na baterii</span></h1></div>
<div class="region">
<div class="contents"><p class="p">Počítače mohou požadovat mnoho energie. Pomocí několika jednoduchých strategií pro šetření energií můžete snížit svůj účet za elektřinu a pomoci životnímu prostředí. Pokud máte notebook pomůže vám to také prodloužit běh počítače na baterii.</p></div>
<div id="general" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Obecné tipy</span></h2></div>
<div class="region"><div class="contents"><div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Když počítač nepoužíváte, <span class="link"><a href="shell-exit.html#suspend" title="Uspávání">uspěte jej</a></span>. Tím výrazně zredukujete využití energie a stále jej můžete velmi rychle probudit a pokračovat v práci.</p></li>
<li class="list"><p class="p"><span class="link"><a href="shell-exit.html#shutdown" title="Vypnutí nebo restartování">Turn off</a></span> the computer when you
    will not be using it for longer periods. Some people worry that turning off
    a computer regularly may cause it to wear out faster, but this is not the
    case.</p></li>
<li class="list"><p class="p">pro změnu nastavení napájení použijte nastavení <span class="gui">Napájení</span> v <span class="app">Nastavení systému</span>. Je tam mnoho možností k ušetření energie: můžete <span class="link"><a href="display-dimscreen.html" title="Nastavení jasu obrazovky">automaticky vypnout</a></span> display po určité době; <span class="link"><a href="display-dimscreen.html" title="Nastavení jasu obrazovky">snížit podsvícení</a></span> (pro notebooky); a nechat počítač po určité době <span class="link"><a href="power-suspend.html" title="Co se stane, když uspím svůj počítač?">automaticky uspat</a></span> pokud ho určitou dobu nepoužíváte.</p></li>
<li class="list"><p class="p">Vypněte jakákoli externí zařízení (např. tiskárny a skenery), nejsou-li používána.</p></li>
</ul></div></div></div></div></div>
</div></div>
<div id="laptop" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Notebooky, netbooky a další zařízení s bateriemi</span></h2></div>
<div class="region"><div class="contents"><div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list">
<p class="p"><span class="link"><a href="display-dimscreen.html" title="Nastavení jasu obrazovky">Snižte jas obrazovky</a></span>; napájení obrazovky má velký podíl na spotřebě energie přenosných počítačů.</p>
<p class="p">Většina přenosných počítačů má na klávesnici klávesy (nebo klávesové zkratky) sloužící ke změně jasu.</p>
</li>
<li class="list">
<p class="p">Jestliže nepotřebujete internetové připojení, vypněte WiFi/Bluetooth adaptér. Tato zařízení mají velkou spotřebu energie.</p>
<p class="p">Některé počítače mají fyzické přepínače, které můžete použít pro vypnutí těchto zařízení, jiné využívají klávesové zkratky. Až budete připojení potřebovat, můžete je opět zapnout.</p>
</li>
</ul></div></div></div></div></div>
</div></div>
<div id="advanced" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Pokročilé tipy</span></h2></div>
<div class="region"><div class="contents"><div class="list"><div class="inner"><div class="region"><ul class="list"><li class="list">
<p class="p">Omezte počet operací, které jsou prováděny na pozadí. Když mají počítače více práce, spotřebovávají více energie.</p>
<p class="p">Většina běžících aplikací nedělá téměř nic, když je aktivně nepoužíváte. Nicméně aplikace stahující data z internetu, přehrávající hudbu nebo filmy mohou mít na spotřebu energie vliv.</p>
</li></ul></div></div></div></div></div>
