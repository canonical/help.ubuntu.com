<div class="hgroup"><h1 class="title"><span class="title">Proč se počítač vypíná při dosažení 10% kapacity baterie?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Když je baterie nabitá příliš málo, počítač se automaticky <span class="link"><a href="power-suspend.html" title="Co se stane, když uspím svůj počítač?">uspí</a></span>. Tím se zajistí, že se baterie kompletně nevybije, její vybití ji ničí. Pokud se baterie úplně vybije, počítač se nestihne korektně vypnout.</p>
<p class="p">You can change what happens when the battery level gets too low. First, click the 
<span class="gui">battery menu</span> in the <span class="gui">menu bar</span> and select <span class="gui">Power Settings</span>. At the <span class="gui">'When
power is critically low'</span> setting, you can choose for the computer to
suspend, <span class="link"><a href="power-hibernate.html" title="Jak mohu uspat svůj počítač na disk?">hibernate</a></span>, or shut down. If you choose shut down, your applications and
documents <span class="em">will not</span> be saved when the computer turns off.</p>
<p class="p">Některé počítače mají problém s uspáváním a nejsou schopné se probudit, takže musíte vaše data a aplikace otevřít znovu po zapnutí počítače. V takovém případě je možné že nějaká data ztratíte pokud si je předtím neuložíte. Zkuste si <span class="link"><a href="power-suspendfail.html" title="Proč se můj počítač neprobudí po uspání do paměti?">opravit problém s uspáváním</a></span>.</p>
