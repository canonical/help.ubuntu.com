<div class="hgroup"><h1 class="title"><span class="title">Můj počítač nejde zapnout</span></h1></div>
<div class="region">
<div class="contents"><p class="p">Existuje několik důvodů, proč počítač nemusí jít zapnout. V tomto tématu naleznete stručný přehled možných důvodů.</p></div>
<div id="nopower" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Počítač není připojen, baterie je prázdná nebo jsou špatně zapojeny kabely</span></h2></div>
<div class="region"><div class="contents"><p class="p">Ujistěte se, že napájecí kabely jsou správně zapojené a zařízení zapnutá. Zkontrolujte také monitor. Máte-li notebook, zapojte napájecí kabel. Ujistěte se, že vaměnitelná baterie je na správném místě.</p></div></div>
</div></div>
<div id="hardwareproblem" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Problém s hardwarem</span></h2></div>
<div class="region"><div class="contents"><p class="p">Nějaká součást počítače může být poškozená či nefunkční. V takovém případě nechte svůj počítač opravit. Běžné závady jsou rozbité napájení, špatně zapojené součátky a vadná základní deska.</p></div></div>
</div></div>
<div id="beeps" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Počítač pípá a poté se vypne</span></h2></div>
<div class="region"><div class="contents"><p class="p">Když váš počítač při zapínání několikrát pípne, znamená to že je v něm nějaký hardwarový problém. Tato pípíní jsou občas nazývána <span class="em">beep codes</span> a jejich počet a struktura značí co se stalo za problém. Různí výrobci používají různé kódy, podívejte se do návodu k základní desce nebo nechte počítač opravit.</p></div></div>
</div></div>
<div id="fans" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Větráky se točí ale na obrazovce není nic vidět</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Nejprve zkontrolujte zda je monitor správně připojen k počítači, elektřině a je zapnutý.</p>
<p class="p">Problém může být také způsoben poruchou hardwaru. Když zapnete počítač, větráky se roztočí ale ostatní důležité části mohou být poškozené. Odneste počítač do servisu.</p>
</div></div>
