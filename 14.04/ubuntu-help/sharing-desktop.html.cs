<div class="hgroup"><h1 class="title"><span class="title">Sdílejte svou plochu</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete nechat ostatní lidi ovládat vaši plochu z jiného počítače díky aplikaci přenášející vaši plochu. Nastavte <span class="app">Vzdálenou plochu</span> pro povolení přístupu k vašemu desktopu a její zabezpečení.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">V nabídce <span class="gui">Dash</span> otevřete <span class="app">Sdílení plochy</span>.</p></li>
<li class="steps"><p class="p">Chcete-li <span class="gui">nechat ostatní připojit se k tomuto počítači</span>, vyberte tuto volbu. To znamená, že lidé budou moci vidět vaši plochu.</p></li>
<li class="steps"><p class="p">Chcete-li aby také mohli pracovat s vaším počítačem, vyberte příslušnou volbu. Toto umožní ostatním pracovat s vaším kurzorem, spouštět aplikace a procházet soubory dle bezpečnostního nastavení které zrovna používáte.</p></li>
</ol></div></div></div>
</div>
<div id="security" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zabezpečení</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Je důležité porozumět všem důsledkům každé bezpečnostní volby před jejím změněním.</p>
<div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Potvrzení přístupu k počítači</dt>
<dd class="terms">
<p class="p">Chcete-li si vybrat zda se někdo může připojit k vašemu počítači, vyberte <span class="gui">Vyžadovat povolení k přístupu k tomuto počítači</span>. Pokud tuto volbu zrušíte, nebudete moci rozhodnout zda se k vám může někdo připojit.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Tato možnost je ve výchozím stavu povolena.</p></div></div></div></div>
</dd>
<dt class="terms">Povolit heslo</dt>
<dd class="terms">
<p class="p">Vyžadujete-li heslo pro přístup k vašemu počítači, vyberte <span class="gui">Vyžadovat heslo pro připojení</span>. Pokud tuto volbu zrušíte, kdokoli se bude moci bez hesla připojit.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Tato volba je ve výchozím nastavení vypnutá, ale můžete ji povolit a nastavit heslo.</p></div></div></div></div>
</dd>
<dt class="terms">Povolení přístupu k počítači přes internet</dt>
<dd class="terms">
<p class="p">Pokud váš router podporuje protokol UPnP a je povolený, můžete povolit připojení lidem z i z jiných sítí. Pro povolení vyberte <span class="gui">Automaticky nastavit router na otevření a přeposílání portů</span></p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Tato možnost je ve výchozím stavu zakázána.</p></div></div></div></div>
</dd>
</dl></div></div></div>
</div></div>
</div></div>
<div id="notification-icon" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zobrazit ikonu v oznamovací oblasti</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Chcete-li mít možnost návštěvníky odpojit, vyberte tuto volbu. Vyberete-li <span class="gui">Vždy</span>, tato ikona bude vždy viditelná nezávisle na tom zda je někdo připojen.</p>
<div class="note note-warning" title="Varování"><div class="inner"><div class="region"><div class="contents"><p class="p">Je-li tato volba vypnutá, bude možné se připojit i bez vašeho vědomí dle nastavení zabezpečení.</p></div></div></div></div>
