<div id="shutdown" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Vypnutí nebo restartování</span></h2></div>
<div class="region">
<div class="contents">
<p class="p">Pokud chcete počítač zcela vypnout nebo provést úplný restart, klikněte na <span class="gui">systémovou nabídku</span> a vyberte <span class="gui">Vypnout</span>.</p>
<p class="p">Pokud jsou přihlášeni i jiní uživatelé, systém vám nemusí vypnutí nebo restartování povolit, protože by se tím ukončila jejich sezení. Pokud jste uživatel s oprávněními správce, můžete být před vypnutí požádáni o heslo.</p>
