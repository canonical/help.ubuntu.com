<div class="hgroup"><h1 class="title"><span class="title">Odhlášení, vypínání a přepínání uživatelů</span></h1></div>
<div class="region">
<div class="contents"><p class="p">Když skončíte práci s počítačem, můžete jej vypnout, uspat (pro šetření energií) nebo ponechat zapnutý a pouze se odhlásit.</p></div>
<div id="logout" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Odhlášení nebo přepínání uživatelů</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pokud chcete, aby mohl váš počítač používat další uživatel, můžete se buď odhlásit, nebo zůstat přihlášeni a pouze přepnout uživatele. Pokud přepnete uživatele, všechny vaše aplikace zůstanou spuštěny a když se znovu přihlásíte, vše zůstane tak, jak jste to zanechali.</p>
<p class="p">Pro odhlášení nebo přepnutí uživatele klikněte na <span class="link"><a href="unity-menubar-intro.html" title="Ovládání aplikací &amp; nastavení v hlavním panelu">systémovou nabídku</a></span> zcela vpravo na horním panelu a vyberte patřičnou položku.</p>
</div></div>
</div></div>
<div id="lock-screen" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Uzamčení obrazovky</span></h2></div>
<div class="region">
<div class="contents">
<p class="p">Pokud na krátkou dobu opustíte počítač, měli byste uzamknout obrazovku, aby ostatní lidé nemohli přistupovat k vašim souborům a běžícím aplikacím. Když se vrátíte jednoduše zadáte své heslo pro opětovné přihlášení. Pokud obrazovku neuzamknete, automaticky se uzamkne po uplynutí určité doby.</p>
<p class="p">Pro uzamčení obrazovky klikněte na <span class="gui">systémovou nabídku</span> v horním panelu a vyberte <span class="gui">Uzamknout obrazovku</span>.</p>
<p class="p">Když je vaše obrazovka uzamčena, ostatní uživatelé se mohou přihlásit ke svým účtům, pokud v dialogu pro zadání hesla zvolí <span class="gui">Přepnout uživatele</span>.  Poté, co skončí, můžete se přepnout zpět do svého prostředí.</p>
</div>
