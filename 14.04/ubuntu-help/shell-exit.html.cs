<div id="suspend" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Uspávání</span></h2></div>
<div class="region">
<div class="contents">
<p class="p">Chcete-li šetřit energii, uspěte počítač když ho nepoužíváte. Používáte-li notebook, Ubuntu se automaticky uspí po zavření víka. Uspání uloží stav počítače do poměti a vypne většinu funkcí. Během spánku se spotřebovává velmi málo energie.</p>
<p class="p">Chcete-li uspat počítač ručně, klikněte na systémové menu a vyberte <span class="gui">Uspat do paměti</span></p>
</div>
