<div class="hgroup"><h1 class="title"><span class="title">Maximalizace a minimalizace okna</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">You can maximize a window to take up all of the space on your desktop
  and unmaximize a window to restore it to its normal size. You can also
  maximize windows vertically along the left and right sides of the 
  screen, so you can look at two windows at once. See
  <span class="link"><a href="shell-windows-tiled.html" title="Dlaždice">Dlaždice</a></span> for details.</p>
<p class="p">To maximize a window, grab the titlebar and drag it to the top of the
  screen, or double-click the titlebar. To maximize a window using the 
  keyboard, hold down <span class="key"><kbd>Ctrl</kbd></span> and <span class="key"><kbd><span class="link"><a href="windows-key.html" title='Která klávesa je "Super"?'>Super</a></span></kbd></span> and press 
  <span class="key"><kbd>↑</kbd></span>.</p>
<p class="p">Obnovit původní velkost okna můžete buď dvojklikem, nebo odtažením od krajů obrazovky. Můžete také použít zkratku <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd><span class="link"><a href="windows-key.html" title='Která klávesa je "Super"?'>Super</a></span></kbd></span>+<span class="key"><kbd>↓</kbd></span></span>.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Podržte klávesu <span class="key"><kbd>Alt</kbd></span> a myší přetáhněte okno do požadované pozice.</p></div></div></div></div>
