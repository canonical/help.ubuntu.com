<div class="hgroup"><h1 class="title"><span class="title">Přepínání mezi pracovními plochami</span></h1></div>
<div class="region">
<div class="contents">
<div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Myší:</dt>
<dd class="terms"><p class="p">Přejděte na <span class="link"><a href="unity-launcher-intro.html" title="Použití Launcheru pro spouštění aplikací">Launcher</a></span> a klikněte na <span class="gui">přepínač ploch</span> v dolní části Launcheru. Pro přepnutí se na konkrétní plochu poklepejte na vybrané okno nebo plochu nebo, pro návrat na předchozí plochu, stiskněte přepínač oken ještě jednou.</p></dd>
<dt class="terms">Klávesnicí:</dt>
<dd class="terms"><div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Stiskněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>→</kbd></span></span> k přesunu na plochu, která je napravo od stávající plochy.</p></li>
<li class="list"><p class="p">Stiskněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>←</kbd></span></span> k přesunu na plochu, která je vlevo od stávající plochy.</p></li>
<li class="list"><p class="p">Stiskněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>↓</kbd></span></span> k přesunu na plochu, která je pod stávající plochou.</p></li>
<li class="list"><p class="p">Stiskněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>↑</kbd></span></span> k přesunu na plochu, která je nad stávající plochou.</p></li>
</ul></div></div></div></dd>
</dl></div></div></div>
