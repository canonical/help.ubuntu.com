<div class="hgroup"><h1 class="title"><span class="title">Slyšitelné rušení nebo praskání při přehrávání hudby.</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Slyšíte-li praskání či rušení při přehrávání hudby, máte pravděpodobně špatně zapojené nebo nekvalitní zvukové kabely, nebo problém s ovladačem zvukové karty.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list">
<p class="p">Zkontrolujte, zda-li jsou reproduktory zapojeny správně.</p>
<p class="p">Jestliže nejsou reproduktory zapojeny správně, nebo jsou zapojeny do špatné zdířky, tak je pravděpodobné, že uslyšíte bzučivý zvuk.</p>
</li>
<li class="list">
<p class="p">Ujistěte se, že kabel od mikrofonu/sluchátek není poškozen.</p>
<p class="p">Audio kabeláž a konektory se mohou časem poškodit. Zkuste je připojit k jinému zařízení (například k MP3 přehrávači nebo CD přehrávači) a zkontrolujte, jestli slyšíte praskavý zvuk. Jestliže ano, budete tuto kabeláž muset vyměnit.</p>
</li>
<li class="list">
<p class="p">Překontrolujte, zda-li zvukové ovladače nejsou příliš dobré.</p>
<p class="p">Některé karty nespolupracují s Linuxem příliš dobře, protože nemají kvalitní ovladače. Tento problém je poměrně složitý na identifikace. Pokuste se nalézt typ své karty na internetu; k hledanému termínu přidejte slovo "Ubuntu", tak se dozvíte, jestli mají i ostatní stejný problém.</p>
<p class="p">Pro více informací o své zvukové kartě můžete v <span class="app">Terminálu</span> spustit příkaz <span class="cmd">sudo lspci -v</span>.</p>
</li>
</ul></div></div></div>
