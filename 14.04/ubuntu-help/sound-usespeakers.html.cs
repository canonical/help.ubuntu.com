<div class="hgroup"><h1 class="title"><span class="title">Použití jiných reproduktorů nebo sluchátek</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete použít externí reproduktory nebo sluchátka. Reproduktory jsou obvykle připojeny přes jack nebo přes USB.</p>
<p class="p">Jestliže mají reproduktory nebo sluchátka připojení přes jack, zapojte je do tomu určených zdířek. Většina počítačů má dvě zdířky: jednu pro mikrofon a druhou pro reproduktory. Hledejte zobrazení reproduktoru na zdířce. Reproduktory, které jsou připojeny přes jack, budou obvykle nastaveny jako výchozí. Jestliže ne, tak postupujte podle následujících instrukcí, které vám umožní nastavit výchozí zařízení.</p>
<p class="p">Některé počítače podporují vícekanálový výstup pro prostorový zvuk. Obvykle používají více jacků, často červeně obrvené. Pokud si nejste jisti zapojením, zkuste si poslechnout výstupy pro různá nastavení. Klikněte na ikonu zvukum <span class="gui">Nastavení zvuku</span>, vyberte správné reproduktory ze seznamu zařízení a <span class="gui">Test zvuku</span>. Ve vyskakovacím okně klikněte na tlačítko pro každý reproduktor. Přeskupte reproduktory tak, aby odpovídaly rozložení na obrazovce, nebo odpovídajícím způsobem přepojte kabely.</p>
<p class="p">Jestliže mát USB reproduktory nebo sluchátka, případně analogová sluchátka zapojená do USB zvukové karty, zapojte je do USB portu. USB reproduktory se budou identifikovat jako zvláštní zařízení a bude třeba je nastavit jako výchozí.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Veberte výchozí audio vstupní zařízení</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="gui">nabídku zvuku</span> v <span class="gui">horním panelu</span> a vyberte <span class="gui">Předvolby zvuku</span>.</p></li>
<li class="steps"><p class="p">Na záložce <span class="gui">Výstup</span> vyberte zařízení ze se seznamu.</p></li>
</ol></div>
</div></div>
<p class="p">Jestliže nevidíte zařízení na záložce <span class="gui">Výstup</span>, překontrolujte záložku <span class="gui">Hardware</span>. Vyberte zařízení a vyzkoušejte odlišný profil.</p>
</div>
