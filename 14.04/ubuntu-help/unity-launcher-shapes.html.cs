<div class="hgroup"><h1 class="title"><span class="title">Co znamenají různé tvary a barvy jednotlivých ikon v Launcheru?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Při spuštění aplikace ikona Launcheru pulzuje, dává tím najevo, že Ubuntu právě spouští aplikaci. Toto je užitečné, protože některé aplikace se spustí hned a u jiných to trvá delší dobu.</p>
<p class="p">Jakmile aplikace ukončí spouštění, objeví se v levé části malý <span class="em">bílý trojúhelník</span>. Dva trojúhelníky znamenají, že jsou spuštěny dvě okna stejné aplikace. Jestliže je otevřeno více oken, zobrazí se tři trojúhelníky.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Apps that aren't currently running have translucent Launcher icon squares.
    When an app is running, the Launcher icon square is full of color.</p></div></div></div></div>
</div>
<div id="launcher-notifications" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Oznámení</span></h2></div>
<div class="region"><div class="contents">
<p class="p">If an app wants your attention to notify you of something (like a finished download),
    the Launcher icon will wiggle and glow and the white triangle will become <span class="em">blue</span>.
    Click the Launcher icon to dismiss the notification.</p>
<p class="p">Aplikace mohou také zobrazovat <span class="em">číslo</span> na své ikoně v Launcheru. Aplikace pro rychlé posílání zpráv toho využívají k zobrazení počtu nepřečtených zpráv. <span class="gui">Aktualizace softwaru</span> pro zobrazení počtu dostupných aktualizací.</p>
<p class="p">Aplikace mohou také použít <span class="em">indikátor průběhu</span> pro označení jak probíhá nějaká akce bez nutnosti mít otevřené okno.</p>
</div></div>
