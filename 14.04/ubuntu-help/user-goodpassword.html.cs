<div class="hgroup"><h1 class="title"><span class="title">Volba bezpečného hesla</span></h1></div>
<div class="region">
<div class="contents">
<div class="note note-important" title="Důležité"><div class="inner"><div class="region"><div class="contents"><p class="p">Vaše heslo by mělo být snadné na zapamatování, ale složité na uhádnutí pro ostatní (včetně počítačových programů).</p></div></div></div></div>
<p class="p">Volba dobrého hesla vám pomůže udržet váš počítač v bezpečí. Pokud je vaše heslo snadno uhádnutelné, může se někdo dostat k vašim osobním informacím.</p>
<p class="p">Někteří lidé se mohou pomocí počítače snažit systematicky uhádnout vaše heslo. Co může být složité pro člověka, nemusí být složité pro počítačový program. Zde je několik tipů pro výběr dobrého hesla:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list">
<p class="p">
        Use a mixture of upper-case and lower-case letters, numbers, symbols, 
        and spaces in the password. This makes it more difficult to guess. There 
        are more symbols to choose from, so more possible passwords would have 
        to be checked by someone when trying to guess yours.
      </p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">
          A good method for choosing a password is to take the first letter of 
          each word in a phrase that you can remember. The phrase could be the 
          name of a movie, a book, a song, or an album. For example, "Flatland: 
          A Romance of Many Dimensions" would become F:ARoMD or faromd or f: 
          aromd.
        </p></div></div></div></div>
</li>
<li class="list"><p class="p">Vytvořte si heslo, tak dlouhé, jak je to možné. Čím více znaků obsahuje, tím déle by mělo trvat osobě nebo počítači uhodnout ho.</p></li>
<li class="list"><p class="p">Nepoužívejte žádná slova žádného jazyka. Toto zkouší útočníci jako první možnost. Světově nejpoužívanější heslo je "password" - lidé takto triviální hesla velmi rychle uhodnou!</p></li>
<li class="list"><p class="p">Nepoužívejte jakékoli osobní informace, např. datum narození, číslo státní poznávací značky nebo jména členů rodiny.</p></li>
<li class="list"><p class="p">Nepoužívejte žádná podstatná jména.</p></li>
<li class="list">
<p class="p">Vybírejte si heslo, které je možné napsat rychle, abyste předešli možnému úniku hesla tím, že vás bude někdo sledovat.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Nikdy si hesla nikam nepište. Může je někdo najít!</p></div></div></div></div>
</li>
<li class="list"><p class="p">Používejte odlišná hesla pro odlišné věci.</p></li>
<li class="list">
<p class="p">Používejte odlišná hesla pro odlišné účty.</p>
<p class="p">Pokud používáte stejné heslo pro všechny své účty a někdo ho uhádne, bude mít okamžitě přístup ke všem vašim účtům.</p>
<p class="p">
        It can be difficult to remember lots of passwords. Though not as secure 
        as using a different passwords for everything, it may be easier to use 
        the same one for things that don't matter (like websites), and different 
        ones for important things (like your online banking account and your 
        email).
      </p>
</li>
<li class="list"><p class="p">Pravidelně měňte hesla.</p></li>
</ul></div></div></div>
