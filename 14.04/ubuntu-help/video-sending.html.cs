<div class="hgroup"><h1 class="title"><span class="title">Ostatní nemohou přehrát videa, která jsem vytvořil</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud jste vytvořili video na počítači s Linuxem a poslali jej někomu s Windows nebo Mac OS, možná zjistíte že mají problémy s přehráváním videa.</p>
<p class="p">Pro správné přehrání videa musí ten, komu jej pošlete, mít nainstalován správné <span class="em">kodeky</span>. Kodek je malý kus softwaru, který umožňuje vzít a přehrát video. Existuje mnoho různých formátů videa a každý pro přehrání vyžaduje jiný kodek. Zjistit, jaký formát má vaše video můžete pomocí:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Otevřete <span class="link"><a href="files-browse.html" title="Procházení souborů a složek">správce souborů</a></span>.</p></li>
<li class="list"><p class="p">Klikněte pravým tlačítkem na video soubor a vyberte <span class="gui">Vlastnosti</span>.</p></li>
<li class="list"><p class="p">Přejděte na záložku <span class="gui">Zvuk/video</span> a podívejte se, jaký <span class="gui">kodek</span> je napsán v části <span class="gui">Video</span>.</p></li>
</ul></div></div></div>
<p class="p">Zeptejte se osoby, která má problém s přehráním videa, jestli má nainstalovaný správný kodek. Je to užitečné pro vyhledání kodeku a aplikace pro přehrání na webu. Například, pokud vaše video používá formát <span class="em">Theora</span> a máte přítele, který používá Windows Media Player zkuste hledat "theora windows media player". Většinou se vám podaří stáhnout správný kodek zdarma, pokud není nainstalován.</p>
<p class="p">Jestliže nemůžete nalézt správný kodek, vyzkoušejte <span class="link"><a href="http://www.videolan.org/vlc/" title="http://www.videolan.org/vlc/">VLC media player</a></span>. Funguje stejně dobře na Linuxu jako na Windows nebo Mac OS, podporuje mnoho různých video formátů. Jinak se budete muset pokusit konvertovat video do odlišného video formátu. Mnoho video editorů to umí a jsou dostupné specifické video konvertory. Koukněte se do <span class="app">Centra Softwaru pro Ubuntu</span>, kde uvidíte, co je dostupné.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Existuje několik dalších problémů, které by mohly někomu zabránit v přehrávání videa. Video mohlo být poškozeno během posílání (velké soubory někdy nejsou kopírovány správně), aplikace pro přehrávání videa může obsahovat chyby, nebo video nemusí být vytvořeno správně (mohli nastat některé chyby při ukládání videa).</p></div></div></div></div>
