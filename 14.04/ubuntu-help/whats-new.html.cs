<div class="hgroup"><h1 class="title"><span class="title">Co je nového v Ubuntu 14.04?</span></h1></div>
<div class="region">
<div class="contents"><p class="p">Ubuntu 14.04 LTS continues the evolution of the <span class="em">Unity</span> interface. Below are
a few highlights of changes since Ubuntu 12.04 LTS.</p></div>
<div id="new-features" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Nové funkce a vlastnosti:</span></h2></div>
<div class="region"><div class="contents"><div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">The menus of the currently focused application are by default available
    in Ubuntu's integrated <span class="link"><a href="unity-menubar-intro.html#app-menus" title="Nabídky aplikací">menu bar</a></span>.
    However, if you want you can switch to the conventional style and show the app
    menus in the window title bar of respective application instead (this latter
    option is new since Ubuntu 13.10).</p></li>
<li class="list"><p class="p">Support added for scaling Unity on a per-monitor basis, which allows for a
    better experience on high-DPI displays. Similar scaling support is also available
    in LibreOffice and Chromium.</p></li>
<li class="list"><p class="p">Improved look and style including rounded window decorations, a new
    interface for screen unlock, and other tweaks to the theme.</p></li>
<li class="list"><p class="p">The <span class="link"><a href="keyboard-layouts.html" title="Use alternative input sources">integrated <span class="gui">Text Entry</span> interface</a></span>
    and the related input source <span class="link"><a href="unity-menubar-intro.html#status-menus" title="Notifikační applety">
    status menu</a></span> are designed to set both keyboard layout and IBus input methods.</p></li>
<li class="list">
<p class="p">A number of improvements have been made to the <span class="gui">Dash</span>:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">The <span class="gui">Dash</span> may now search dozens of different online sources simultaneously.
        The Internet-based searches <span class="link"><a href="unity-shopping.html" title="Proč jsou v nabídce Dash odkazy pro nakupování?">may be disabled</a></span>.</p></li>
<li class="list"><p class="p">The <span class="gui">Dash</span> search results may be filtered by category and source..</p></li>
<li class="list"><p class="p">Right-clicking on a search result in the <span class="gui">Dash</span> will display a full-screen preview with more information.</p></li>
<li class="list"><p class="p">Use the <span class="link"><a href="unity-dash-photos.html" title="Lens pro fotografie">photos lens</a></span> to view
        photos from your computer or from your social networks.</p></li>
<li class="list"><p class="p">Přidání nebo odebrání sekcí v nabídce <span class="gui">Dash</span> pro uživatelské přizpůsobení.</p></li>
<li class="list"><p class="p">Procházení zpráv ze sociálních sítí pomocí nové <span class="link"><a href="unity-dash-friends.html" title="Sekce Přátelé">sekce Přátelé</a></span>.</p></li>
</ul></div></div></div>
</li>
<li class="list"><p class="p">Enter your credentials in <span class="link"><a href="accounts.html" title="Online účty">Online Accounts</a></span>
    to easily set up online integration for the Dash, Empathy, and more.</p></li>
<li class="list"><p class="p">Mějte stále přehled o <span class="link"><a href="contacts.html" title="Kontakty">kontaktech</a></span> na vaše přátele a kolegy ve vašem adresáři.</p></li>
</ul></div></div></div></div></div>
