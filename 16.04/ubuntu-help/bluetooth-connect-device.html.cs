<div class="hgroup"><h1 class="title"><span class="title">Spojení počítače se zařízením s Bluetooth</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Předtím, než můžete Bluetooth zařízení jako myš nebo headset použít, musíte nejprve s tímto zařízením počítač spojit. Říká se tomu také párování Bluetooth zařízení.</p>
<div class="note note-important" title="Důležité"><div class="inner"><div class="region"><div class="contents"><p class="p">Před tím, než začnete, se ujistěte, že máte Bluetooth povoleno. Navštivte <span class="link"><a href="bluetooth-turn-on-off.html" title="Zapnutí nebo vypnutí Bluetooth">Zapnutí nebo vypnutí Bluetooth</a></span>.</p></div></div></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu Bluetooth v horním panelu a vyberte <span class="gui">Nastavit nové zařízení</span>.</p></li>
<li class="steps"><p class="p">Nastavte své zařízení tak, aby bylo <span class="link"><a href="bluetooth-visibility.html" title="Co je to viditelnost?">nalezitelné nebo viditelné</a></span> a položte jej někam v okruhu 10 metrů od počítače. Po kliknutí na <span class="gui">Pokračovat</span> počítač začne dané zařízení hledat.</p></li>
<li class="steps"><p class="p">Pokud je vypsáno příliš mnoho zařízení, vyberte v menu <span class="gui">Typ zařízení</span> typ vašeho zařízení.</p></li>
<li class="steps">
<p class="p">Kliknutím na <span class="gui">Nastavení PIN</span> nastavíte jak bude PIN doručen na dané zařízení.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Automatický PIN kód použije šesti číselný kód. Zařízení bez klávesnice nebo bez obrazovky, jako například myši nebo sluchátka, mohou vyžadovat použití specifického PIN kódu, například 0000, nebo PIN kód nepoužívají. Podívejte se do manuálu daného zařízení.</p></div></div></div></div>
<p class="p">Vyberte vhodné nastavení PIN pro vaše zařízení a potom klikněte na <span class="gui">Zavřít</span>.</p>
</li>
<li class="steps"><p class="p">Pro pokračování klikněte na <span class="gui">Pokračovat</span>. Pokud jste zadal/a vlastní PIN, tak se objeví na obrazovce.</p></li>
<li class="steps">
<p class="p">Je-li to nutné, potvrďte PIN na druhém zařízení. Zařízení může PIN zobrazit, nebo vás může vyzvat k jeho zadání. Potvrďte PIN a poté kliněte na <span class="gui">Sedí</span></p>
<p class="p">Na vtšině zařízení je potřeba zadat PIN během 20 sekund, jinak se připojení nepodaří. Pokud se to stane, vraťte se do seznamu zařízení a začněte znovu.</p>
</li>
<li class="steps"><p class="p">Až se objeví zpráva o úspěšném připojení, klikněte na <span class="gui">Zavřít</span>.</p></li>
</ol></div></div></div>
<p class="p">Později můžete <span class="link"><a href="bluetooth-remove-connection.html" title="Odstranit připojení mezi bluetooth zařízeními.">bluetooth připojení odstranit</a></span>.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">To control access to your shared files, refer to the
        <span class="gui">Bluetooth Sharing</span> settings. See
        <span class="link"><a href="sharing-bluetooth.html" title="Control sharing over Bluetooth">Control sharing over Bluetooth</a></span>.</p></div></div></div></div>
</div>
