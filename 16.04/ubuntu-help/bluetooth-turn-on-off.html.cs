<div class="hgroup"><h1 class="title"><span class="title">Zapnutí nebo vypnutí Bluetooth</span></h1></div>
<div class="region">
<div class="contents">
<div class="media media-image floatend"><div class="inner"><img src="figures/bluetooth-active.svg" class="media media-block" alt="Ikona bluetooth v horním panelu"></div></div>
<p class="p">Se zapnutým Bluetooth můžete používat Bluetooth zařízení, posílat a přijímat soubory, ale vypnuté Bluetooth šetří energii. Bluetooth můžete zapnout kliknutím na ikonu Bluetooth na horní liště a zapnutím <span class="gui">Bluetooth</span>.</p>
<p class="p">Mnoho noteboků má hardwarový přepínač či klávesovou zkratku, která ovládá Bluetooth. Pokud je Bluetooth hardwarově vypnuté, neuvidíte v panelu ikonu Bluetooth. Podívejte se, kde má počítač spínač či klávesou zkratku pro zapnutí Bluetooth. Často se používá klávesa <span class="key"><kbd class="key-Fn">Fn</kbd></span>.</p>
<p class="p">Bluetooth můžete vypnout kliknutím na ikonu Bluetooth na horní liště a vypnutím <span class="gui">Bluetooth</span>.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">You only need to switch <span class="gui">Visible</span> on if you are connecting
to this computer from another device. See <span class="link"><a href="bluetooth-visibility.html" title="Co je to viditelnost?">Co je to viditelnost?</a></span>
for more information.</p></div></div></div></div>
