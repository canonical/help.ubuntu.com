<div class="hgroup"><h1 class="title"><span class="title">Jaké nástroje pro určování barev jsou podporovány?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">GNOME závisí na systému správy barev Argyll pro podporu nástrojů barev. Následující nástroje pro měření displeje jsou tudíž podporovány:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Gretag-Macbeth i1 Pro (spektrometr)</p></li>
<li class="list"><p class="p">Gretag-Macbeth i1 Monitor (spektrometr)</p></li>
<li class="list"><p class="p">Gretag-Macbeth i1 Display 1, 2 nebo LT (kolorimetr)</p></li>
<li class="list"><p class="p">X-Rite i1 Display Pro (kolorimetr)</p></li>
<li class="list"><p class="p">X-Rite ColorMunki Design nebo Photo (spektrometr)</p></li>
<li class="list"><p class="p">X-Rite ColorMunki Create (kolorimetr)</p></li>
<li class="list"><p class="p">X-Rite ColorMunki Display (kolorimetr)</p></li>
<li class="list"><p class="p">Pantone Huey (kolorimetr)</p></li>
<li class="list"><p class="p">MonacoOPTIX (kolorimetr)</p></li>
<li class="list"><p class="p">ColorVision Spyder 2 a 3 (kolorimetr)</p></li>
<li class="list"><p class="p">Colorimètre HCFR (kolorimetr)</p></li>
</ul></div></div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Pantone Huey je momentálně nejlevnějším a nejlépe podporovaným hardwarem v Linuxu.</p></div></div></div></div>
<p class="p">Díky Argyllu je podporováno mnoho spotovacích a stripovacích čtecích reflektivních spektrometrů, které vám pomohou s kalibrací a charakterizací vašich tiskáren:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">X-Rite DTP20 "Pulse" ("swipe" typ reflektivního spektrometru)</p></li>
<li class="list"><p class="p">X-Rite DTP22 Digital Swatchbook (spotovací typ reflektivního spetrometru)</p></li>
<li class="list"><p class="p">X-Rite DTP41 (spotovací a stripovací čtecí reflektivní spektrometr)</p></li>
<li class="list"><p class="p">X-Rite DTP41T (spotovací a stripovací čtecí reflektivní spektrometr)</p></li>
<li class="list"><p class="p">X-Rite DTP51 (spotovací čtecí reflektivní spektrometr)</p></li>
</ul></div></div></div>
