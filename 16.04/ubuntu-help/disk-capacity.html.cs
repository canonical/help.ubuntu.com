<div class="hgroup"><h1 class="title"><span class="title">Kontrola kolik zbývá na disku volného místa</span></h1></div>
<div class="region">
<div class="contents"><p class="p">Kolik místa na disku zbývá můžete zjistit pomocí <span class="app">Analyzátoru využití disku</span> nebo <span class="app">Sledování systému</span>.</p></div>
<div id="disk-usage-analyzer" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Kontrola pomocí Analyzátoru využití disku</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Ke kontrole volného místa na disku a kapacity disku použijte <span class="app">Analyzátor využití disku</span>.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Open the <span class="app">Disk Usage Analyzer</span> application from the
 <span class="gui">Dash</span>. The window will display the <span class="gui">Total file system capacity</span>
  and <span class="gui">Total file system usage</span>.</p></li>
<li class="list"><p class="p">V nástrojové liště si vyberte, zda se má <span class="gui">Projít domovská složka</span>, <span class="gui">Projít souborový systém</span>, <span class="gui">Projít složku</span> nebo <span class="gui">Projít vzdálenou složku</span>.</p></li>
</ul></div></div></div>
<p class="p">Informace se zobrazují ve vztahu k <span class="gui">Adresáři</span>, <span class="gui">Zaplnění</span>, <span class="gui">Velikosti</span> a <span class="gui">Obsahu</span>. Nápověda je dostupná ve <span class="link"><a href="https://help.gnome.org/users/baobab/stable/" title="https://help.gnome.org/users/baobab/stable/"><span class="app">Správci obsazení disku</span></a></span>.</p>
</div></div>
</div></div>
<div id="system-monitor" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Kontrola pomocí Sledování systému</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Kontrola volného místa na disku a diskové kapacity pomocí <span class="app">Sledování systému</span>:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Z nabídky <span class="gui">Dash</span> spusťte aplikaci <span class="app">Sledování systému</span>.</p></li>
<li class="steps"><p class="p">Pro přehled diskových oddílů a využití místa na disku přejděte na kartu <span class="gui">Souborové systémy</span>. Potřebné informace naleznete ve sloupcích <span class="gui">Celkem</span>, <span class="gui">Volné</span>, <span class="gui">K dispozici</span> a <span class="gui">Použito</span>.</p></li>
</ol></div></div></div>
</div></div>
</div></div>
<div id="disk-full" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Co dělat, když je disk příliš zaplněn?</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pokud je disk příliš zaplněn, měli byste:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Smazat soubory, které nejsou důležité nebo je již nebudete používat.</p></li>
<li class="list"><p class="p">Udělejte <span class="link"><a href="backup-why.html" title="Záloha důležitých souborů">zálohy</a></span> důležitých souborů, které nebudete delší dobu potřebovat a vymažte je z pevného disku.</p></li>
</ul></div></div></div>
</div></div>
