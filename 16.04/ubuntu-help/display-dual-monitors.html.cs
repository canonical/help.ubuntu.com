<div id="unity-steps" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Nastavení externího monitoru</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pro nastavení více monitorů, připojte externí monitor k počítači. Pokud ho systém sám okamžitě nerozpozná, nebo si chcete upravit nastavení:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Displeje</span>.</p></li>
<li class="steps"><p class="p">Klikněte na obrázek monitoru, který si přejete aktivovat nebo deaktivovat a poté jej <span class="gui">zapněte či vypněte</span>.</p></li>
<li class="steps">
<p class="p">Ve výchozím nastavení je Launcher zobrazován pouze na primárním monitoru. Pro změnu primárního monitoru upravte monitor v rozevírací nabídce <span class="gui">Umístění launcheru</span>. Můžete také přetáhnout Launcher v náhledu na monitor, který chcete nastavit jako primární.</p>
<p class="p">Pokud chcete, aby se Launcher zobrazoval na všech monitorech, změňte <span class="gui">Umístění Launcheru</span> na <span class="gui">Všechny monitory</span>.</p>
</li>
<li class="steps">
<p class="p">Pro změny "pozice" monitoru na něj klikněte a přetáhněte jej na vámi požadované místo.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud chcete, aby se na obou monitorech zobrazoval stejný obsah, zaškrtněte položku <span class="gui">Zrcadlit displeje</span>.</p></div></div></div></div>
</li>
<li class="steps"><p class="p">Až budete s nastavením spokojeni, klikněte na <span class="gui">Použít</span> a poté klikněte na <span class="gui">Zachovat toto nastavení</span>.</p></li>
<li class="steps"><p class="p">
					To close the <span class="gui">Displays</span> click on the
					<span class="gui">x</span> in the top corner.
				</p></li>
</ol></div></div></div>
</div></div>
</div></div>
<div id="sticky-edges" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Přichytávání na hranách</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Při práci s více monitory se může snadno stát, že vám kurzor myši "proklouzne" na druhý monitor, ačkoli si to nepřejete. V Unity existuje funkce <span class="gui">Přichytávat na hranách</span>, která tento problém řeší tím, že vyžaduje větší "sílu" při přesunu kurzoru z jednoho monitoru na druhý.</p>
<p class="p">Pokud se vám tato vlastnost nelíbí, vypněte ji pomocí přepínače <span class="gui">Přichytávat na hranách</span>.</p>
</div></div>
