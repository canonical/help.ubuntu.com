<div class="hgroup"><h1 class="title"><span class="title">Mazání souborů a složek</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud již soubor nebo složku nepotřebujete, můžete je smazat. Když nějakou položku smažete, bude přesunuta do <span class="gui">Koše</span>, kde bude uložena do doby, než koš vyprázdníte. Pokud nějakou položku smažete omylem nebo zjistíte, že ji znovu potřebujete, můžete ji z <span class="gui">Koše</span> <span class="link"><a href="files-recover.html" title="Obnovení souboru z Koše">obnovit</a></span> do původního umístění.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Pro přesun souboru do koše:</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Jedním kliknutím vyberte položku, kterou si přejete přesunout do koše.</p></li>
<li class="steps"><p class="p">Zmáčkněte klávesu <span class="key"><kbd>Delete</kbd></span> na klávesnici. Případně můžete soubory přetáhnout do <span class="gui">Koše</span>.</p></li>
</ol></div>
</div></div>
<p class="p">Pro trvalé smazání souborů a uvolnění místa na disku vašeho počítače musíte vyprázdnit koš. Pro vyprázdnění koše klikněte pravým tlačítkem myši na <span class="gui">Koš</span> v postranním panelu a vyberte <span class="gui">Vyprázdnit koš</span>.</p>
</div>
<div id="permanent" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Trvalé smazání souboru</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Soubor můžete smazat trvale, aniž by se přesouval nejdříve do koše.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h3><span class="title">Pro trvalé smazání souboru:</span></h3></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Vyberte položku, kterou si přejete smazat.</p></li>
<li class="steps"><p class="p">Stiskněte a držte <span class="key"><kbd>Shift</kbd></span> a poté stiskněte klávesu <span class="key"><kbd>Delete</kbd></span>.</p></li>
<li class="steps"><p class="p">Protože tuto akci nemůžete vrátit, budete požádání o potvrzení, že si skutečně přejete smazat daný soubor nebo složku.</p></li>
</ol></div>
</div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">If you frequently need to delete files without using the
  trash (for example, if you often work with sensitive data), you can add a
  <span class="gui">Delete</span> entry to the right-click menu for files and folders.
  Click <span class="gui">Files</span> in the menu bar, pick <span class="gui">Preferences</span> and select
  the <span class="gui">Behavior</span> tab. Select <span class="gui">Include a Delete command that
  bypasses Trash</span>.</p></div></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Smazané soubory na <span class="link"><a href="files.html#removable" title="Výměnná média a externí disky">výměnných zařízeních</a></span> nemusí být viditelné na ostatních operačních systémech jako Windows nebo Mac OS. Tyto soubory tam však stále jsou a budou dostupné jakmile toto zařízení připojíte ke svému počítači.</p></div></div></div></div>
