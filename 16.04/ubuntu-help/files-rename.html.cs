<div class="hgroup"><h1 class="title"><span class="title">Přejmenování souboru nebo složky</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Změnit název souboru nebo složky můžete pomocí správce souborů.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Pro přejmenování souboru nebo složky:</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte pravým tlačítkem na položku a vyberte <span class="gui">Přejmenovat</span> nebo vyberte soubor a stiskněte <span class="key"><kbd>F2</kbd></span>.</p></li>
<li class="steps"><p class="p">Napište nové jméno a stiskněte <span class="key"><kbd>Enter</kbd></span>.</p></li>
</ol></div>
</div></div>
<p class="p">Přejmenovat soubor můžete také u okna <span class="link"><a href="nautilus-file-properties-basic.html" title="Vlastnosti souborů">vlastností</a></span>.</p>
<p class="p">Pokud přejmenováváte soubor, označí  se pouze první část jeho názvu. nikoli jeho přípona (část za tečkou "."). Přípona normálně označuje o jaký typ souboru se jedná (například <span class="file">soubor.pdf</span> je PDF dokument) a obvykle ji nechcete měnit. Pokud ji změnit potřebujete, označte celý název souboru a změňte ho.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">If you renamed the wrong file, or named your file improperly, you can
    undo the rename. To revert the action and restore the former name, immediately
    click <span class="gui">Edit</span> in the menu bar and select <span class="gui">Undo Rename</span>.</p></div></div></div></div>
</div>
<div id="valid-chars" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Platné znaky v názvech souborů</span></h2></div>
<div class="region"><div class="contents">
<p class="p">V názvech souborů můžete používat jakékoli znaky kromě <span class="key"><kbd>/</kbd></span> (lomítka). Některá zařízení ale mohou používat <span class="em">souborový systém</span> mající více omezení pro názvy souborů. Proto je lepší se při pojmenovávání souborů vyhnout následujícím znakům <span class="key"><kbd>|</kbd></span>, <span class="key"><kbd>\</kbd></span>, <span class="key"><kbd>?</kbd></span>, <span class="key"><kbd>*</kbd></span>, <span class="key"><kbd>&lt;</kbd></span>, <span class="key"><kbd>"</kbd></span>, <span class="key"><kbd>:</kbd></span>, <span class="key"><kbd>&gt;</kbd></span>, <span class="key"><kbd>/</kbd></span>.</p>
<div class="note note-warning" title="Varování"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud pojmenujete soubor tak, že začíná na <span class="key"><kbd>.</kbd></span> (tečku), bude ve správci souborů <span class="link"><a href="files-hidden.html" title="Skrytí souboru">skryt</a></span> a ve výchozím stavu jej neuvidíte.</p></div></div></div></div>
</div></div>
</div></div>
<div id="common-probs" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Obvyklé problémy</span></h2></div>
<div class="region"><div class="contents"><div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Název souboru je již používán</dt>
<dd class="terms">
<p class="p">Není možné mít ve stejné složce dva soubory nebo složky téhož jména. Pokud se pokusíte změnit název souboru na takový, který již v aktuální složce existuje, správce souborů vám to nepovolí.</p>
<p class="p">U jmen souborů a složek záleží na velikosti písmen. Soubory <span class="file">Soubor.txt</span> a <span class="file">SOUBOR.txt</span> jsou tak dva různé soubory. Můžete tedy takto různě pojmenovat dva soubory ve stejné složce, ale nedoporučuje se to.</p>
</dd>
<dt class="terms">Název souboru je příliš dlouhý</dt>
<dd class="terms"><p class="p">na některých souborovách systémech nemohou mít názvy souborů více než 255 znaků. Limit 255 znaků se vztahuje na celou cestu k souboru, tedy <span class="file">/home/franta/Documenty/práce/finanční-návrhy/… </span>, snažte se tedy vyvarovat příliš dlouhých názvů souborů a adresářů.</p></dd>
<dt class="terms">Možnost přejmenování je zašedlá.</dt>
<dd class="terms"><p class="p">Pokud je položka <span class="gui">Přejmenovat</span> zašedlá, nemáte právo daný soubor přejmenovat. Takovéto soubory byste měli přejmenovávat se zvýšenou opatrností, protože přejmenování některých systémových souborů může způsobit vážné problémy. Pro více informací navštivte <span class="link"><a href="nautilus-file-properties-permissions.html" title="Nastavení oprávnění souborů">Nastavení oprávnění souborů</a></span></p></dd>
</dl></div></div></div></div></div>
