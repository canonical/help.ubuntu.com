<div class="hgroup"><h1 class="title"><span class="title">Prostřední kliknutí</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Mnoho myší a některé touchpady mají prostřední tlačítko. Na myši s rolovacím kolečkem obvykle jeho stlačení funguje jako kliknutí prostředním tlačíkem. Pokud vaše myš nemá prostřední tlačítko, můžete kliknout levým a pravým tlačítkem najednou. Pokud je to pro vás nepohodlné, můžete zkusit <span class="link"><a href="https://wiki.ubuntu.com/X/Quirks#A2-button_Mice" title="https://wiki.ubuntu.com/X/Quirks#A2-button_Mice"> tento návod</a></span>.</p>
<p class="p">On touchpads that support multi-finger taps, you can tap with three
fingers at once to middle-click. You have to
<span class="link"><a href="mouse-touchpad-click.html" title="Click, drag, or scroll with the touchpad">enable tap clicking</a></span> in the
touchpad settings for this to work.</p>
<p class="p">Mnoho aplikací používá prostřední tlačíko pro rozšířené akce.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list">
<p class="p">Jednou z nejrozšířenějších funkcí je vložení textu, někdy pod názvem primární vložení výběru. Vyberte text, který chcete kopírovat a kliknutím prostředním tlačítkem ho vložíte na určené místo.</p>
<p class="p">Vkládání textu pomocí středního tlačítka je zcela odděleno od klasické schránky. Výbraný text se do schránky nekopíruje a tato metoda funguje pouze s prostředním tlačítkem.</p>
</li>
<li class="list"><p class="p">Na posuvnících klasické kliknutí do volného prostoru posune posuvník o nějakou hodnotu (například stránku) ve zvoleném směru. Můžete také použít střední tlačítko pro posun na přesnou pozici.</p></li>
<li class="list"><p class="p">You can quickly open a new
  window for an application with
 middle-click. Simply
  middle-click on the application's icon, either in
 the <span class="gui">Launcher</span> on the left, or in
  the <span class="gui">Dash</span>.</p></li>
<li class="list"><p class="p">Většina webových prohlížečů po kliknutí prostředním tlačítkem načte odkaz v novém panelu. Jednuduše klikněte na odkaz prostředním tlačítkem a otevře se nový panel. Pozor si dejte ve <span class="app">Firefoxu</span>, pokud se v něm netrefíte středním tlačítkem na odkaz, vybraný text se na odkaz změní.</p></li>
<li class="list"><p class="p">Ve správci souborů má střední tlačítko dvě funkce. Pokud takto kliknete na adresář, otevře se v novém panelu, podobně jako odkaz ve webovém prohlížeči. Pokud kliknete středním tlačítkem na soubor, otevře se normálně jako po dvojkliku.</p></li>
</ul></div></div></div>
<p class="p">Některé specializované aplikace využívají prostřední tlačítko pro určité funkce. Podívejte se do nápovědy programu na <span class="em">prostřední klik</span> nebo <span class="em">prostřední tlačítko</span>.</p>
