<div class="hgroup"><h1 class="title"><span class="title">Proč není můj hudební přehrávač rozpoznán, když jej připojím?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud je přehrávač připojen k počítači ale nevidíte ho ve svém hudebním organizátoru, je možné že nebyl správně rozpoznán jako přehrávač.</p>
<p class="p">Zkuste ho odpojit a znovu připojit. Pokud to nepomůže, <span class="link"><a href="files-browse.html" title="Procházení souborů a složek">otevřte správce souborů</a></span>. Přehrávač by měl být vidět mezi zařízeními v postranním panelu - po kliknutí se otevře obsah přehrávače. Nyní klikněte na <span class="guiseq"><span class="gui">Soubor</span> ▸ <span class="gui">Nový...</span> ▸ <span class="gui">Prázdný soubor</span></span> a pojmenujte ho <span class="input">.is_audio_player</span> a potvrďte enterem. Tečka na začátku názvu je důležitá a všechna písmena by měla být malá. Tento soubor řekne počítači, že je zařízení hudebním přehrávačem.</p>
<p class="p">Nyní najděte přehrávač ve správci souborů a odpojte ho (pravým kliknutím a volbou <span class="gui">Vysunout</span>. Vytáhněte ho a připojte zpět. Tentokrát bude korektně rozpoznán jako přehrávač. Možná bude třeba restartovat také organizátor hudby.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Tyto instrukce nebudou fungovat u iPodů a některých dalších audio přehrávačů. Fungují pro přehrávače podporující tzv. <span class="em">USB Mass Storage</span>. Tuto informaci byste měli nalézt v manuálu daného přehrávače.</p></div></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud se podíváte znovu do správce souborů, soubor <span class="input">.is_audio_player</span> již neuvidíte. Tečka na začátku názvu souboru značí skrytý soubor. Skryté soubory zobrazíte pomocí <span class="guiseq"><span class="gui">Zobrazit</span> ▸ <span class="gui">Zobrazit skryté soubory</span></span>.</p></div></div></div></div>
