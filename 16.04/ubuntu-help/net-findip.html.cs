<div class="hgroup"><h1 class="title"><span class="title">Zjištění své IP adresy</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Znalost IP adresy pomáhá s řešením problémů s internetovýh připojením. Možná budete překvapeni zjištěním, že máte <span class="em">dvě</span> adresy: pro vnitřní síť a pro komunikaci na internetu.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Zjištění vnitřní (síťové) IP adresy</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Open <span class="gui">Network</span> and select <span class="gui">Wired</span> or <span class="gui">Wireless</span> from the list on the left, depending on which network connection you want to find the IP address for.</p></li>
<li class="steps"><p class="p">Vaše interní IP adresa bude zobrazena na seznamu informací.</p></li>
</ol></div>
</div></div>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Zjištění externí (internetové) IP adresy</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Navštivte <span class="link"><a href="http://whatismyipaddress.com/" title="http://whatismyipaddress.com/">whatismyipaddress.com</a></span>.</p></li>
<li class="steps"><p class="p">Na stránce bude zobrazena vaše externí IP adresa.</p></li>
</ol></div>
</div></div>
<p class="p">Podle toho, jakým způsobem je váš počítač připojen k Internetu, mohou být tyto adresy stejné.</p>
</div>
