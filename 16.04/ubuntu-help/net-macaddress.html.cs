<div class="hgroup"><h1 class="title"><span class="title">Co je MAC adresa?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">A <span class="em">MAC address</span> is the unique identifier that is assigned by the manufacturer to a piece of network hardware (like a wireless card or an Ethernet card). MAC stands for <span class="em">Media Access Control</span>, and each identifier is intended to be unique to a particular device.</p>
<p class="p">MAC adresa sestává z šesti dvojic znaků oddělených dvojtečkou.  Příkladem MAC adresy může být <span class="code">00:1B:44:11:3A:B7</span>.</p>
<p class="p">Pro nalezení MAC adresy vašeho síťového hardwaru:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="gui">nabídku sítě</span> na horním panelu.</p></li>
<li class="steps"><p class="p">Vyberte <span class="gui">Informace o spojení</span>.</p></li>
<li class="steps"><p class="p">Vaše MAC adresa bude zobrazena jako položka <span class="gui">Hardwarová adresa</span>.</p></li>
</ol></div></div></div>
<p class="p">In practice, you may need to <span class="link"><a href="net-wireless-edit-connection.html" title="Úprava bezdrátových připojení">modify or "spoof" a MAC address</a></span>. For example, some internet service providers may require that a specific MAC address be used to access their service. If the network card stops working, and you need to swap a new card in, the service won't work anymore. In such cases, you would need to spoof the MAC address.</p>
