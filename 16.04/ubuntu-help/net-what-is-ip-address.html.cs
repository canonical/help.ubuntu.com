<div class="hgroup"><h1 class="title"><span class="title">Co je to IP adresa?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">"IP adresa" je zkratka pro <span class="em">Internet Protocol address</span> a každé zařízení připojené k internetu nějakou má.</p>
<p class="p">IP adresa je podobná telefonnímu číslu. Telefonní číslo je unikátní sada čísel identifikující váš telefon tak, aby vám mohli jiní lidé zavolat. Podobně je IP adresa sada čísel identifikující počítač tak, aby mohl odesílat a přijímat data z jiných počítačů.</p>
<p class="p">V současné době je většina IP adres složena z 4 skupin čísel oddělených tečkou. IP adresa vypadá třeba takhle: <span class="code">192.168.1.42</span>.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">IP adresa může být <span class="em">dynamická</span> nebo <span class="em">statická</span>. Dynamická adresaa je přidělena počítači pouze dočasně. Statická je zadána napevno a nemění se. Většinou se používají dynamické adresy - statické se většinou používají pouze když je k tomu zvláštní důvod, třeba na serverech.</p></div></div></div></div>
