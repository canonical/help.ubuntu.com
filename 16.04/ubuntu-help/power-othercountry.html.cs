<div class="hgroup"><h1 class="title"><span class="title">Bude počítač fungovat i v jiném státě?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">V různých zemích se používá různé napětí v síti (obvykle 110V nebo 220-240V) a frekvence (obvykle 50 nebo 60Hz). Počítač by měl fungovat po připojení odpovídajícím napájecím kabelem. Možná budete muset přepnout přepínač na vašem zařízení.</p>
<p class="p">Máte-li notebook, budete si muset zajistit jenom správný napájecí kabel. některé notebooky mají přibaleno více takzvaných redukcí, takže tu správnou možná již máte. Pokud ne, zapojení do standardního cestovního adaptéru by mělo stačit.</p>
<p class="p">Pokud máte stolní počítač, můžete použít kabel s jiným tvarem zástrčky. V takovém případě možná bude potřeba přepnout nastavení napájení. Mnoho počítačů takový přepínač nemá a budou fungovat i s jiným napětím. Podívejte se na zadní stranu počítače a najděte zástrčku pro zapojení do sítě. Poblíž by měl být malý přepínač označený "110V" nebo "220V". Přepněte ho, je-li to potřeba.</p>
<div class="note note-warning" title="Varování"><div class="inner"><div class="region"><div class="contents"><p class="p">Při výměně napájecího kabelu buďte opatrní. Nejdříve počítač úplně vypněte.</p></div></div></div></div>
