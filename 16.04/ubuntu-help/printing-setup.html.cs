<div class="hgroup"><h1 class="title"><span class="title">Nastavení místní tiskárny</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Váš systém umí automaticky rozpoznat mnoho druhů tiskáren okamžitě po připojení. Většina tiskáren se do počítače připojuje pomocí USB kabelu.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">You do not need to select whether you want to install network or local
  printer now. They are listed in one window.</p></div></div></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Ujistěte se, že je tiskárna zapojena.</p></li>
<li class="steps"><p class="p">Pomocí správného kabelu připojte tiskárnu do vašeho počítače. Je možné, že na obrazovce uvidíte průběh vyhledávání ovladačů pro vaši novou tiskárnu a systém vás může požádat o ověření vyžadované pro jejich instalaci.</p></li>
<li class="steps"><p class="p">A message will appear when the system is finished installing the
 printer. Select <span class="gui">Print Test Page</span> to print a test page, or
 <span class="gui">Options</span> to make additional changes in the printer setup.</p></li>
</ol></div></div></div>
<p class="p">Pokud vaše tiskárna nebyla nastavena automaticky, můžete ji přidat v dialogu nastavení tiskáren.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Otevřte <span class="gui">Tiskárny</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Přidat</span> a v okně Zařízení vyberte vaši tiskárnu.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Vpřed</span> a vyčkejte na vyhledání ovladačů.</p></li>
<li class="steps"><p class="p">Pokud si to budete přát, můžete pro tiskárnu upravit název, popisek a umístění. Po dokončení úprav klikněte na <span class="gui">Použít</span>.</p></li>
<li class="steps"><p class="p">Nyní můžete vytisknout testovací stránku nebo kliknout na <span class="gui">Zrušit</span> a tento krok přeskočit.</p></li>
</ol></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud je pro vaši tiskárnu k dispozici více ovladačů, je možné, že budete požádání o výběr ovladače. Pro vybrání doporučeného ovladače jednoduše na obrazovce s výrobci a modely klikněte na Vpřed.</p></div></div></div></div>
<p class="p">Poté, co nainstalujete novou tiskárnu, možná budete chtít <span class="link"><a href="printing-setup-default-printer.html" title="Nastavení výchozí tiskárny">změnit svou výchozí tiskárnu</a></span>.</p>
</div>
