<div class="hgroup"><h1 class="title"><span class="title">Změna formátu data a jednotek</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete měnit nastavení dat, časů, čísel, měny a měrných jednotek dle vaší země a potřeb.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Jazyková podpora</span> a vyberte kartu <span class="gui">Místní formáty</span>.</p></li>
<li class="steps"><p class="p">Vyberte si region který nejlépe odpovídá formátu který chcete použít. Ve výchozím stavu se zobrazují pouze regiony které odpovídají nastavení <span class="gui">jazyka</span>.</p></li>
<li class="steps"><p class="p">Aby se tyto změny projevily, musíte odhlásit a znovu přihlásit. Klikněte na ikonu zcela vpravo na horním panelu a vyberte <span class="gui">Odhlásit</span>.</p></li>
</ol></div></div></div>
<p class="p">Po výběru regionu se zobrazí několik příkladů jak se budou zobrazovat data a ostatní hodnoty. Ikdyž to není zobrazeno, region také řídí nastavení prvního dne v týdnu.</p>
</div>
<div id="system" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Změna formátu systému</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Když změníte nastavení regionu, změny se projeví po opětovném přihlášení. Můžete také změnit nastavení systému pro změnu nastavení přihlašovací obrazovky.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Změňte nastavení, viz výše</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Uplatnit v rámci celého systému</span>.</p></li>
<li class="steps"><p class="p">Pro změnu jsou vyžadována <span class="link"><a href="user-admin-explain.html" title="Jak funguje oprávnění správce?">správcovská oprávnění</a></span>. Zadejte své heslo respektive heslo vyžadovaného správcovského účtu.</p></li>
</ol></div></div></div>
