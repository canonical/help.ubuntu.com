<div class="hgroup"><h1 class="title"><span class="title">Operace s okny</span></h1></div>
<div class="region">
<div class="contents"><p class="p">Okna mohou mít různou velikost a mohou se překrývat.</p></div>
<div id="min-rest-close" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Minimalizace, obnova a uzavření</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pro minimalizaci nebo skrytí okna:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Klikněte na <span class="gui">-</span> v levém horním rohu okna. Pokud je aplikace maximalizovaná (zabírá celou plochu), menu se objeví úplně nahoře obrazovky. Jinak je menu u horního okraje okna.</p></li>
<li class="list"><p class="p">Or press <span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Space</kbd></span></span> to bring up the
       window menu.  Then press <span class="key"><kbd>n</kbd></span>. The window 'disappears' into the
       Launcher.</p></li>
</ul></div></div></div>
<p class="p">Pro obnovu okna:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list"><li class="list"><p class="p">Klikněte na <span class="link"><a href="unity-launcher-intro.html" title="Použití Launcheru pro spouštění aplikací">Launcher</a></span> nebo se na něj přepněte pomocí přepínače oken <span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span>.</p></li></ul></div></div></div>
<p class="p">Pro uzavření okna:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Klikněte na <span class="gui">x</span> v levém horním rohu okna nebo</p></li>
<li class="list"><p class="p">Stiskněte <span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>F4</kbd></span></span> nebo</p></li>
<li class="list"><p class="p">Stiskněte <span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Mezerník</kbd></span></span> pro zobrazení nabídky okna. Poté stiskněte <span class="key"><kbd>c</kbd></span>.</p></li>
</ul></div></div></div>
</div></div>
</div></div>
<div id="resize" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Změna velikosti</span></h2></div>
<div class="region"><div class="contents">
<div class="note note-important" title="Důležité"><div class="inner"><div class="region"><div class="contents"><p class="p">U okno není možné změnit velikost pokud je <span class="em">maximalizované</span>.</p></div></div></div></div>
<p class="p">Pro horizontální a/nebo vertikální změnu velikosti okna:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list"><li class="list"><p class="p">Přesuňte kurzor do rohu okna dokud se nezmění na 'rohový kurzor'. Kliknutím a tahem můžete změnit velikost okna.</p></li></ul></div></div></div>
<p class="p">Pro změnu pouze v horizontálním směru:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list"><li class="list"><p class="p">Přesuňte kurzor k pravé či levé straně okna dokud se nezmění na 'stranový kurzor'. Kliknutím a tahem můžete změnit velikost okna v jednom směru.</p></li></ul></div></div></div>
<p class="p">Pro změnu pouze ve vertikálním směru:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list"><li class="list"><p class="p">Přesuňte kurzor k horní či dolní straně okna dokud se nezmění na 'stranový kurzor'. Kliknutím a tahem můžete změnit velikost okna v jednom směru.</p></li></ul></div></div></div>
</div></div>
</div></div>
<div id="arrange" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Rozmístění oken na pracovní ploše</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pro umístění dvou oken vedle sebe:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Klikněte na titulkový proužek okna a přetáhněte ho na jednu stranu obrazovky. Když se kurzor myši dotkne hrany, polovina obrazovky se zvýrazní. Pusťte myš a okno vyplní polovinu obrazovky.</p></li>
<li class="list"><p class="p">Přetáhněte jiné okno ke druhému kraji a až se druhá polovina zvýrazní, pusťte ho. Obě okna vyplní polovinu obrazovky.</p></li>
</ul></div></div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Držením <span class="key"><kbd>Alt</kbd></span> táhnutím myší za okno ho můžete libovolně přesouvat. Pro některé lidi je to pohodlnější než tažení za titulkový proužek aplikace.</p></div></div></div></div>
