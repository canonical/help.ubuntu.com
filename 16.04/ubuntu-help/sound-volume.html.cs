<div class="hgroup"><h1 class="title"><span class="title">Změna hlasitosti zvuku</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pro změnu hlasitosti klikněte na <span class="gui">nabídku zvuku</span> v <span class="gui">horním panelu</span> a přesuňte posuvník doleva nebo doprava. Zcela ztišit zvuk můžete pomocí <span class="gui">Ztlumit</span>.</p>
<p class="p">Na některých klávesnicích jsou klávesy umožňující ovládání zvuku. Obvykle vypadají jako stylizované reproduktory. Často bývají blízko funkčních kláves ("Fx") v horní části klávesnice. Na klávesnicích u notebooků jsou obvykle přímo na funkčních klávesách. Pro jejich využití musíte stisknout klávesu <span class="key"><kbd class="key-Fn">Fn</kbd></span>.</p>
<p class="p">Samozřejmě, mát-li externí reproduktory, můžete hlasitost ovládat přímo na nich. Některá sluchátka mají samostatné ovládání též.</p>
</div>
<div id="apps" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Změna hlasitosti pro jednotlivé aplikace</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Můžete nastavit hlasitost pouze pro jednu aplikace, ale pro ostatní zůstane nezměněna. Toto je užitečné posloucháte-li například hudbu a prohlížíte si webové stránky. Vypnete zvuk pro webový prohlížeč a tím zvuky z něj nebudou přerušovat hudbu.</p>
<p class="p">Některé aplikace mají ovládání zvuku přímo ve svém okně. Pokud má vaše aplikace tuto možnost, použijte ji pro změnu hlasitosti. V opačném případě klikněte na <span class="gui">nabídku zvuku</span> na <span class="gui">horním panelu</span> a klikněte na <span class="gui">Předvolby zvuku</span>. Přejděte na kartu <span class="gui">Aplikace</span> a zde nastavte hlasitost pro danou aplikaci.</p>
<p class="p">Jsou zde uvedeny pouze aplikace, které přehrávají nějaké zvuky. Jestliže tu aplikace uvedena není a přesto slouží k přehrávání zvuků, pravděpodobně nepodporuje toto ovládání. V tomto případě nemůžete měnit ovládání hlasitosti pro tuto aplikaci.</p>
</div></div>
