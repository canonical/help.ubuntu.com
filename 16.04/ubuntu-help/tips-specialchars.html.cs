<div class="hgroup"><h1 class="title"><span class="title">Vkládání speciálních znaků</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete vkládat a procházet tisíce znaků z většiny světových systémů písem a to i přesto, že nejsou na vaší klávesnici. Na této stránce naleznete různé možnosti pro vkládání speciálních znaků.</p>
