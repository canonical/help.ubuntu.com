<div id="charmap" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Mapa znaků</span></h2></div>
<div class="region"><div class="contents">
<p class="p">GNOME nabízí aplikaci Mapa znaků, která vám umožňuje procházet všechny znaky v Unicode. Mapu znaků použijte pro nalezení znaku, který potřebujete a poté jej překopírujte kamkoli chcete.</p>
<p class="p">You can find <span class="app">Character Map</span> in the <span class="gui">Dash</span>.
    For more information on the character map, see the
    <span class="link"><a href="https://help.gnome.org/users/gucharmap/stable/" title="https://help.gnome.org/users/gucharmap/stable/">Character Map Manual</a></span>.</p>
</div></div>
</div></div>
<div id="ctrlshiftu" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Numerické vyjádření</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Všechny Unicode znaky můžete zadat pomocí klávesnice jako číslo znaku. každý znak má své čtyřciferné číslo. Pro nalezení správného čísla nalezněte znak v mapě znaků a podívejte se do stavového řádku na detaily znaku. Číslo znaku je kód za <span class="gui">U+</span>.</p>
<p class="p">To enter a character by its code point, press
    <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Shift</kbd></span>+<span class="key"><kbd>U</kbd></span></span>, type the
    four-character code point, and press <span class="key"><kbd>Enter</kbd></span>. If you 
    often use characters that you can't easily access with other methods, 
    you might find it useful to memorize the code point for those 
    characters so you can enter them quickly.</p>
</div></div>
</div></div>
<div id="sources" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Input sources</span></h2></div>
<div class="region"><div class="contents"><p class="p">You can make your keyboard behave like the keyboard for another 
    language, regardless of the letters printed on the keys. You can even 
    switch between different input sources using an icon in the menu 
    bar. To learn how, see <span class="link"><a href="keyboard-layouts.html" title="Use alternative input sources">Use alternative input sources</a></span>.</p></div></div>
