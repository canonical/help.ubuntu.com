<div id="dash-filters" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Filtry</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Filtry umožňují další zpřesnění výsledků.</p>
<p class="p">Klikněte na <span class="gui">Filtrovat výsledky</span> pro výběr filtrů. například klikněte na <span class="gui">Zdroje</span> pro zobrazení dostupných zdrojů.</p>
