<div id="dash-lenses" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Lens</span></h2></div>
<div class="region">
<div class="contents">
<p class="p">Rozšíření pro nabídku Dash umožňují zaměřit se na konkrétní výsledky a vyloučit výsledky vyhledávání z ostatních rozšíření.</p>
<p class="p">Dostupná rozšíření naleznete v <span class="gui">liště rozšíření</span>, tmavší pruh ve spodní části nabídky Dash.</p>
<p class="p">Kliknutím na odpovídající ikonu se přepnete na jinou lens, nebo stiskněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span>.</p>
