<div class="hgroup"><h1 class="title"><span class="title">Find applications, files, music, and more with the Dash</span></h1></div>
<div class="region">
<div class="contents">
<div class="media media-image floatend"><div class="inner"><img src="figures/unity-dash-sample.png" class="media media-block" alt="Unity vyhledávání"></div></div>
<p class="p">Nabídka <span class="gui">Dash</span> umožňuje vyhledávání aplikací, souborů, hudby a videa. Také zobrazuje nedávno použité položky. Jestliže jste někdy pracovali na nějaké tabulce nebo jste upravovali fotografii a zapomněli jste, kam jste soubor uložili, tak tu funkci určitě uvítáte.</p>
<p class="p">Pro použití nabídky <span class="gui">Dash</span> klikněte na horní ikonu v <span class="link"><a href="unity-launcher-intro.html" title="Použití Launcheru pro spouštění aplikací">Launcheru</a></span>. Tato ikona má Ubuntu logo. Pro rychlejší přístup stačí stisknout klávesu <span class="key"><a href="windows-key.html" title='Která klávesa je "Super"?'><kbd>Super</kbd></a></span> key.</p>
<p class="p">Pro opuštění nabídky <span class="gui">Dash</span> stiskněte znovu horní ikonu nebo zmáčkněte klávesu <span class="key"><kbd>Super</kbd></span> případně <span class="key"><kbd>Esc</kbd></span>.</p>
</div>
<div id="dash-home" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Nalezněte cokoli z domovské nabídky Dash</span></h2></div>
<div class="region">
<div class="contents">
<p class="p">The first thing you'll see when opening the Dash is the Dash Home. Without typing
    or clicking anything, the Dash Home will show you applications and files you've used recently.</p>
<p class="p">každý typ se zobrazí pouze na jednom řádku. Chcete-li více výsledků, klikněte na <span class="gui">Zobrazit více výsledků</span>.</p>
<p class="p">Hledat můžete prostým napsáním hledaného výrazu a výsledky z různých lens se automaticky objeví.</p>
<p class="p">Klikněte na výsledek hledání pro jeho otevření, nebo stiskněte <span class="key"><kbd>Enter</kbd></span> pro otevření prvního výsledku v seznamu.</p>
</div>
