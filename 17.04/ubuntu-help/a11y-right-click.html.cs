<div class="hgroup"><h1 class="title"><span class="title">Simulace kliknutí pravým tlačítkem myši</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Máte možnost simulovat pravý klik držením levého tlačítka myši. Je to užitečné pokud je pro vás složité ovládat prsty na jedné ruce nezávisle nebo pokud má vaše ukazovací zařízení pouze jedno tlačítko.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Click the <span class="em">system menu</span> <span class="media"><span class="media media-image"><img src="figures/system-devices-panel.svg" class="media media-inline" alt="Power cog icon"></span></span> and
      select <span class="gui">System Settings</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Univerzální přístup</span> a vyberte kartu <span class="gui">Ukazování a klikání</span>.</p></li>
<li class="steps"><p class="p">Zapněte <span class="gui">Simulované druhé kliknutí</span>.</p></li>
</ol></div></div></div>
<p class="p">Nastavte, jak dlouho je třeba držet levé tlačítko, než je registrováno, jako pravé kliknutí. V záložce <span class="gui">Ukazování a Klikání</span> změňte <span class="gui">Zpoždění přijetí</span> pod <span class="gui">Simulované druhé kliknutí</span>.</p>
<p class="p">
    To right-click with simulated secondary click, hold down the left mouse 
    button where you would normally right-click, then release. The pointer fills 
    with blue as you hold down the left mouse button. Once it is entirely blue, 
    release the mouse button to right-click.
  </p>
<p class="p">Některé speciální kurzory, jako například pro změnu velikosti, nemění barvu. Simulace kliknutí funguje normálně i bez vizuální odezvy.</p>
<p class="p">Používáte-li tzv. <span class="link"><a href="mouse-mousekeys.html" title="Click and move mouse pointer using the keypad">myš z klávesnice</a></span>, můžete simulovat pravé tlačítko stisknutím klávesy <span class="key"><kbd>5</kbd></span> na numerické klávesnici.</p>
