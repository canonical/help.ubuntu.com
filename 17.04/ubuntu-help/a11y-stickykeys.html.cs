<div class="hgroup"><h1 class="title"><span class="title">Kombinace kláves jedním prstem</span></h1></div>
<div class="region">
<div class="contents">
<p class="p"><span class="em">Kombinace kláves jedním prstem</span> vám umožňuje mačkat klávesové zkratky klávesu po klávese místo obvyklého stisku všech kláves najednou. Jedná se například o zkratku <span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span> pro přepínání oken. Bez této funkce byste museli stisknout obě klávesy najednou, s kombinací kláves jedním prstem stačí, když zmáčknete napřed jen <span class="key"><kbd>Alt</kbd></span> a poté teprve jen klávesu <span class="key"><kbd>Tab</kbd></span>.</p>
<p class="p">Kombinaci kláves jedním prstem budete pravděpodobně chtít zapnout pokud vám dělá problém držet více kláves najednou.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Click the <span class="em">system menu</span> <span class="media"><span class="media media-image"><img src="figures/system-devices-panel.svg" class="media media-inline" alt="Power cog icon"></span></span> and
      select <span class="gui">System Settings</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Univerzální přístup</span> a vyberte kartu <span class="gui">Psaní</span>.</p></li>
<li class="steps"><p class="p">Zapněte <span class="gui">Kombinace kláves jedním prstem</span></p></li>
</ol></div></div></div>
<div class="note note-tip" title="Tip"><div class="inner">
<div class="title title-note"><h2><span class="title">Rychle zapnout/vypnout kombinaci kláves jedním prstem</span></h2></div>
<div class="region"><div class="contents"><p class="p">
      Under <span class="gui">Enable by Keyboard</span>, select <span class="gui">Turn on accessibility 
      features from the keyboard</span> to turn sticky keys on and off from the 
      keyboard. When this option is selected, you can press <span class="key"><kbd>Shift</kbd></span> 
      five times in a row to enable or disable sticky keys.
    </p></div></div>
</div></div>
<p class="p">Pokud stisknete dvě klávesy současně, můžete dočasně vypnout kombinaci kláves jedním prstem a normálním způsobem použít klávesovou zkratku.</p>
<p class="p">Když máte tuto funkci povolenou a například stisknete <span class="key"><kbd>Alt</kbd></span> a <span class="key"><kbd>Tab</kbd></span> tak i přes zapnutou kombinaci kláves jedním prstem nebude počítač čekat na stisk další klávesy. <span class="em">Bude</span> ale čekat, pokud stisknete pouze jednu klávesu. Toto je užitečné pokud můžete stisknout některé klávesové zkratky najednou (například pokud leží blízko sebe), ale ostatní nikoli.</p>
<p class="p">Pro povolení této vlastnosti vyberte <span class="gui">Vypnout při současném stisku dvou kláves</span>.</p>
<p class="p">Můžete si nastavit, aby počítač vydal pípnutí, když začnete psát klávesovou zkratku při zapnuté kombinaci kláves jedním prstem. Je to užitečné v případě pokud si přejete vědět, že kombinace kláves jedním prstem očekává napsání klávesové zkratky, takže následující klávesa bude vyhodnocena jako část klávesové zkratky. Pro povolení této vlastnosti vyberte <span class="gui">Zvukové znamení při zmáčknutí modifikátoru</span>.</p>
