<div class="hgroup"><h1 class="title"><span class="title">Kontrola provedené zálohy</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Potom co zazálohujete vaše soubory, měli byste překontrolovat, jestli vše proběhlo úspěšně. Kdyby vše neproběhlo korektně, mohli byste o svá data přijít, například tím, že některé zálohované soubory budou chybět.</p>
<p class="p">Jestliže kopírujete data v souborovém manažeru, tak počítač kontroluje, jestli vše probíhá dobře. Nicméně budete-li přesouvat velmi důležitá data, tak budete možná chtít provést i nějaké další dodatečné kontroly, jestli je vše v pořádku.</p>
<p class="p">Můžete provést dodatečnou kontrolu projitím zkopírovaných souborů a složek na cílovém médiu. Při kontrole se ujistěte, že soubory a složky, které byly přeneseny jsou v tuto chvíli v záloze, můžete se tak dodatečně ujistit, že byl proces úspěšný.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Jestliže zjistíte, že pravidelně zálohujete větší objem dat, tak bude možná jednodušší použít nějaký specializovaný program, jako například <span class="app">Déjà Dup</span>. Tento program nabízí mnohem lepší možnosti ovládání a práce s daty než pouhé kopírování souborů.</p></div></div></div></div>
