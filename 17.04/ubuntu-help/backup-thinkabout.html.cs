<div class="hgroup"><h1 class="title"><span class="title">Kde najdu soubory, které chci zazálohovat?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Listed below are the most common locations of important files and settings
  that you may want to back up.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list" style="list-style-type:disc">
<li class="list">
<p class="p">Osobní soubory (dokumenty, hudba, fotografie a videa)</p>
<p class="p">These are usually 
  stored in your home folder (<span class="file">/home/your_name</span>). They could be in 
  subfolders such as Desktop, Documents, Pictures, Music, and Videos.</p>
<p class="p">Pokud je na vašem médiu dostatek místa  (pokud je to například externí disk), zvažte zazálohování celého Domovského adresáře. Kolik místa váš Domovský adresář zabírá, můžete určit pomocí nástroje <span class="app">Analyzátor využití disku</span>.</p>
</li>
<li class="list">
<p class="p">Skryté soubory</p>
<p class="p">Jakýkoli soubor nebo adresář začínající tečkou (.) je skrytý. Pro zobrazení skrytých souborů vyberte <span class="guiseq"><span class="gui">Zobrazit</span> ▸ <span class="gui">Zobrazovat skryté soubory</span></span> nebo stiskněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>H</kbd></span></span>. Můžete je vzít a zkopírovat jako jakýkoli jiný soubor.</p>
</li>
<li class="list">
<p class="p">Osobní nastavení (předvolby pracovního prostředí, motiv a nastavení softwaru)</p>
<p class="p">Většina aplikací ukládá svá nastavení ve skrytých souborech ve vašem domovském adresáři (o skrytých souborech vizte výše).</p>
<p class="p">Nastavení většiny aplikací je uloženo ve skrytých složkách <span class="file">.config</span>, <span class="file">.gconf</span>, <span class="file">.gnome2</span>, and <span class="file">.local</span> ve vašem domovském adresáři.</p>
</li>
<li class="list">
<p class="p">Systémová nastavení</p>
<p class="p">Nastavení důležitých částí vašeho systému nejsou uloženy ve vašem domovském adresáři. Ačkoli existuje mnoho míst, kde mohou být tato nastavení uložena, s největší pravděpodobností se nachází ve složce <span class="file">/etc</span>. Na domácím počítači tyto soubory obecně nemusíte zálohovat. Pokud ale provozujete server, může být užitečné zazálohovat soubory s nastavením spuštěných služeb.</p>
</li>
</ul></div></div></div>
