<div class="hgroup"><h1 class="title"><span class="title">Nemohu připojit své Bluetooth zařízení</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Existuje mnoho důvodů proč se nepodaří připojit Bluetooth zařízení, jako je například telefon nebo sluchátka.</p>
<div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Připojení zablokováno nebo je nedůvěryhodné</dt>
<dd class="terms"><p class="p">Některá Bluetooth zařízení ve výchozím nastavení blokují pokusy o připojení, nebo vyžadují změnu nastavení. Ujistěte se, že je zařízení správně nastaveno.</p></dd>
<dt class="terms">Bluetooth zařízení nerozpoznáno</dt>
<dd class="terms"><p class="p">Váš Bluetooth adaptér nemusí být počítačem správně rozpoznán. Toto se může stát kvůli tomu, že <span class="link"><a href="hardware-driver" title="hardware-driver">ovladač zařízení</a></span> není správně nainstalován. Některá Bluetooth zařízení nejsou v Linuxu podporována. V takovém případě je pravděpodobně nutné sehnat jiné zařízení.</p></dd>
<dt class="terms">Adaptér není zapnutý</dt>
<dd class="terms"><p class="p">Ujistěte se, že je Bluetooth adaptér zapnutý. Klikněte na ikonku Bluetooth v <span class="gui">panelu</span> a ověřte, že není <span class="link"><a href="bluetooth-turn-on-off" title="bluetooth-turn-on-off">zakázán</a></span>.</p></dd>
<dt class="terms">Připojení bluetooth zařízení vypnuto</dt>
<dd class="terms"><p class="p">Ujistěte se, že je připojované zařízení zapnuté. Například pokud připojujete telefon, ověřte že není uspaný.</p></dd>
<dt class="terms">Žádný bluetooth adaptér nebyl v tomto počítači nalezen</dt>
<dd class="terms"><p class="p">Mnoho počítačů bluetooth adaptér nemá. Můžete si nějaký koupit, chcete-li bluetooth používat.</p></dd>
</dl></div></div></div>
