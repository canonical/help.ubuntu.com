<div class="hgroup"><h1 class="title"><span class="title">Jak zkalibruji svůj skaner?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Nascanovaný soubor můžete uložit jako nekomprimovaný soubor TIFF. Poté můžete kliknout na <span class="gui">Kalibrovat...</span> z nabídky <span class="guiseq"><span class="gui">Systémové nastavení</span> ▸ <span class="gui">Barvy</span></span> pro vytvoření profilu pro scanner.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Skenery jsou úžasně stabilní vůči teplotě a času a obvykle nepotřebují být seřizovány.</p></div></div></div></div>
