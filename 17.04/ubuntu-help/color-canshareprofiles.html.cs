<div class="hgroup"><h1 class="title"><span class="title">Mohu sdílet svůj barevný profil?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Barevné profily, které jsou pořízeny, jsou specifické vzhledem k hardwaru a světelným podmínkám, při kterých byla prováděna kalibrace. Displej, který byl zapnut na pár hodin, bude mít zcela odlišný barevný profil, než podobný displej s jiným výrobním číslem, který svítil tisíce hodin.</p>
<p class="p">To znamená, pokud sdílíte svůj barevný profil s někým jiným, můžete jej <span class="em">přiblížit</span> kalibraci, ale je nanejvýš zavádějící říci, že je jeho displej kalibrován.</p>
<p class="p">Podobně sdílení profilu vytvořeného za specifických světelných podmínek nedává příliš smysl, přestože vám někdo doporučil řízené osvětlení (žádný sluneční svit z oken, černé stěny, denní žárovky atd.) v místnosti, kde pracujete s obrázky.</p>
<div class="note note-warning" title="Varování"><div class="inner"><div class="region"><div class="contents"><p class="p">Je důležité obezřetně zkontrolovat redistribuční podmínky pro profily stažené z webových stránek výrobce nebo vytvořené na míru.</p></div></div></div></div>
