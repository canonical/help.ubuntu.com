<div class="hgroup"><h1 class="title"><span class="title">Co je barevný profil?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Barevný profil je sada dat, která charakterizuje buď zařízení jako je projektor nebo barevný prostor jako je sRGB.</p>
<p class="p">Většina barevných profilů je ve formátu ICC profilu, což je malý soubor s příponou <span class="input">.ICC</span> nebo <span class="input">.ICM</span>.</p>
<p class="p">Barevné profily mohou být vestavěny přímo do obrázku, kde specifikují rozsah gamutu dat. To zajišťuje, že uživatelé uvidí stejné barvy na rozdílných zařízeních.</p>
<p class="p">Každé zařízení, které manipuluje s barvou, by mělo mít vlastní ICC profil a pokud je toto splněno říkáme, že systém má <span class="em">od začátku do konce fungující správu barvy</span>. V takovém případě si uživatel může být jist, že barvy nejsou ztraceny nebo změněny.</p>
