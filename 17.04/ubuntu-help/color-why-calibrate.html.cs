<div class="hgroup"><h1 class="title"><span class="title">Proč potřebuji provést kalibraci?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Výchozí profily jsou obvykle špatné. Pokud výrobce vytvoří nový model, pak vezme pouze pár výrobků z produkční řady a zprůměruje naměřené hodnoty:</p>
<div class="media media-image"><div class="inner"><img src="figures/color-average.png" class="media media-block" alt="Zprůměrované profily"></div></div>
<p class="p">Displeje se poměrně hodně různí model od modelu a mění se v průběhu stárnutí displeje. Mnohem více obtížné je to s tiskárnami, kde změnou typu nebo gramáže papíru můžete zcela zneplatnit charakterizační stav a učinit tak profil nepřesným.</p>
<p class="p">Nejlepší způsob, jak se ujistit, že je profil přesně nastaven, je udělat si kalibraci sám. Nebo nechat tuto činnost na externí firmě, které dodáte přesný charakterizační stav.</p>
