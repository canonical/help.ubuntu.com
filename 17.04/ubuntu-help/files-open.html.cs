<div class="hgroup"><h1 class="title"><span class="title">Otevírání souborů jinými aplikacemi</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Když poklepáte na soubor ve správci souborů, otevře se v aplikaci, která je pro tento typ souborů nastavena jako výchozí. Můžete jej otevřít v jiné aplikaci, vyhledat další aplikaci nebo nastavit výchozí aplikaci pro všechny soubory daného typu.</p>
<p class="p">Pro otevření souboru jinou aplikací než výchozí na něj klikněte pravým tlačítkem myši a ze zobrazené nabídky vyberte požadovanou aplikaci. Pokud není aplikace, kterou si přejete použít, zobrazena, vyberte <span class="gui">Otevřít jinou aplikací</span>. Ve výchozím stavu zobrazuje správce souborů pouze takové aplikace, o nichž ví, že jsou pro daný typ souboru vhodná. Pro procházení všech na počítači dostupných aplikací vyberte <span class="gui">Zobrazit další aplikace</span>.</p>
<p class="p">Pokud stále nemůžete najít aplikaci, kterou si přejete použít, můžete zkusit vyhledat další aplikace pomocí <span class="gui">Vyhledat aplikace online</span>. Správce souborů vyhledá balíky obsahující aplikace, které jsou pro daný typ souborů vhodné.</p>
</div>
<div id="default" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Změna výchozí aplikace</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Výchozí aplikaci určenou pro otevírání určitého typu souborů můžete snadno změnit. Tím získáte možnost otevřít daný soubor ve vámi preferované aplikace jednoduchým poklepáním. Můžete si například například nastavit, aby se MP3 soubory otevírali ve vašem oblíbeném hudebním přehrávači.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Vyberte soubor typu jehož výchozí aplikaci si přejete změnit. Pokud tedy chcete změnit aplikaci určenou pro otevírání MP3 souborů, vyberte soubor <span class="file">.mp3</span>.</p></li>
<li class="steps"><p class="p">Klikněte pravým tlačítkem na soubor a vyberte <span class="gui">Vlastnosti</span>.</p></li>
<li class="steps"><p class="p">Vyberte záložku <span class="gui">Otevřít s</span>.</p></li>
<li class="steps">
<p class="p">Vyberte vámi požadovanou aplikaci a klikněte na <span class="gui">Nastavit jako výchozí</span>. Ve výchozím stavu zobrazuje správce souborů pouze takové aplikace, u kterých ví, že dokáží s daným typem souboru pracovat, pokud chcete zobrazit všechny aplikace nainstalované na svém počítači, klikněte na <span class="gui">Zobrazit ostatní aplikace</span>.</p>
<p class="p">Pokud <span class="gui">Další aplikace</span> obsahují nějakou aplikaci, kterou chcete v některých případech použít, ale nechcete ji mít jako výchozí, můžete si tuto aplikaci přidat do seznamu <span class="gui">Doporučené aplikace</span> pomocí tlačítka <span class="gui">Přidat</span>. Nyní můžete touto aplikací otevírat vybraný typ souborů pomocí seznamu v kontextové nabídce.</p>
</li>
</ol></div></div></div>
<p class="p">Tímto změníte výchozí aplikaci nejen pro zvolený soubor, ale pro všechny soubory stejného typu.</p>
</div></div>
