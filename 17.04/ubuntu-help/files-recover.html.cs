<div class="hgroup"><h1 class="title"><span class="title">Obnovení souboru z Koše</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud smažete soubor pomocí správce souborů, bude přesunut do <span class="gui">Koše</span> a mělo by být možné jej obnovit.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Pro obnovu souboru z Koše:</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Přejděte na <span class="gui">Launcher</span> a vyberte ikonu <span class="app">Koš</span>, která se nachází na jeho spodním konci.</p></li>
<li class="steps"><p class="p">Pokud se smazaný soubor v koši nachází, klikněte na něj pravým tlačítkem a vyberte <span class="gui">Obnovit</span>. Soubor bude obnoven v té složce, z níž byl původně smazán.</p></li>
</ol></div>
</div></div>
<p class="p">Pokud jste soubor smazali pomocí klávesové zkratky <span class="keyseq"><span class="key"><kbd>Shift</kbd></span>+<span class="key"><kbd>Delete </kbd></span></span> nebo pomocí příkazové řádky, byl soubor smazán natrvalo. Natrvalo smazané soubory není možné obnovit z <span class="gui">Koše</span>.</p>
<p class="p">Existuje několik nástrojů, které jsou za určitých okolností schopny obnovit natrvalo smazané soubory. Tyto nástroje ale bohužel nejsou příliš jednoduše použitelné. Pokud jste si smazali nějaký důležitý soubor, nejlépe uděláte, když se zeptáte na fóru, zda je možné jej obnovit.</p>
</div>
