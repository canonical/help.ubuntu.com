<div class="hgroup"><h1 class="title"><span class="title">Bezpečné odebrání externího média</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Používáte-li externí úložiště, jako například USB flash disky, je doporučeno je před vyjmutím bezpečně odpojit. Pokud je pouze vyjmete, riskujete ztrátu dat, pokud s nimi nějaká aplikace zrovna pracuje. Používáte-li CD nebo DVD, postupujte úplně stejně.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Pro vysunutí výměnného zařízení:</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p"><span class="link"><a href="files-browse.html" title="Procházení souborů a složek">Otevřete správce souborů</a></span>.</p></li>
<li class="steps">
<p class="p">Nalezněte zařízení v postranním panelu. Vedle jeho názvu by měla být zobrazena malá ikonu pro vysunutí. Pro odebrání nebo vysunutí zařízení na tuto ikonu klikněte.</p>
<p class="p">Můžete také kliknout pravým tlačítkem myši na název daného zařízení a vybrat <span class="gui">Vysunout</span>.</p>
</li>
</ol></div>
</div></div>
</div>
<div id="remove-busy-device" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Bezpečné odebrání zařízení, které je používáno</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pokud je nějaký soubor otevřen a používán nějakou aplikací, bezpečné odpojení se nezdaří. Zobrazí se okno s upozorněním že "zařízení je zaněprázdněno" a seznam otevřených souborů. Po zavření všech souborů bude zařízení bezpečně odpojeno. Potom ho můžete bezpečně vyjmout či vysunout.</p>
<p class="p">Pokud nemůžete nějaký soubor zavřít, například zvůli zamrzlé aplikaci, můžete kliknout na soubor pravým tlačítkem a zvolit <span class="gui">Ukončit proces</span>. Aplikace budou násilně ukončena a všechny její soubory budou uzavřeny.</p>
<div class="note note-warning" title="Varování"><div class="inner"><div class="region"><div class="contents"><p class="p">You can also choose <span class="gui">Eject Anyway</span> to
  remove the device without closing the files. This may cause errors in
  applications that have those files open.</p></div></div></div></div>
