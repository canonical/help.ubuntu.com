<div class="hgroup"><h1 class="title"><span class="title">Změna pozadí pracovní plochy</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pro pozadí pracovní plochy si můžete vybrat jiný obrázek nebo nastavit jednoduchou barvu nebo přechod.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte pravým tlačítkem na plochu a vyberte <span class="gui">Změnit pozadí plochy</span>.</p></li>
<li class="steps"><p class="p">Select an image or color. The settings are applied immediately.</p></li>
</ol></div></div></div>
<p class="p">V rozbalovací nabídce umístěné v pravé horní části okna naleznete tři možnosti.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list" style="list-style-type:disc">
<li class="list">
<p class="p">Pro výběr některého z profesionálních obrázků distribuovaných s Ubuntu, klikněte na <span class="gui">Tapety</span>. Kromě pozadí Ubuntu jsou všechny ostatní pozadí vítězi komunitní soutěže o nejlepší pozadí obrazovky.</p>
<p class="p">Některé tapety jsou částečně průhledné a umožňují nastavení podkladové barvy, která poté prosvítá. Pro takové tapety bude v levém dolním rohu zobrazena možnost výběru barvy.</p>
</li>
<li class="list"><p class="p">Pro výběr vlastní fotografie ze složky Obrázky vyberte položku <span class="gui">Složka Obrázky</span>. V této složce ukládá fotografie většina aplikací pro správa fotografií.</p></li>
<li class="list"><p class="p">Pro vybrání jednoduché barvy nebo přechodu vyberte <span class="gui">Barvy a přechody</span>. V pravém dolní části se zobrazí tlačítko pro výběr barvy.</p></li>
</ul></div></div></div>
<p class="p">Kliknutí na <span class="gui">+</span> můžete také vyhledat jakýkoli obrázek umístěný na vašem počítači. Jakýkoli obrázek, který takto přidáte se objeví pod položkou <span class="gui">Složka Obrázky</span>. Ze seznamu jej můžete opět odstranit pomocí tlačítka <span class="gui">-</span>. Odstraněním obrázku se seznamu nesmažete původní soubor.</p>
</div>
