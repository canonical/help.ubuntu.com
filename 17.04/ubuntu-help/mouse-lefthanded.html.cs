<div class="hgroup"><h1 class="title"><span class="title">Použití myši pro leváky</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Leváci si mohou pro pohodlnější práci prohodit chování levého a pravého tlačítka myši nebo touchpadu.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Click the <span class="em">system menu</span> <span class="media"><span class="media media-image"><img src="figures/system-devices-panel.svg" class="media media-inline" alt="Power cog icon"></span></span> and
    select <span class="gui">System Settings</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Myš a touchpad</span>.</p></li>
<li class="steps"><p class="p">In the <span class="gui">General</span> section, switch <span class="gui">Primary button</span>
  to <span class="gui">Right</span>.</p></li>
</ol></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Toto nastavení ovlivní jak myš, tak touchpad a stejně tak také další ukazovací zařízení.</p></div></div></div></div>
