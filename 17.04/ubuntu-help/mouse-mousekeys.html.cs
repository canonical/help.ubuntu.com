<div class="hgroup"><h1 class="title"><span class="title">Click and move mouse pointer using the keypad</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud máte potíže s používáním myši nebo podobného zařízení, můžete kurzor ovládat pomocí numerického bloku na klávesnici. Tato funkce se nazývá <span class="em">myš z klávesnice</span>.</p>
<p class="p">To enable mouse keys using the keyboard:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Stiskněte klávesu <span class="key"><kbd><span class="link"><a href="windows-key.html" title='Která klávesa je "Super"?'>Super</a></span></kbd></span> pro otevření nabídky <span class="gui">Dash</span>.</p></li>
<li class="steps"><p class="p">Napište <span class="input">Usnadnění přístupu</span> a potvrďte <span class="key"><kbd>Enterem</kbd></span> pro otevření nastavení.</p></li>
<li class="steps"><p class="p">Stiskněte klávesu <span class="key"><kbd>Tab</kbd></span> k vybrání záložky <span class="gui">Dívání</span>.</p></li>
<li class="steps"><p class="p">Press <span class="key"><kbd>←</kbd></span> twice to switch to the <span class="gui">Pointing and Clicking</span> tab.</p></li>
<li class="steps"><p class="p">Zmáčkněte klávesu <span class="key"><kbd>↓</kbd></span> k vybrání nabídky<span class="gui">Myš z klávesnice</span> a potvrďte <span class="key"><kbd>Enterem</kbd></span>.</p></li>
<li class="steps"><p class="p">Ujistěte se, že máte vypnutý <span class="key"><kbd>Num Lock</kbd></span>. Nyní můžete pohybovat kurzorem pomocí klávesnice.</p></li>
</ol></div></div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">The above instructions provide the shortest way to enable mouse keys using
 only the keyboard. See <span class="gui">Universal Access</span> settings for more
 accessibility options.</p></div></div></div></div>
<p class="p"></p>
<p class="p">
The numeric keypad is a set of numerical buttons on your keyboard, usually arranged
into a square grid. With mouse keys there are three buttons: primary, alternate, and modifier. Initially the primary button is selected.
Here are some examples of how to use mouse keys:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list" style="list-style-type:disc">
<li class="list"><p class="p">
Each number on the keypad (except 0 and 5) corresponds to a direction. For example, pressing
<span class="key"><kbd>8</kbd></span> will move the pointer upwards and pressing <span class="key"><kbd>2</kbd></span> will move
it downwards.
</p></li>
<li class="list"><p class="p">
To click the selected button, press <span class="key"><kbd>5</kbd></span>.
</p></li>
<li class="list"><p class="p">
To double-click the selected button, quickly press 
<span class="key"><kbd>5</kbd></span> twice or press <span class="key"><kbd>+</kbd></span> once. 
</p></li>
<li class="list"><p class="p">
To right click, 
press <span class="key"><kbd>-</kbd></span> to select the alternate button (if it is not already selected) and press <span class="key"><kbd>5</kbd></span>.
</p></li>
<li class="list"><p class="p">
To middle click, 
press <span class="key"><kbd>*</kbd></span> to select the modifier button (if it is not already selected) and press <span class="key"><kbd>5</kbd></span>.
</p></li>
<li class="list"><p class="p">
To drag and drop using the primary button, 
press <span class="key"><kbd>/</kbd></span> to select the primary button (if it is not already selected),
press <span class="key"><kbd>0</kbd></span> to depress the button, drag the pointer to the required position, and 
press <span class="key"><kbd>.</kbd></span> to release the button.
</p></li>
</ul></div></div></div>
<p class="p">
