<p class="p">
</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">If you have a keyboard without a keypad (as with some laptops),
you may need to hold down the function (<span class="key"><kbd class="key-Fn">Fn</kbd></span>)
key and use certain other keys on your keyboard as a keypad. If you use this
feature often, you may wish to purchase an external USB keypad.
</p></div></div></div></div>
<p class="p">
</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Chcete-li psát čísla a zároveň ovládat pomocí numerického bloku myš, zapněte <span class="key"><kbd>Num Lock</kbd></span>. Pokud je <span class="key"><kbd>Num Lock</kbd></span> zapnutý, je funkce pohybu myši dočasně vypnuta.</p></div></div></div></div>
<p class="p">
</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Čísledné klávesy na horní straně klávesnice nelze použít pro ovládání myši. takto lze použít pouze čísla na numerickém bloku.</p></div></div></div></div>
