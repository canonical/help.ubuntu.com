<div id="plugged-in" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Ujistěte se, že je myš připojena</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pokud máte myš s kabelem, ujistěte se, že je správně připojen do počítače.</p>
<p class="p">Pokud máte USB myš (s obdélníkovým konektorem), zkuste jej připojit do jiného USB portu. Pokud máte PS/2 myš (s malým kulatým konektorem se šesti piny), ujistěte se, že je připojena do zeleného portu pro myš a ne do fialového pro klávesnici. Nebyla-li myš připojena, je možné, že budete muset restartovat počítač.</p>
</div></div>
</div></div>
<div id="connected" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Ujistěte se, že je myš rozpoznána počítačem</span></h2></div>
<div class="region"><div class="contents">
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Stiskněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>T</kbd></span></span> pro otevření <span class="app">Terminálu</span>.</p></li>
<li class="steps"><p class="p">V okně terminálu napište <span class="cmd">xsetpointer -l | grep Pointer</span> přesně tak, jak je napsáno zde a stiskněte <span class="key"><kbd>Enter</kbd></span>.</p></li>
<li class="steps"><p class="p">Objeví se krátký seznam zařízení. Zkontrolujte zda alespoň jedno z nich je <span class="sys">[XExtensionPointer]</span>; vedle je vypsán název myši.</p></li>
<li class="steps"><p class="p">Pokud zde není žádná taková položka (<span class="sys">[XExtensionPointer]</span>), tak myš nebyla nalezena. Zkontrolujte, zda je myš <span class="link"><a href="mouse-problem-notmoving.html#plugged-in" title="Ujistěte se, že je myš připojena">správně připojena</a></span> a v <span class="link"><a href="mouse-problem-notmoving.html#broken" title="Kontrala funkčnosti myši">dobré kondici</a></span>.</p></li>
</ol></div></div></div>
<p class="p">Pokud má vaše myš sériový konektor (RS-232), pro zprovoznění může být potřeba vykonat další akce. Postup závisí na modelu myši.</p>
<p class="p">Oprava nenalezené myši může být komplikovaná. Obraťte se na podporu vydavatele distribuce myslíte-li si, že myš nebyla správně detekována.</p>
</div></div>
</div></div>
<div id="broken" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Kontrala funkčnosti myši</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Připojte myš do jiného počítače a zkontrolujte, zda funguje.</p>
<p class="p">Pokud je myš optická nebo laserová, měla by po zapnutí zespoda slabě svítit. Pokud nesvítí, ujistěte se že je zapnutá. Pokud ani poté myš nesvítí, je pravděpodobně rozbitá.</p>
</div></div>
</div></div>
<div id="wireless-mice" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Kontrola bezdrátové myši</span></h2></div>
<div class="region"><div class="contents">
<div class="list"><div class="inner"><div class="region"><ul class="list" style="list-style-type:disc">
<li class="list"><p class="p">Ujistěte se, že je myš zapnutá. Často je vypínač na spodní straně myši, pomocí něj můžete myš úplně vypnout zatímco je připojena k počítači.</p></li>
<li class="list"><p class="p">Používáte-li myš s Bluetooth, ujistěte se že je správně spárovaná s počítačem. Viz <span class="link"><a href="bluetooth-connect-device.html" title="Spojení počítače se zařízením s Bluetooth">připojení pomocí Bluetooth</a></span>.</p></li>
<li class="list"><p class="p">Klikněte na tlačítko a pohybujte myší. Některé myši se z energetických důvodů uspí dokud s nimi nekliknete. Viz <span class="link"><a href="mouse-wakeup.html" title="Myš reaguje se zpoždění dříve než začne pracovat">probuzení myši</a></span>.</p></li>
<li class="list"><p class="p">Ujistěte se, že je baterie myši nabita.</p></li>
<li class="list"><p class="p">Ujistěte se, že přijímač (dongle) je správně připojen do počítače.</p></li>
<li class="list"><p class="p">Pokud myš a přijímač mohou pracovat na různých frekvencích, ujistěte se že používají přesně stejnou frekvenci.</p></li>
<li class="list"><p class="p">Možná bude třeba smáčknout tlačítko na myši, přijímači či na obou pro navázání spojení. Podívejte se do návodu pro více informací.</p></li>
</ul></div></div></div>
<p class="p">Většina RF (rádiových) myší pracuje automaticky po připojení k počítači. Pokud máte Bluetooth nebo IR (infračervenou) bezdrátovou myš, možná bude pro zprovoznění třeba dalších úkonů. Postup závisí na modelu myši.</p>
</div></div>
