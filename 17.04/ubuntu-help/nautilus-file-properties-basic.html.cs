<div class="hgroup"><h1 class="title"><span class="title">Vlastnosti souborů</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pro zobrazení informací o souborech nebo složkách na něj klikněte pravým tlačítkem a vyberte <span class="gui">Vlastnosti</span>. Můžete také vybrat soubor stisknout <span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Enter</kbd></span></span>.</p>
<p class="p">Okno Vlastnosti souboru zobrazuje typ souboru, velikost a datum a čas poslední změny. Pokud tyto informace potřebujete často, můžete je mít stále zobrazené ve <span class="link"><a href="nautilus-list.html" title="Nastavení sloupců ve správci souborů">sloupcovém roložení</a></span> nebo jako <span class="link"><a href="nautilus-display.html#icon-captions" title="Popisky ikon">ikony s popisky</a></span>.</p>
<p class="p">Informace obsažené na kartě <span class="gui">Základní</span> jsou vysvětleny níže. Jsou zde také karty <span class="gui"><span class="link"><a href="nautilus-file-properties-permissions.html" title="Nastavení oprávnění souborů">Oprávnění</a></span></span> a <span class="gui"><span class="link"><a href="files-open.html#default" title="Změna výchozí aplikace">Otevřít s</a></span></span>. Pro některé typy souborů jako obrázky a videa zde bude také další karta obsahující informace jako rozměry, trvání a kodeky.</p>
</div>
<div id="basic" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Základní vlastnosti</span></h2></div>
<div class="region"><div class="contents"><div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms"><span class="gui">Název</span></dt>
<dd class="terms"><p class="p">Změnou tohoto okna můžete přejmenovat soubor. Soubor můžete také přejmenovat mimo toto okno. Navštivte <span class="link"><a href="files-rename.html" title="Přejmenování souboru nebo složky">Přejmenování souboru nebo složky</a></span>.</p></dd>
<dt class="terms"><span class="gui">Typ</span></dt>
<dd class="terms">
<p class="p">Tato položka vám pomůže identifikovat typ souboru, jako například PDF dokument, OpenDocument Text nebo obrázek JPEG. Typ souboru určuje, jakou aplikací se má soubor otevírat a další věci. Například nemůžete otevřít obrázek hudebním přehrávačem. Pro více informací navštivte <span class="link"><a href="files-open.html" title="Otevírání souborů jinými aplikacemi">Otevírání souborů jinými aplikacemi</a></span>.</p>
<p class="p">V závorce je také zobrazen <span class="em">MIME typ</span> souboru. MIME typ je standardní cesta, kterou počítače používají pro odkázání se na typ souboru.</p>
</dd>
<dt class="terms">Obsah</dt>
<dd class="terms"><p class="p">Toto pole je zobrazeno pokud si prohlížíte vlastnosti adresáře. Pomůže vám zjisti počet položek v adresáři. Pokud adresář obsahuje další složky, každý vnitřní adresář je počítán jako jeden i přesto, že obsahuje další položky. Každý soubor je také počítán jako jedna položka. Pokud je adresář prázdný, obsah bude zobrazen jako <span class="gui">nic</span>.</p></dd>
<dt class="terms">Velikost</dt>
<dd class="terms">
<p class="p">Toto pole je zobrazeno pokud si prohlížíte soubor (ne složku). Velikost souboru vám říká, kolik prostoru na disku zabere. Indikuje to také, jak dlouho bude trvat než se soubor stáhne nebo pošle emailem (velké soubory zaberou delší dobu pro poslání/stažení).</p>
<p class="p">Velikosti mohou být uvedeny v bytech, KB, MB nebo GB. V případě posledních tří údajů bude velikost zobrazena také v bytech v závorce. Technicky, 1 KB je 1024 bytů, 1 MB je 1024 KB atd.</p>
</dd>
<dt class="terms">Umístění</dt>
<dd class="terms"><p class="p">Umístění každého souboru na vašem počítače je dáno tzv. <span class="em">absolutní cestou</span>. Toto je unikátní "adresa" souboru na vašem počítači vytvořená ze seznamu složek, které musíte projít, abyste nalezli daný soubor. Pokud by například uživatel Jim měl soubor <span class="file">Resume.pdf</span> ve svém domovském adresáři, jeho umístění by bylo <span class="file">/home/jim/Resume.pdf</span>.</p></dd>
<dt class="terms">Svazek</dt>
<dd class="terms"><p class="p">Souborový systém nebo zařízení, na němž je soubor umístěn. Toto ukazuje, kde je soubor fyzicky umístěn, pokud je například na pevném disku nebo na CD nebo <span class="link"><a href="nautilus-connect.html" title="Procházení souborů na server nebo po síti">síti nebo souborovém serveru</a></span>. Pevné disky mohou být rozděleny na několik <span class="link"><a href="disk-partitions.html" title="Správa disků a oddílů">diskových oddílů</a></span>. Patřičný oddíl bude zobrazen pod <span class="gui">Svazek</span>.</p></dd>
<dt class="terms">Volné místo</dt>
<dd class="terms"><p class="p">Tato položka je zobrazována pouze pro složky. Zobrazuje informaci o volném prostoru na disku, na němž je složka umístěna. Je to užitečné pro zjištění, zda je disk plný.</p></dd>
<dt class="terms">Použitý</dt>
<dd class="terms"><p class="p">Datum a čas, kdy byl soubor naposledy otevřen.</p></dd>
<dt class="terms">Změněný</dt>
<dd class="terms"><p class="p">Datum a čas, kdy byl soubor naposledy změněný a uložený.</p></dd>
</dl></div></div></div></div></div>
