<div class="hgroup"><h1 class="title"><span class="title">Společenské sítě z počítače</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">V Ubuntu můžete na sociální sítě přispívat přímo z pracovního prostředí systému. Ubuntu používá tzv. rozšíření <span class="app">Přátelé</span>, které vám umožní spravovat sociální sítě z jednoho místa a přidávat příspěvky z <span class="gui">Nabídky zpráv</span> bez otevření webových stránek.</p>
<p class="p">Pro nastavení účtů společenský sítí:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">
      Open the <span class="link"><a href="unity-menubar-intro.html" title="Ovládání aplikací &amp; nastavení v hlavním panelu">System menu</a></span> on the right hand side of the menu bar and select "System Settings...".
    </p></li>
<li class="steps"><p class="p">Vyberte <span class="gui">Online účty</span></p></li>
<li class="steps"><p class="p">Vyberte sociální síť, kterou chcete nastavit a klikněte na <span class="gui">Přidat účet...</span></p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Autorizovat</span>, zadejte nastavení pro svůj účet a pokračujte dle instrukcí.</p></li>
</ol></div></div></div>
<p class="p">V pravé části <span class="link"><a href="unity-menubar-intro.html" title="Ovládání aplikací &amp; nastavení v hlavním panelu"> nabídky zpráv</a></span>, v sekci  <span class="gui">Přenosy</span>, vidíte zprávy ze sociálních sítí. Klikněte na jakoukoliv položku v této sekci a otevře se aplikace <span class="gui">Friends</span>, kde můžete číst nebo přidávat zprávy na sociální síť.</p>
</div>
