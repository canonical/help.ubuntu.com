<div class="hgroup"><h1 class="title"><span class="title">Nastavení, v jakém webovém prohlížeči budou otevírány odkazy</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud v jakékoli aplikaci kliknete na odkaz na webovou stránku, automaticky se otevře webový prohlížeč s danou stránkou. Pokud máte nainstalováno více prohlížečů, může se stát, že daná stránka nebude otevřena v prohlížeči, v němž byste ji chtěli otevřít. Pro vyřešení tohoto problémů musíte nastavit výchozí webový prohlížeč:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Click the <span class="em">system menu</span> <span class="media"><span class="media media-image"><img src="figures/system-devices-panel.svg" class="media media-inline" alt="Power cog icon"></span></span> and
  select <span class="gui">System Settings</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Podrobnosti</span> a ze seznamu v levé části okna vyberte <span class="gui">Vychozí aplikace</span>.</p></li>
<li class="steps"><p class="p">Změnou položky <span class="gui">Web</span> nastavte prohlížeč v němž chcete otevírat odkazy.</p></li>
</ol></div></div></div>
<p class="p">Když otevřete jiný než vámi právě nastavený webový prohlížeč, může se stát, že vám oznámí, že již není výchozím prohlížečem. Pokud se tak stane klikněte na tlačítko <span class="gui">Zrušit</span> nebo podobné, aby se nepokusil nastavit sám sebe jako výchozí prohlížeč.</p>
</div>
