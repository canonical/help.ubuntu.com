<div class="hgroup"><h1 class="title"><span class="title">Měl bych skenovat své emaily kvůli virům?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Viry jsou programy které po průniku do počítače mohou způsobovat problémy. Běžný způsob šíření je pomocí infikovancýh emailů.</p>
<p class="p">Viry, které by mohli ohrozit linuxové počítače jsou velmi vzácné, takže je <span class="link"><a href="net-antivirus.html" title="Potřebuji antivirový software?">nepravděpodobné, že byste nějaký chytily přes mail nebo podobně</a></span>. Pokud byste náhodou dostali nějaký email se skrytým virem, nebude mít na vašem počítači pravděpodobně žádný účinek. Proto nejspíš nepotřebujete skenovat své maily kvůli virům.</p>
<p class="p">Možná chcete prohledávat emaily při přeposílání pošty, aby se nestalo že vir přepošlete někomu jinému. Napříkla, pokud váš kamarád používá Windows a pošle zavirovaný email, který přepošlete někomu jiného kdo také používá Windows, je možné že také chytí infekci, ikdyž vy jste díky Linuxu imunní. Můžete si antivirus nainstalovat, ale většina uživatelů Windows nebo MacOS antivirus stejně má.</p>
