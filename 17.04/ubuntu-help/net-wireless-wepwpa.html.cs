<div class="hgroup"><h1 class="title"><span class="title">Co znamená WEP a WPA?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">WEP, WPA a WPA2 jsou názvy různých nástrojů pro zabezpečení bezdrátové komunikace. Šifrování skryje spojení před ostatními takže vás nikdo nemůže odposlouchávat nebo sledovat na které stránky se díváte. Standard WEP znamená <span class="em">Wired Equivalent Privacy</span> a WPA značí <span class="em">Wireless Protected Access</span>. WPA2 je druhá verze standardu WPA.</p>
<p class="p">Použití <span class="em">nějakého</span> šifrování je sice vždy lepší než nepoužití žádného, ale WEP je z těchto standardů nejméně bezpečný a měl(a) byste se mu vyhnout. WPA2 je z těchto tří nejlepší volbou. Pokud vaše karta a router umí WPA2, použijte tento standard.</p>
