<div class="hgroup"><h1 class="title"><span class="title">Můj počítač se připojuje k nesprávné síti</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Když vypnete počítač nebo se přesunete na jíné místo, počítač se pokusí automaticky připojit na síť kterou již z minula zná. Pokud se pokaždé pokouší připojit na špatnou síť (místo toho aby se připojil na správnou), udělejte následující:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="gui">nabídku sítí</span> na horním panelu a vyberte <span class="gui">Upravit připojení</span>.</p></li>
<li class="steps"><p class="p">Otevřte panel <span class="gui">Bezdrátová</span> a najděte síť ke které se <span class="em">nechcete</span> připojovat.</p></li>
<li class="steps"><p class="p">Klikněte na  <span class="gui">Odstranit</span>. Počítač se již nikdy automaticky nepokusí k této síti připojit.</p></li>
</ol></div></div></div>
<p class="p">Pokud se k ní budete v bucoucnu chtít zase připojit, jednoduše ji zvolte v seznamu bezdrátových sítí, který se objeví po kliknutí na menu Sítě - stejěn jako k ostatním sítím.</p>
</div>
