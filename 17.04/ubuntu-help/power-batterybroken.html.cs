<div class="hgroup"><h1 class="title"><span class="title">Chybové hlášení oznamuje nízkou kapacitu baterie</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Při prvním přihlášení se může zobrazovat zpráva říkající:</p>
<div class="quote"><div class="inner"><div class="region"><blockquote class="contents"><p class="p"><span class="em">Baterie může být poškozena. Vaše baterie má velmi malou kapacitu, což znamená, že může být stará nebo rozbitá.</span></p></blockquote></div></div></div>
<p class="p">Tato zpráva se zobrazuje ve chvíli, když počítač zjistí, že vaše baterie má nízkou kapacitu uložení energie. Nejpravděpodobnější důvod pro to je, že vaše je stará, pravděpodobně není rozbitá, takže není důvod k obavám.</p>
<p class="p">Časem se snižuje kapacita všech notebookových baterií. Po čase (běžně po roce a více) má baterie pouze zlomek kapacity, jakou měla v době, kdy byla nová. Výše zmíněná zpráva se zobrazí právě v tomto případě.</p>
<p class="p">Pokud je váš počítač nový, nebo máte novou baterii, tak by měla kapacita baterie dosahovat vysokých procent. Pokud ne, baterie může výt vadná a měl(a) byste zvážit její náhradu.</p>
</div>
