<div class="hgroup"><h1 class="title"><span class="title">Odhadovaná výdrž na baterie nesouhlasí</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Při kontrole, jak dlouho vydrží ještě počítač v provozu na baterie, můžete zjistit, že oznamovaný čas je odlišný od toho, jak dlouho baterie skutečně vydrží. Je to způsobeno tím, že výdrž na baterie může být jen odhadován. Normálně se odhad časem zlepšuje.</p>
<p class="p">Při předpovídání životnosti baterie je třeba započítat mnoho faktorů. Jedním z nich je aktuální spotřeba počítače: ta závisí na množství otevřených programů, připojených zařízeních, náročnosti úloh (přehrávání DVD nebo konvertování hudby)... Tyto faktory se mění každou chvíli takže je těžké je předvídat.</p>
<p class="p">Další faktorem je způsob, jakým se baterie vybíjí. Některé baterie ztrácí náboj tím rychleji, čím více jsou vybité jsou. Bez znalosti toho, jak se baterie vybíjí, je odhad výdrže na baterii skutečně jen velmi hrubý.</p>
<p class="p">V průběhu vybíjení baterie správce napájení zjistí, jak se vybíjí a naučí se, jak lépe odhadovat výdrž počítače při běhu na baterii. I tak ale čas nebude nikdy zcela přesný.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents">
<p class="p">Pokud dostáváte zcela nereálná data ohledně výdrže na baterii (například stovky dní), správci napájení pravděpodobně chybí nějaká data nutná pro správný odhad.</p>
<p class="p">Pokud na chvíli odpojíte napájení a necháte počítač běžet jen na baterii a poté zase kabel připojíte, správce napájení by měl být schopen získat data která potřebuje.</p>
