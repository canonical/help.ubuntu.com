<div class="hgroup"><h1 class="title"><span class="title">Můj počítač se silně přehřívá</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Většina počítačů se po chvíli ohřeje a některé až do horka. To je normální, je to cesta jak se vnitřní součásti chladí. Ovšem příliš horký počítač může být také přehřátý a to může způsobit poškození.</p>
<p class="p">Většina notebooků se stane rozumně teplou po chívli používání. V zásadě se nemusíte ničeho obávat - počítače produkují mnoho tepla a notebooky jsou kompaktní, takže se musí chladit rychle a jejich konstrukce se také ohřívá. Některé notebooky se ovšem zahřívají příliš a jejich používání pak není pohodlné, což je důsledek lajdáckého návrhu jejich chlazení. Někdy můžete koupit příslušenství usnadňující chlazení.</p>
<p class="p">Nedostatečné chlazení počítače způsobí, že bude na dotek horký. Pokud vás jeho teplota znopokojuje, můžete koupit další ventilátory a nebo zkontrolujte, zda nejsou ventilátory zanesené prachem a jinými nečistotami. Také můžete počítač přesunout na lépe větrané místo - když je v uzavřeném prostoru, vzduch nemůže dostatečně cirkulovat a chlazení má horší účinnost.</p>
<p class="p">Někteří lidé se obávají zdravotních rizik při používání horkého počítače. Tvrdí, že používání notebooku na klíně má vliv na (mužskou) plodnost, někteří lidé se o extrémně horký počítač popálili. Pokud se obáváte o své své zdraví, navštivte svého lékaře, nebo si přestaňte dávat počítač do klína.</p>
<p class="p">Nejmodernější počítače se při příliš vysoké teplotě sami vypnou, což sje chrání před poškozením. To může být příčina častého vypínání vašeho počítače. Pokud se váš počítač často přehřívá, nechte si ho spravit.</p>
