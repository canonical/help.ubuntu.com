<div class="hgroup"><h1 class="title"><span class="title">Po probuzení nejsem připojen k bezdrátové síti</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">If you have suspended your computer, or it has hibernated, you may find that
your wireless internet connection does not work when you resume it again. This
happens when the <span class="link"><a href="hardware-driver.html" title="Co je to ovladač?">driver</a></span> for the wireless
device does not fully support certain power saving features. Typically, the
wireless connection fails to turn on properly when the computer is resumed.</p>
<p class="p">Pokud se vám toto stane, zkuste bezdrátovou síť vypnout a zase zapnout:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Click the <span class="em">system menu</span> <span class="media"><span class="media media-image"><img src="figures/system-devices-panel.svg" class="media media-inline" alt="Power cog icon"></span></span> and
    select <span class="gui">System Settings</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Síť</span> a vyberte <span class="gui">Bezdrátové</span></p></li>
<li class="steps"><p class="p">Vypněte a znovu zapměte bezdrátovou síť</p></li>
<li class="steps"><p class="p">Pokud síť stále nefunguje, <span class="gui">vypněte bezdrát</span> a znovu ho zapněte.</p></li>
</ol></div></div></div>
<p class="p">Pokud ani to nefunguje, restartování počítače určitě pomůže.</p>
