<div class="hgroup"><h1 class="title"><span class="title">Proč se můj počítač neprobudí po uspání do paměti?</span></h1></div>
<div class="region">
<div class="contents"><p class="p">Pokud <span class="link"><a href="power-suspend.html" title="Co se stane, když uspím svůj počítač?">uspíte</a></span> nebo <span class="link"><a href="power-hibernate.html" title="Jak mohu uspat svůj počítač na disk?">hibernujete</a></span> počítač a poté ho zkusíte znovu zapnout, může se stát že se neprobudí. To se může stát když váš hardware uspávání nepodporuje.</p></div>
<div id="resume" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Můj počítač se uspává ale neprobouzí</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Uspaný počítač by se měl po stisknutí nějaké klávesy nebo po kliknutí myši probudit a zeptat se vás na heslo. Pokud se to nestane, zkuste stisknout tlačítko napájení (nedržte ho, zmáčkněte ho krátce  jako klávesu na klávesnici).</p>
<p class="p">Pokud se ani poté neprobudí, ujistěte se že je monitor zapnutý a zkuste znovu zmáčknout nějakou klávesu.</p>
<p class="p">Když nic jiného nezbývá, vypněte počítač podržením tlačítka napájení po dobu 5 až 10 sekund. Tím ztratíte veškerá neuložená data, ale budete poté moci počítač zase zapnout.</p>
<p class="p">Pokud uspávání nikdy nefunguje, pak tato funkce není vaším hardwarem podporována.</p>
<div class="note note-warning" title="Varování"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud se baterie vybíjí a vy nezajistíte jiný zdroj napájení (elektřinu ze zásuvky), počítač se vypne.</p></div></div></div></div>
</div></div>
</div></div>
<div id="hibernate" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Žádné aplikace či dokumenty se po zapnutí počítače neotevřou</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pokud se po hibernaci  a probuzení počítače vaše sezení neobnoví, pravděpodobně se hibernace nepovedla. Někdy je to stává kvůli malému problému a příště se hibernace podaří. Hibernace se nemusí povést, pokud jste nainstaloval(a) software který vyžaduje pro dokončení instalace restart počítače - v takovém případě vždy počítač vypněte, nehibernujte.</p>
<p class="p">Je také možné že tuto funkci hardware počítače nepodporuje. Mohou to způsobit i některé Linuxové ovladače. Pokud se hibernace nepovede ani napodruhé, je pravděpodobně problém v hardwaru.</p>
</div></div>
</div></div>
<div id="hardware" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Bezdrátové připojení (nebo jiné zařízení) nefunguje pro probuzení počítače</span></h2></div>
<div class="region"><div class="contents">
<p class="p">If you suspend or hibernate your computer and then resume it again, you
  may find that your internet connection, mouse, or some other device doesn't
  work properly. This could be because the device's driver doesn't
  properly support suspend or hibernate. This is a <span class="link"><a href="hardware-driver.html" title="Co je to ovladač?">problem with the driver</a></span> and not the device
  itself.</p>
<p class="p">Pokud má zařízení vlastní vypínač, zkuste ho vypnouot a znovu zapnout. Ve většině případů začne znovu fungovat. Pokud je připojené přes USB, odpojte ho a znovu připojte - mělo by fungovat.</p>
<p class="p">Pokud zařízení nemůžete vypnout nebo odpojit, nebo to nefunguje, budete muset restartovat počítač.</p>
</div></div>
