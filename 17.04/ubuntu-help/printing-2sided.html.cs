<div class="hgroup"><h1 class="title"><span class="title">Tisk po obou stranách a rozložení více stran</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Chcete-li vždy tisknout oboustranně:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="guiseq"><span class="gui">Soubor</span> ▸ <span class="gui">Tisk</span></span>.</p></li>
<li class="steps">
<p class="p">Klikněte na <span class="gui">nastavení stránky</span> v okně Tisk a vyberte <span class="gui">Oboustranně</span>. Pokud tato volba není k dispozici, vaše tiskárna oboustranný tisk neumí.</p>
<p class="p">Tiskárny řeší oboustranný tisk různými způsoby. Zkuste si něco vytisknout ať vidíte jak vaše tiskárna funguje.</p>
</li>
<li class="steps"><p class="p">Můžete tisknout více stran dokumentu na jednu stranu papíru. Podívejte se na volbu <span class="gui">Stránek na stranu papíru</span>.</p></li>
</ol></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Dostupnost těchto voleb závisí na typu tiskárny kterou máte a na aplikaci ze které třisknete. Toto nastavení by mělo být dostupné vždy.</p></div></div></div></div>
