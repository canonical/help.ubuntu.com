<div class="hgroup"><h1 class="title"><span class="title">Nastavení aplikací zobrazených v Launcheru</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">To add an application to the <span class="link"><a href="unity-launcher-intro.html" title="Použití Launcheru pro spouštění aplikací">Launcher</a></span> for quick access:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list" style="list-style-type:disc">
<li class="list"><p class="p">Drag the application's icon from the <span class="link"><a href="unity-dash-intro.html" title="Find applications, files, music, and more with the Dash">Dash</a></span> onto the Launcher.</p></li>
<li class="list"><p class="p">Nebo, pokud je daná aplikace spuštěna, můžete na ni kliknout pravým tlačítkem myši a vybrat <span class="gui">Přidat do Launcheru</span>.</p></li>
</ul></div></div></div>
<p class="p">The Launcher icon order can be changed by dragging an icon off of the 
Launcher, and then back onto it in the desired location.</p>
<p class="p">To remove an application icon from the Launcher, right click on the 
application icon and select <span class="gui">Unlock from Launcher</span>.</p>
