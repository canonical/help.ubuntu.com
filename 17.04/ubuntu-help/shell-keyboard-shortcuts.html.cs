<div class="hgroup"><h1 class="title"><span class="title">Užitečné klávesové zkratky</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Tato stránka obsahuje přehled klávesových zkratek určených pro jednodušší a efektivnější pohyb v pracovním prostředí. Pokud nemůžete vůbec použít myš nebo jiné ukazovací zařízení, navštivte <span class="link"><a href="keyboard-nav.html" title="Navigace pomocí klávesnice">Navigace pomocí klávesnice</a></span> pro informace o možnostech práce pouze s klávesnicí.</p>
<div class="table ui-expander">
<div class="yelp-data yelp-data-ui-expander" dir="ltr" data-yelp-expanded="true"></div>
<div class="inner">
<div class="title title-table"><h2><span class="title">Práce s pracovním prostředím</span></h2></div>
<div class="region"><table class="table" style="border-top-style: solid;border-bottom-style: solid;">
<tr>
<td><p class="p"><span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>F4</kbd></span></span></p></td>
<td><p class="p">Zavřít aktuální okno</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>F2</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p">Zobrazení příkazového okna (pro rychlé spouštění příkazů)</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p"><span class="link"><a href="shell-windows-switching.html" title="Přepínání mezi okny">Rychlé přepínání mezi okny.</a></span> Pro změnu směru podržte <span class="key"><kbd>Shift</kbd></span>.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>`</kbd></span></span></p></td>
<td style="border-top-style: solid;">
<p class="p">Přepínání mezi okny aktuální aplikace nebo mezi okny vybrané aplikace po použití <span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span>.</p>
<p class="p">Tato zkratka využívá klávesu <span class="key"><kbd>`</kbd></span> z amerických klávesnic, kde je <span class="key"><kbd>`</kbd></span> nad klávesou <span class="key"><kbd>Tab</kbd></span>. Na ostatních klávesnicích je tato zkratka <span class="key"><kbd>Alt</kbd></span> a jakákoli klávesa nad <span class="key"><kbd>Tab</kbd></span>.</p>
</td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><a href="windows-key.html" title='Která klávesa je "Super"?'><kbd>Super</kbd></a></span>+<span class="key"><kbd>S</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p">Aktivace přepínače ploch. Náhled na všechny <span class="link"><a href="shell-workspaces.html" title="Co jsou to pracovní plochy a jak mi pomohou?">plochy</a></span>.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><a href="windows-key.html" title='Která klávesa je "Super"?'><kbd>Super</kbd></a></span>+<span class="key"><kbd>W</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p">Aktivace "Expo". Zobrazení všech oken ze aktuální pracovní plochy.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>šipky</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p"><span class="link"><a href="shell-workspaces-switch.html" title="Přepínání mezi pracovními plochami">Přepínání mezi pracovními plochami.</a></span></p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Shift</kbd></span>+<span class="key"><kbd>šipky</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p"><span class="link"><a href="shell-workspaces-movewindow.html" title="Přesun okna na jinou pracovní plochu">Přesun aktuálního okna na jinou pracovní plochu.</a></span></p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Delete</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p"><span class="link"><a href="shell-exit.html" title="Odhlášení, vypínání a přepínání uživatelů">Odhlášení.</a></span></p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><a href="windows-key.html" title='Která klávesa je "Super"?'><kbd>Super</kbd></a></span>+<span class="key"><kbd>D</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p">Skrytí všech oken a zobrazení pracovní plochy. Po dalším stisknutí obnovení všech oken.</p></td>
</tr>
<tr>
<td style="border-top-style: solid;"><p class="p"><span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>L</kbd></span></span></p></td>
<td style="border-top-style: solid;"><p class="p"><span class="link"><a href="shell-exit.html#lock-screen" title="Uzamčení obrazovky">Uzamčení obrazovky.</a></span></p></td>
</tr>
</table></div>
