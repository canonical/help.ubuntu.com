<div class="hgroup"><h1 class="title"><span class="title">Přepínání mezi okny</span></h1></div>
<div class="region">
<div class="contents"></div>
<div id="launcher" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Z Launcheru</span></h2></div>
<div class="region"><div class="contents"><div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">
Show the <span class="gui">Launcher</span> by moving your mouse to the top left corner of your screen.
    </p></li>
<li class="steps"><p class="p">Aplikace, které jsou spuštěny, mají na levé straně zobrazenu malou bílou trojúhelníkovou šipku. Pro zobrazení běžící aplikace prostě klikněte na danou ikonu.</p></li>
<li class="steps"><p class="p">Pokud má spuštěná aplikace otevřeno více oken, bude na levé straně zobrazeno více šipek. Pro zobrazení zmenšeného výběru všech otevřených oken klikněte podruhé na danou ikonu. Klikněte na okno, které si přejete vybrat.</p></li>
</ol></div></div></div></div></div>
</div></div>
<div id="keyboard1" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">From the keyboard (method 1)</span></h2></div>
<div class="region"><div class="contents">
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p"><span class="gui">Přepínač oken</span> vyvoláte pomocí <span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span>.</p></li>
<li class="steps">
<p class="p">Still holding down the <span class="key"><kbd>Alt</kbd></span> key, press
  <span class="key"><kbd>Tab</kbd></span> to cycle forwards through the list of icons in the window switcher, or
  <span class="keyseq"><span class="key"><kbd>Shift</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span> to cycle backwards.</p>
<p class="p">Previews of applications that have multiple windows pop up as you cycle through the icons.</p>
<p class="p">To display a preview of an application that has a single window press the <span class="key"><kbd>↓</kbd></span> key.</p>
<p class="p">You can also move between the icons 
  with the <span class="key"><kbd>→</kbd></span> or <span class="key"><kbd>←</kbd></span> keys, or select an icon by
 clicking it with the mouse.</p>
</li>
<li class="steps"><p class="p">Release <span class="key"><kbd>Alt</kbd></span> to switch to the window for the current icon.</p></li>
</ol></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Zobrazena budou pouze okna z aktuální <span class="link"><a href="shell-workspaces.html" title="Co jsou to pracovní plochy a jak mi pomohou?">pracovní plochy</a></span>. Pro zobrazení oken ze všech pracovních ploch podržte klávesy <span class="key"><kbd>Ctrl</kbd></span> a <span class="key"><kbd>Alt</kbd></span> a poté stiskněte <span class="key"><kbd>Tab</kbd></span> nebo <span class="keyseq"><span class="key"><kbd>Shift</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span>.</p></div></div></div></div>
</div></div>
</div></div>
<div id="keyboard2" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">From the keyboard (method 2)</span></h2></div>
<div class="region"><div class="contents"><div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Po stisknutí <span class="keyseq"><span class="key"><kbd><span class="link"><a href="windows-key.html" title='Která klávesa je "Super"?'>Super</a></span></kbd></span>+<span class="key"><kbd>W</kbd></span></span> se zobrazí náhledy všech oken.</p></li>
<li class="steps"><p class="p">Klikněte na okno, na které si přejete přepnout.</p></li>
</ol></div></div></div></div></div>
