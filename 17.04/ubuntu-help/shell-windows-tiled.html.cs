<div class="hgroup"><h1 class="title"><span class="title">Dlaždice</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete maximalizovat jedno okno na pravou a levou polovinu obrazovky což vám umožňuje pohodlně pracovat s dvěma okny najednou.</p>
<p class="p">Pro maximalizaci okna na polovinu obrazovky chyťte titulkový pruh okna a přesuňte ho ke kraji dokud se polovina obrazovky nezvýrazní. Pomocí klávesnice držte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd><span class="link"><a href="windows-key.html" title='Která klávesa je "Super"?'>Super</a></span></kbd></span></span> a stiskněte <span class="key"><kbd>Left</kbd></span> nebo <span class="key"><kbd>Right</kbd></span> šipku.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Podržte klávesu <span class="key"><kbd>Alt</kbd></span> a myší přetáhněte okno do požadované pozice.</p></div></div></div></div>
