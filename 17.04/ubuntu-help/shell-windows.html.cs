<div class="hgroup"><h1 class="title"><span class="title">Okna a pracovní plochy</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Like other desktops, Unity uses windows to display your running applications. Using both the <span class="gui">Dash</span> and the <span class="gui">Launcher</span>, you can launch new applications and control which window is active.</p>
<p class="p">Jako doplněk k oknům můžete pro své aplikace používat také více pracovních ploch. Pro lepší pochopení těchto možností navštivte níže uvedená témata nápovědy okna a pracovní plochy.</p>
