<div class="hgroup"><h1 class="title"><span class="title">Neslyším žádný zvuk</span></h1></div>
<div class="region">
<div class="contents"><p class="p">Pokud neslyšíte vůbec nic, zkuste následující postup.</p></div>
<div id="mute" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Ujistěte se, že zvuk není ztlumen</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Klikněte na <span class="gui">nabídku zvuku</span> v horním panelu (vypadá jako reproduktor) a ujistěte, že zvuk není ztlumený nebo vypnutý.</p>
<p class="p">Některé notebooky mají speciální vypínače nebo klávesy—použijte tuto klávesu, chcete-li zvuk zapnout.</p>
<p class="p">Také si zkontrolujte zda nemáte vypnutý zvuk ve vaší aplikaci (například ztišený přehrávač hudby). přehrávače mají ztišovací a hlasitostní tlačítka přímo v jejich hlavním okně, podívejte se na to. Také zkontrolujte hlavní panel a vyberte <span class="gui">nastavení zvuku</span>. V objevivším se okne se přepněte na panel <span class="gui">Aplikace</span> a zkontrolujte zda nejsou ztišené.</p>
</div></div>
</div></div>
<div id="speakers" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zkontrolujte, zda-li jsou reproduktory zapnuty a správně připojeny.</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Jestliže máte externí reproduktory, ujistěte se, jestli jsou zapnuty a zvuk je zesílen. Také zkontrolujte, je-li kabel správně připojen do výstupní zdířky, která bývá obvykle zelená.</p>
<p class="p">Některé zvukové karty mají možnost přepnout funkci výstupních zdířek - výstup pro reproduktory a vstup pro mikrofon. Výstupní zdířka může být jiná, běží-li tato karta na Linuxu, Windows či Mac OS. Zkuste kabel přepojit do jiné zdířky a zkontrolujte, zda-li to funguje.</p>
<p class="p">Poslední věc, která se musí překontrolovat, je zapojení kabeláže do samotných reproduktorů. Některé reproduktory mají více vstupů.</p>
</div></div>
</div></div>
<div id="device" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zkontrolujte, jestli je vybráno správné zvukové zařízení</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Některé počítače mají více zvukových zařízení. Některé z nich můžou zvuk přehrávat, některé ne, měli byste tudíž zkontrolovat, máte-li vybráno správné zařízení. Vybráním správného zařízení se můžete vyhnout náhodným omylům, které mohou nastat.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="gui">nabídku zvuku</span> v <span class="gui">horním panelu</span> a vyberte <span class="gui">Předvolby zvuku</span>.</p></li>
<li class="steps"><p class="p">Ve zobrazeném okně <span class="gui">Zvuk</span> zkuste ze seznamu <span class="gui">Přehrávat zvuk z</span> vybrat jiný výstup.</p></li>
<li class="steps"><p class="p">Pro vybrané zařízení klikněte na <span class="gui">Testovat zvuk</span>. Ve zobrazeném okně klikněte na tlačítko pro každý reproduktor. Každé tlačítko přehraje hlášení se svou pozicí na odpovídajícím kanálu.</p></li>
<li class="steps"><p class="p">Jestliže daný postup pro vybrané zařízení nefunguje, můžete totéž zkusit i pro další zařízení.</p></li>
</ol></div></div></div>
</div></div>
</div></div>
<div id="hardware-detected" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zkontrolujte, zda-li byla zvuková karta správně detekována.</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Nebyla-li zvuková karta správně detekována, počítač nebude schopen přehrát žádný zvuk. Možným důvodem mohou být chybějící ovladače.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Přejděte do nabídky <span class="link"><a href="unity-dash-intro.html" title="Find applications, files, music, and more with the Dash">Dash</a></span> a otevřete Terminál.</p></li>
<li class="steps"><p class="p">Napište <span class="cmd">aplay -l</span> a stiskněte <span class="key"><kbd>Enter</kbd></span>.</p></li>
<li class="steps"><p class="p">Zobrazí se seznam zařízení. Jestliže zde není žádné <span class="gui">zařízení pro zvukový výstup</span>, zvuková karta nebyla detekována.</p></li>
</ol></div></div></div>
<p class="p">Jestliže není zvuková karta detekována, budete muset ovladače nainstalovat ručně. Jak instalovat závisí na zvukové kartě, kterou vlastníte.</p>
<p class="p">Následujícím příkazem <span class="cmd">lspci</span> v <span class="app">Terminálu</span> zjistíte jakou zvukovou kartu máte. Přesnější výsledky získáte, jestliže příkaz <span class="cmd">lspci</span> použijete jako <span class="link"><a href="user-admin-explain.html" title="Jak funguje oprávnění správce?">správce</a></span>; do terminálu vložte <span class="cmd">sudo lspci</span> a napište své heslo. Podívejte se, jeli vypsán nějaký <span class="em">audio controller</span> nebo <span class="em">audio zařízení</span> - tento záznam by měl obsahovat model a číslo zvukové karty. <span class="cmd">sudo lspci -v</span> zobrazí mnohem více informací.</p>
<p class="p">You may be able to find and install drivers for your card by searching the Internet. Otherwise, you can <span class="link"><a href="report-ubuntu-bug.html" title="Nahlášení problému v Ubuntu">file a bug</a></span>.</p>
<p class="p">Jestliže nenajdete vhodné ovladače, můžete si také pořídit novou zvukovou kartu. Můžete si pořídit zvukovou kartu, která se připojuje jako externí do USB.</p>
</div></div>
