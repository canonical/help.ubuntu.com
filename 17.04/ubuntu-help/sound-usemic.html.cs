<div class="hgroup"><h1 class="title"><span class="title">Použití jiného mikrofonu</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete použít externí mikrofon k povídání si s přáteli, hovorům s kolegy, nahrávání záznamů či pro použití ve hrách. Ikdyž má počítač zabudovaný interní mikrofon nebo webkameru, externí mikrofon obvykle poskytuje výrazně lepší kvalitu nahraného zvuku.</p>
<p class="p">Pokud má váš mikrofon válcovitý konektor, strčte ho rovnou do počítače. Většina počítačů má dvě zástrčky: jednu pro mikrofon a druhou pro reproduktory. Podívejte s ena obrázek vedle zástrčky. Připojený mikrofon by se měl automaticky nastavit jako hlavní. Pokud ne, podívejte se níže na návod k nastavení vstupních zařízení.</p>
<p class="p">Jestliže máte USB mikrofon, připojte ho do USB. USB mikrofon se ohlásí jako samostatné zařízení a možná se bude muset nastavit jako výchozí.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Veberte výchozí audio vstupní zařízení</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="gui">nabídku zvuku</span> v <span class="gui">horním panelu</span> a vyberte <span class="gui">Předvolby zvuku</span>.</p></li>
<li class="steps"><p class="p">V záložce <span class="gui">Vstup</span> vyberte zařízení ze seznamu. Vstupní indikátor by se měl pohybovat, budete-li hovořit.</p></li>
</ol></div>
