<div class="hgroup"><h1 class="title"><span class="title">Proč jsou v nabídce Dash odkazy pro nakupování?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">In addition to helping you find apps or files on your computer, the 
<span class="gui">Dash</span> can also show you related online results for your searches. 
Online sources include Amazon.com and dozens of other online sources.</p>
<p class="p">Po zakoupení hudby nebo jiných produktů z těchto zdrojů Canonical obdrží malou část zisku výměnou za přivedení nových zákazníků do těchto obchodů. Canonical, společnost která vytvořila a podporuje Ubuntu používá tyto peníze na vylepšování Ubuntu.</p>
<p class="p">By default, the inclusion of online search results is turned off. 
If you want to include online search results, you can 
  enable this feature through the <span class="link"><a href="security-settings.html" title="Security &amp; Privacy"><span class="gui">Security &amp; Privacy</span></a></span> settings.</p>
