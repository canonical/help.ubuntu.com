<div class="hgroup"><h1 class="title"><span class="title">Přidání nového uživatele</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete vytvořit více uživatelských účtů na jednom počítači. Vytvořte účet pro každého člena domácnosti nebo každého zaměstnance. Každý uživatel má vlastní domovský adresář, dokumenty a nastavení.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Uživatelské účty</span>.</p></li>
<li class="steps"><p class="p">Pro přidání nového uživatele budete potřebovat <span class="link"><a href="user-admin-explain.html" title="Jak funguje oprávnění správce?">administrátorské oprávnění</a></span>. Klikněte na <span class="gui">Odemknout</span> v pravém horním rohu a zadejte své heslo.</p></li>
<li class="steps"><p class="p">Pro přidání nového uživatele klikněte na <span class="key"><kbd>+</kbd></span> v levé části pod seznamem uživatelů.</p></li>
<li class="steps"><p class="p">Pokud chcte aby měl nový uživatel také administrátorská oprávnění, vyberte typ účtu <span class="gui">Administrátor</span>. Administrátoři mohou dělat důležité změny, například přidávat a mazat uživatele, instalovat programy a ovladače a měnit datum  a čas.</p></li>
<li class="steps"><p class="p">Zadejte celé jméno uživatele. Uživatelské jméno bude vyplněno automaticky na zakládě celého jména uživatele. Původní nastavení bude pravděpodobně dostačující, nicméně můžete jméno změnit.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Vytvořit</span>.</p></li>
<li class="steps">
<p class="p">Účet je zakázaný dokud nezvolíte nějaké heslo. V <span class="gui">Volbých přihlášení</span> klikněte na <span class="gui">Zakázat účet</span> vedle <span class="gui">Heslo</span>. Vyberte <span class="gui">Nastavit heslo</span> ze seznamu a nechte uživatele zadat své heslo do políček <span class="gui">Nové heslo</span> a <span class="gui">Potvrzení hesla</span>. Viz <span class="link"><a href="user-goodpassword.html" title="Volba bezpečného hesla">dobré heslo</a></span>.</p>
<p class="p">Můžete také kliknout na tlačítko vedle políčka <span class="gui">Nové heslo</span> a vygenerovat nové, bezpečné heslo. Tato hesla jsou velmi složitá na odhadnutí, nicméně jsou i velmi obtížně zapamatovatelná, buďte proto opatrní.</p>
</li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Změnit</span>.</p></li>
</ol></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">V okně <span class="gui">Účet uživatele</span> můžete kliknout na obrázek vedle uživatelkéo jména pro nastavení uživatelova obrázku. Tento obrázek se použije na přihlašovací obrazovce. GNOME poskytuje několik obrázků které můžete použít, nebo si vyberte vlastní nebo můžete použít webkameru.</p></div></div></div></div>
