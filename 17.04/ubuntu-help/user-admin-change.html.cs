<div class="hgroup"><h1 class="title"><span class="title">Nastavení, kdo má správcovská oprávnění</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Administrátorská práva jsou způsob, kterým můžete rozhodnout, kdo bude mít právo měnit důležité části systému. Můžete rozhodnout, kdo bude mít administrátorská práva a kdo ne. Toto je dobrý způsob, kterým můžete zabezpečit systém a vyhnout se nechtěným změnám v systému.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na ikonu zcela vpravo na <span class="gui">horním panelu</span> a vyberte <span class="gui">Nastavení systému</span>.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui">Uživatelské účty</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Odemknout</span> a vložte vaše heslo pro editaci profilových nastavení. (Chete-li účtu přiřadit administrátorská práva, musí být váš účet nastaven jako administrátorský.)</p></li>
<li class="steps"><p class="p">Vyberte uživatele, u kterého chcete změnit práva.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Výchozí</span> vedle <span class="gui">Typ účtu</span> a vyberte <span class="gui">Správce</span>.</p></li>
<li class="steps"><p class="p">Zavřete okno Uživatelské účty. Práva budou aktivní při dalším přihlášení do systému.</p></li>
</ol></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents">
<p class="p">První uživatelský účet má přiřazena administrátorská práva. Tento účet byl vytvořen při instalaci systému.</p>
<p class="p">Není moudré mít v systému příliš mnoho účtů s právy<span class="gui">Administrátora</span>.</p>
</div></div></div></div>
