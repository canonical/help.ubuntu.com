<div class="hgroup"><h1 class="title"><span class="title">Kopírování a přesun souborů a složek</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Soubory a složky můžete kopírovat nebo přesouvat na nové místo pomocí tažení a upuštění myší, použitím příkazů kopírovat a vložit nebo pomocí klávesových zkratek.</p>
<p class="p">Například můžete chtít zkopírovat prezentaci na flashdisk, když si ji potřebujete vzít na pracovní cestu. Nebo můžete chtít udělat kopii dokumentu, než v něm provedete nějaké změny (a původní kopii pak použít, když se změny ukážou nesprávné).</p>
<p class="p">Stejný postup se používá pro soubory i složky. Soubory i složky se kopírují a přesouvají tím samým způsobem.</p>
<div class="steps ui-expander">
<div class="yelp-data yelp-data-ui-expander" dir="ltr" data-yelp-expanded="false"></div>
<div class="inner">
<div class="title title-steps"><h2><span class="title">Kopírování a vkládání souborů</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Jedním kliknutím vyberte soubor, který chcete kopírovat.</p></li>
<li class="steps"><p class="p">Klikněte pravým tlačítkem a vyberte <span class="gui">Kopírovat</span> nebo zmáčkněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>C</kbd></span></span>.</p></li>
<li class="steps"><p class="p">Přejděte do jiné složky, kam chcete soubor zkopírovat.</p></li>
<li class="steps"><p class="p">Kliknutím na tlačítko s nabídkou a zvolením <span class="gui">Vložit</span> kopírování souboru dokončíte. Nebo můžete zmáčknout <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>V</kbd></span></span>. Nyní budete mít stejné kopie souboru v původní a další složce.</p></li>
</ol></div>
</div>
</div>
<div class="steps ui-expander">
<div class="yelp-data yelp-data-ui-expander" dir="ltr" data-yelp-expanded="false"></div>
<div class="inner">
<div class="title title-steps"><h2><span class="title">Vyjmutí a vložení souboru, aby se přesunul</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Jedním kliknutím vyberte soubor, který chcete přesunout.</p></li>
<li class="steps"><p class="p">Klikněte pravým tlačítkem a vyberte <span class="gui">Vyjmout</span> nebo zmáčkněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>X</kbd></span></span>.</p></li>
<li class="steps"><p class="p">Přejděte do jiné složky, kam chcete soubor přesunout.</p></li>
<li class="steps"><p class="p">Kliknutím na tlačítko s nabídkou a zvolením <span class="gui">Vložit</span> přesun souboru dokončíte. Nebo můžete zmáčknout <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>V</kbd></span></span>. Soubor se z původní složky odebere a přesune se do jiné složky.</p></li>
</ol></div>
</div>
</div>
<div class="steps ui-expander">
<div class="yelp-data yelp-data-ui-expander" dir="ltr" data-yelp-expanded="false"></div>
<div class="inner">
<div class="title title-steps"><h2><span class="title">Kopírování a přesun souborů přetažením</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete správce souborů a přejděte do složky, která obsahuje soubor, který chcete kopírovat.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Soubory</span> na horní liště a vyberte <span class="gui">Nové okno</span> (nebo zmáčkněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>N</kbd></span></span>), aby se otevřelo druhé okno. V novém okně přejděte do složky, do které chcete soubor přesunout nebo zkopírovat.</p></li>
<li class="steps">
<p class="p">Klikněte na soubor a přetáhněte jej do jiného okna. Pokud je cílová složka na <span class="em">stejném</span> zařízení, soubor se <span class="em">přesune</span>, pokud je na <span class="em">jiném</span> zařízení, soubor se <span class="em">zkopíruje</span>.</p>
<p class="p">Například, když přetáhnete soubor z flashdisku do své domovské složky, soubor se zkopíruje, protože se jedná o dvě různá zařízení.</p>
<p class="p">Během přetahování můžete vynutit, aby byl soubor zkopírován, pomocí držení klávesy <span class="key"><kbd>Ctrl</kbd></span>, nebo aby byl přesunut, pomocí držení klávesy <span class="key"><kbd>Shift</kbd></span>.</p>
</li>
</ol></div>
</div>
</div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Není možné kopírovat nebo přesouvat soubory do složky, která je <span class="em">jen ke čtení</span>. Některé složky jsou jen ke čtení, aby se zabránilo změnám v jejich obsahu. Měnit věci, aby nebyly jen ke čtení, lze pomocí <span class="link"><a href="nautilus-file-properties-permissions.html" title="Nastavení oprávnění souboru">změny oprávnění k souboru</a></span>.</p></div></div></div></div>
