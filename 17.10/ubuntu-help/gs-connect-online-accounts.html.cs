<div class="hgroup"><h1 class="title"><span class="title">Připojení k účtům on-line</span></h1></div>
<div class="region">
<div class="contents">
<div class="media media-image"><div class="inner"><img src="gs-goa1.svg" width="100%" class="media media-block" alt=""></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="gui"><a href="shell-introduction.html#yourname" title="Vy a váš počítač">systémovou nabídku</a></span> v pravé části horní lišty.</p></li>
<li class="steps"><p class="p">Klikněte na tlačítko nastavení v levé dolní části nabídky.</p></li>
</ol></div></div></div>
<div class="media media-image"><div class="inner"><img src="gs-goa2.svg" width="100%" class="media media-block" alt=""></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps" start="3"><li class="steps">
<p class="p">Klikněte na panel <span class="gui">Účty on-line</span> a následně klikněte účet on-line služby, který chcete použít.</p>
<p class="p">Tím se otevře nové okno, ve kterém se můžete k účtu přihlásit.</p>
</li></ol></div></div></div>
<div class="media media-image"><div class="inner"><img src="gs-goa4.svg" width="100%" class="media media-block" alt=""></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps" start="4">
<li class="steps"><p class="p">Ve většině případů budete muset po příhlášení udělit přístup k on-line službě, aby se mohla začít používat.</p></li>
<li class="steps"><p class="p">Například, když se připojujete ke svému účtu Google, budete muset kliknout na tlačítko <span class="gui">Povolit</span>.</p></li>
</ol></div></div></div>
<div class="media media-image"><div class="inner"><img src="gs-goa5.svg" width="100%" class="media media-block" alt=""></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps" start="6"><li class="steps"><p class="p">Řada účtů on-line umožňuje zvolit služby, které ve spojitosti s tímto účtem chcete používat. Pokud některou službu používat nechcete, zakažte ji kliknutím na přepínač <span class="gui">❙/○</span> na pravé straně okna.</p></li></ol></div></div></div>
</div>
