<div id="plugged-in" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zkontrolujte, že je myš připojená</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pokud máte myš s kabelem, zkontrolujte zda je ve zdířce počítače pořádně zastrčený.</p>
<p class="p">Pokud se jedná o myš do USB (obdélníkový konektor), zkuste ji připojit do jiné zdířky USB. Pokud je to myš do PS/2 (malý kulatý konektor se šesti kolíky), zkontrolujte, jestli je připojen do zelené zdířky a ne do fialové. Pokud jej přepojíte, může být potřeba restartovat počítač.</p>
</div></div>
</div></div>
<div id="broken" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zkontrolujte, jestli je myš funkční</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Připojte myš k jinému počítače a ověřte, že funguje.</p>
<p class="p">Jestliže je myš optická nebo laserová, mělo by ze spodní části myši po zapnutí vycházet světlo. Pokud vaše myš nesvítí, zkontrolujte, jestli je zapnutá. Pokud je a přesto nesvítí, je nejspíše rozbitá.</p>
</div></div>
</div></div>
<div id="wireless-mice" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Kontrola bezdrátové myši</span></h2></div>
<div class="region"><div class="contents">
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Ujistěte se, že je myš zapnutá. Často bývá na spodní straně vypínač, kterým se dá úplně vypnout, abyste ji mohli přenášet, aniž by se neustále probouzela.</p></li>
<li class="list"><p class="p">Pokud používáte myš Bluetooth, ujistěte se, že ji máte správně spárovanou se svým počítačem. Viz <span class="link"><a href="bluetooth-connect-device.html" title="Připojení počítače k zařízení Bluetooth">Připojení počítače k zařízení Bluetooth</a></span>.</p></li>
<li class="list"><p class="p">Klikněte tlačítkem a podívejte se, jestli se ukazatel myši dokáže pohybovat. Některé bezdrátové myši se při uspání kvůli úspoře energie neprobudí, dokud nekliknete tlačítkem. Viz <span class="link"><a href="mouse-wakeup.html" title="Myš začne fungovat až po nějaké prodlevě">Myš začne fungovat až po nějaké prodlevě</a></span>.</p></li>
<li class="list"><p class="p">Zkontrolujte, jestli jsou baterie v myši nabité.</p></li>
<li class="list"><p class="p">Ujistěte se, že přijímač je pořádně zastrčený do počítače.</p></li>
<li class="list"><p class="p">Pokud vaše myš a přijímač umí pracovat na různých rádiových kanálech, ujistěte se, že oba používají ten samý kanál.</p></li>
<li class="list"><p class="p">Možná budete muset na myši nebo přijímači nebo obojím zmáčknout tlačítko, aby se ustavilo spojení. Podrobněji by to mělo být popsáno v příručce k vaší myši.</p></li>
</ul></div></div></div>
<p class="p">Většina bezdrátových (RF) myší by měla po připojení k počítači začít fungovat automaticky. Pokud máte myš Bluetooth nebo infračervenou (IR), bude možná potřeba provést některé další kroky, abyste ji zprovoznili. Postup se může lišit podle výrobce a modelu myši.</p>
</div></div>
