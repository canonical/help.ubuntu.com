<div class="hgroup"><h1 class="title"><span class="title">Potřebuji antivirový software?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Jestliže jste zvyklí na používání Windows nebo Mac OS, asi jste také zvyklí, že po celou dobu běží antivirový software. Běží na pozadí a trvale kontroluje počítač ohledně virů, které by si do něj mohly najít cestu a způsobit problémy.</p>
<p class="p">Antivirový software pro Linux existuje, ale nejspíše jej ani nebudete potřebovat. Viry, které by napadaly linuxové systémy jsou velmi zřídkavé. Někdo argumentuje tím, že Linux není příliš rozšířený, takže se nevyplatí pro něj viry psát. Jiní zase tvrdí, že Linux je ze své podstaty bezpečnější a bezpečnostní chyby, které by viry mohly využít, jsou velmi rychle opravovány.</p>
<p class="p">Ať už je pravdou jedno nebo druhé, a nejspíše od obojího trochu, viry pro Linux jsou opravdu hodně vzácné a tak se jich v současnosti nemusíte obávat.</p>
<p class="p">Pokud chcete být opravdu mimořádně v bezpečí, nebo chcete kontrolovat ohledně virů soubory, které předáváte mezi sebou a ostatními lidmi používajícími Windows nebo Mac OS, můžete si i tak antivirový software nainstalovat. Podívejte se do instalátoru softwaru své distribuce nebo si nějaký vyhledejte on-line, pár jich je k dispozici.</p>
