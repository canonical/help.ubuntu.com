<div class="hgroup"><h1 class="title"><span class="title">Proč se moje síť pořád odpojuje?</span></h1></div>
<div class="region">
<div class="contents"><p class="p">Můžete se setkat s tím, že jste odpojeni od bezdrátové sítě, ačkoliv jste chtěli zůstat připojeni. Když se to stane, počítač se normálně bude snažit k síti připojit znovu (ikona sítě v horní liště bude během pokusu o znovupřipojení zobrazovat tři tečky), ale takové situace mohou být otravné, pokud například zrovna něco děláte na Internetu.</p></div>
<div id="signal" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Slabý bezdrátový signál</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Nejčastějším důvodem odpojení od bezdrátové sítě je, že máte slabý signál. Bezdrátové sítě mají omezený dosah, takže když jste příliš daleko od základny s vysílačem, nezískáte dostatečně silný signál pro obsluhu spojení. Navíc zdi a další objekty v přímé trase k vysílači signál zeslabují.</p>
<p class="p">Ikona sítě na horní liště zobrazuje, jak silný signál je. Pokud je slabý, pokuste se přesunout blíže k základní stanici s vysílačem.</p>
</div></div>
</div></div>
<div id="network" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Síťové spojení nebylo správně sestaveno</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Někdy se může stát, že když se připojujete k bezdrátové síti, vypadá to na první pohled, že jste se úspěšně připojili, ale vzápětí jste odpojeni. Je to proto, že počítač se připojil úspěšně jen z části – sice ustanovil spojení, ale nebylo možné z nějakého důvodu celou operaci dotáhnout do konce.</p>
<p class="p">Mezi možné důvody patří, že jste zadali nesprávné heslo nebo počítač nemá dovoleno se připojit do této sítě (například proto, že síť požaduje pro přihlášení uživatelské jméno).</p>
</div></div>
</div></div>
<div id="hardware" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Nespolehlivý bezdrátový hardware/ovladač</span></h2></div>
<div class="region"><div class="contents"><p class="p">Některý bezdrátový hardware může být lehce nespolehlivý. Bezdrátové sítě jsou složité, takže bezdrátové karty a stanice občas fungují s drobnými problémy, které ale mohou vést k zahození spojení. Je to sice nepříjemné, ale u řady zařízení se to stává. Pokud dojde k odpojení jen sem tam, může to být tento důvod. Jestli se to ale stává příliš často, je na zvážení výměna hardwaru.</p></div></div>
</div></div>
<div id="busy" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Přetížená bezdrátová síť</span></h2></div>
<div class="region"><div class="contents"><p class="p">Do bezdrátové sítě na vytíženém místě (např. univerzita nebo kavárna) se často snaží připojit hodně počítačů naráz. Takováto síť je pak někdy přetížená a nedokáže obsloužit všechny počítače, které se do ní snaží připojit, takže některé odmítne.</p></div></div>
</div></div>
