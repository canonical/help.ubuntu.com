<div class="hgroup"><h1 class="title"><span class="title">Vymáčkněte z baterie svého notebooku co nejvíce</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Jak baterie notebooku stárne, snižuje se její schopnost uchovávat energii a její kapacita postupně klesá. Existuje pár postupů, které můžete použít k prodloužení její životnosti, ale nečekejte od toho žádné zázraky.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Nenechávejte baterii úplně vybít. Vždy dobíjejte <span class="em">dříve</span>, než je baterie úplně na nule, i když má většina baterií zabudovanou ochranu bránící v jejím úplném vybití. Dobíjení, když je baterie vybitá jen z části je účinnější. Na druhou stranu dobíjení jen velmi lehce vybité baterie pro ni také není dobré.</p></li>
<li class="list"><p class="p">Teplo má neblahý vliv na účinnost dobíjení baterie. Nenechávejte baterii zbytečně zahřívat.</p></li>
<li class="list"><p class="p">Baterie stárnou, i když leží nepoužité v původním balení. Nemá cenu kupovat náhradní baterii zároveň s původní baterii – ponechte nákup až na dobu, kdy bude skutečně zapotřebí.</p></li>
</ul></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Tyto rady se týkají lithium iontových baterií (Li-Ion), které jsou dnes nejběžnější. Jiné typy baterií mohou vyžadovat jiný způsob zacházení.</p></div></div></div></div>
