<div class="hgroup"><h1 class="title"><span class="title">Můj počítač je hodně horký</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Počítače se během činnosti zahřívají a někdy mohou být celkem teplé. To je normální, jedná se prostě o způsob, jak se počítač zbavuje přebytečného tepla. Pokud je ale horký příliš, může to být známka přehřívání, které by mohlo vést k jeho poškození.</p>
<p class="p">Většina notebooků se po nějaké chvíli používání zahřeje na rozumnou teplotu. To obecně není nic, s čím byste si měli dělat starosti – každý počítač produkuje množství tepla a notebooky jsou velmi kompaktní, takže se potřebují tepla zbavovat rychleji a výsledkem je zahřívání jejich povrchu. Některé notebooky se ale mohou zahřívat více a pro uživatele je to pak nepříjemné. Obvykle to je důsledkem špatně navrženého systému chlazení. Někdy se to dá řešit pořízením dodatečného chladicího vybavení, které se umístí dospod notebooku a poskytne účinější chlazení.</p>
<p class="p">Pokud máte stolní počítač, které se zdá na dotek příliš horký, má nejspíše nedostatečné chlazení. Jestli vás to znepokojuje, můžete pořídit dodatečné ventilátory, a rovněž zkontrolujte stav současných ventilátorů a případně je vyčistěte od prachu. Můžete také zvážit přemístění počítače na místo s lepším přístupem vzduchu – když je na uzavřeném místě (např. ve skříňce), nedokáže jeho chladicí systém dostatečně účinně odvádět teplo a místo něj nasávat chladný vzduch.</p>
<p class="p">Někteří lidé se obávají zdravotních rizik při používání horkého notebooku. Tvrdí, že dlouhodobé používání horkého notebooku na klíně může způsobit snížení (mužské) plodnosti a jsou hlášeny i případy popálení (v extrémních případech). Pokud máte strach z těchto potenciálních problémů, měli byste to probrat se svým praktickým lékařem. Ale jednodušší je prostě nepokládat si notebook přímo na klín.</p>
<p class="p">Většina moderních počítačů se při přílišném zahřátí sama vypne, aby předešla svému poškození. Pokud se váš počítač sám vypíná, může být toto příčinou. Když dojde k přehřátí počítače, nejspíše bude potřebovat opravu.</p>
