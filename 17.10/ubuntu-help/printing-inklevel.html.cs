<div class="hgroup"><h1 class="title"><span class="title">Jak mohu zkontrolovat stav inkoustu nebo toneru v své tiskárně?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">To, jak zkontrolovat zbývající množství inkoustu nebo toneru v tiskárně, závisí na značce a modelu tiskárny a ovladačích a aplikacích nainstalovaných v počítači.</p>
<p class="p">Některé tiskárny mají vestavěný displej pro zobrazování úrovně zbývajícího inkoustu a dalších informací.</p>
<p class="p">Některé tiskárny hlásí úroveň zbývajícího toneru nebo inkoustu počítači a údaje pak můžete najít v panelu <span class="gui">Tiskárny</span> v <span class="app">Nastaveních</span>.</p>
<p class="p">Ovladače a nástroj pro oznamování stavu pro většinu tiskáren HP je poskytován projektem HP Linux Imaging and Printing. Ostatní výrobci mohou poskytovat vlastní ovladače s podobnými funkcemi.</p>
<p class="p">Volitelně si můžete nainstalovat aplikaci kontrolující a sledujíc úroveň hladiny inkoustů. <span class="app">Inkblot</span> ukazuje stav pro většinu tiskáren HP, Epson a Canon. Podívejte se na <span class="link"><a href="http://libinklevel.sourceforge.net/#supported" title="http://libinklevel.sourceforge.net/#supported">seznam podporovaných modelů</a></span>, jestli je mezi nimi i vaše tiskárna. Jinou aplikací se stejným účelem pro tiskárny Epson a některé jiné je <span class="app">mtink</span>.</p>
<p class="p">Některé tiskárny nejsou zatím v Linuxu plnohodnotně podporovány a další zase nejsou navržené tak, aby úroveň iknoustu nebo toneru hlásily.</p>
