<div class="hgroup"><h1 class="title"><span class="title">Zamykací obrazovka</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Zamknutá obrazovka znamená, že můžete vidět, co se děje po dobu, co je počítač zamknutý a máte možnost získat souhrnné informace o tom, co se událo, když jste byli pryč. Zamykací obrazovka má podobu opony s pěkným obrázkem přes celou obrazovku po dobu uzamknutí a poskytuje užitečné informace:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">jméno přihlášeného uživatele</p></li>
<li class="list"><p class="p">datum a čas a některá upozornění</p></li>
<li class="list"><p class="p">stav baterie a sítě</p></li>
<li class="list"><p class="p">ovládací prvky pro přehrávání multimédií – změnu hlasitosti, přeskakování stop nebo pozastavení vaší hudby, aniž byste museli zadávat heslo</p></li>
</ul></div></div></div>
<p class="p">Když chcete počítač odemknout, tažením myší vytáhněte oponu zamykací obrazovku nahoru, nebo zmáčkněte <span class="key"><kbd>Esc</kbd></span> nebo <span class="key"><kbd>Enter</kbd></span>. Tím se odhalí přihlašovací obrazovka, na které můžete zadat své heslo a počítač tím odemknout. Případně stačí začít heslo rovnou psát a opona se zvedne sama. Pokud je počítač nastaven pro používání více uživateli, máte také možnost přepnout se na jiného uživatele.</p>
</div>
