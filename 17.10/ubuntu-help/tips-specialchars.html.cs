<div id="charmap" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Mapa znaků</span></h2></div>
<div class="region"><div class="contents">
<p class="p">GNOME přichází s aplikací <span class="app">Mapa znaků</span>, která umožňuje procházet všechny znaky Unikódu. Můžete ji použít k vyhledání požadovaného znaku, a k jeho případnému zkopírování a vložení.</p>
<p class="p"><span class="app">Mapu znaků</span> najdete v přehledu <span class="gui">Činností</span>. Více informací o ní najdete v <span class="link"><a href="https://help.gnome.org/users/gucharmap/stable/" title="https://help.gnome.org/users/gucharmap/stable/">příručce k Mapě znaků</a></span>.</p>
</div></div>
</div></div>
<div id="characters" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Znaky</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Další užitečnou aplikací dodávanou s GNOME jsou <span class="app">Znaky</span>. Méně běžné znaky v ní najdete roztříděné do kategorií nebo je můžete vyhledávat podle klíčkových slov.</p>
<p class="p"><span class="app">Znaky</span> spustíte z přehledu <span class="gui">Činností</span>. Více informací o této aplikaci najdete v <span class="link"><a href="help:gnome-characters" title="help:gnome-characters">příručce ke Znakům</a></span>.</p>
</div></div>
</div></div>
<div id="compose" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Klávesa Compose</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Klávesa <span class="key"><kbd>Compose</kbd></span> je speciální klávesa, která umožňuje zmáčknout více kláves po sobě, abyste získali speciální znak. Například pro napsání dlouhého <span class="em">é</span> zmáčkněte <span class="key"><kbd>Compose</kbd></span>, pak <span class="key"><kbd>'</kbd></span> a pak <span class="key"><kbd>e</kbd></span>.</p>
<p class="p">Drtivá většina klávesnic nemá zvlášť klávesu <span class="key"><kbd>Compose</kbd></span>. Můžete si ji ale namapovat na některou jinou existující klávesu na své klávesnici.</p>
<div class="note note-important" title="Důležité"><div class="inner"><div class="region"><div class="contents"><p class="p">Abyste mohli změnit tato nastavení, musíte mít v počítači nainstalován <span class="app">Vyladění</span>.</p></div></div></div></div>
<div class="steps"><div class="inner">
<div class="title title-steps"><h3><span class="title">Nadefinování klávesy Compose</span></h3></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Vyladění</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Vyladění</span> otevřete aplikaci.</p></li>
<li class="steps"><p class="p">Klikněte na kartu <span class="gui">Klávesnice a myš</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Vypnuto</span> vedle nastavení <span class="gui">Klávesa Compose</span>.</p></li>
<li class="steps"><p class="p">V dialogovém okně zapněte přepínač a zvolte klávesovou zkratku, kterou chcete používat.</p></li>
<li class="steps"><p class="p">Zaškrtněte políčko u klávesy, kterou chcete nastavit jako klávesu Compose.</p></li>
<li class="steps"><p class="p">Zavřete okno <span class="gui">Vyladění</span>.</p></li>
</ol></div>
</div></div>
<p class="p">Pomocí klávesy <span class="key"><kbd>Compose</kbd></span> můžete psát řadu běžných znaků, například:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Zmáčkněte <span class="key"><kbd>Compose</kbd></span>, pak <span class="key"><kbd>'</kbd></span> a pak písmeno, nad které se přidá čárka, jako třeba <span class="em">é</span>.</p></li>
<li class="list"><p class="p">Zmáčkněte <span class="key"><kbd>Compose</kbd></span>, pak <span class="key"><kbd>`</kbd></span> a pak písmeno, nad které se přidá obrácená čárka (těžký akcent), jako třeba <span class="em">è</span>.</p></li>
<li class="list"><p class="p">Zmáčkněte <span class="key"><kbd>Compose</kbd></span>, pak <span class="key"><kbd>"</kbd></span> a pak písmeno, nad které se přidá přehláska, jako třeba <span class="em">ë</span>.</p></li>
<li class="list"><p class="p">Zmáčkněte <span class="key"><kbd>Compose</kbd></span>, pak <span class="key"><kbd>-</kbd></span> a pak písmeno, nad které se přidá vodorovná čárka, jako třeba <span class="em">ē</span>.</p></li>
</ul></div></div></div>
<p class="p">Na další možnosti kombinací s klávesou <span class="key"><kbd>Compose</kbd></span> se podívejte na <span class="link"><a href="http://cs.wikipedia.org/wiki/Compose##Nejb.C4.9B.C5.BEn.C4.9Bj.C5.A1.C3.AD_kombinace" title="http://cs.wikipedia.org/wiki/Compose##Nejb.C4.9B.C5.BEn.C4.9Bj.C5.A1.C3.AD_kombinace">Wikipedii na stránku o klávese compose</a></span>.</p>
</div></div>
</div></div>
<div id="ctrlshiftu" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Kódy znaků</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Znaky Unikódu můžete zadávat čistě pomocí klávesnice použitím číselného kódu znaku. Každý znak je představován čtyřmístným kódem znaku v šestnáctkové soustavě. Když si chcete tento kód zjistit, najděte si znak v mapě znaků a podívejte se na stavovou lištu nebo na kartu <span class="gui">Informace o znaku</span>. Jde o čtyři znaky (šestnáctkové číslice) za <span class="gui">U+</span>.</p>
<p class="p">Když chcete zadat znak pomocí jeho kódu, podržte zmáčknuté klávesy <span class="key"><kbd>Ctrl</kbd></span> a <span class="key"><kbd>Shift</kbd></span>, napište <span class="key"><kbd>U</kbd></span> následované čtyřmi znaky kódu a pak <span class="key"><kbd>Ctrl</kbd></span> a <span class="key"><kbd>Shift</kbd></span> pusťte. Pokud často používáte nějaké znaky, které nejsou snadno dostupné jinými metodami, může stát za to, naučit se zpaměti kódy těchto znaků, abyste je mohli rychle zadávat.</p>
</div></div>
</div></div>
<div id="layout" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Rozložení klávesnice</span></h2></div>
<div class="region"><div class="contents"><p class="p">Můžete přimět svoji klávesnici, aby se chovala jako klávesnice pro jiný jazyk, než pro který má na klávesách popisky. Mezi různými rozvrženími klávesnice se můžete přepínat pomocí ikony na horní liště. Jak na to viz <span class="link"><a href="keyboard-layouts.html" title="Použití alternativního rozložení klávesnice">Použití alternativního rozložení klávesnice</a></span>.</p></div></div>
</div></div>
<div id="im" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Vstupní metody</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Vstupní metody rozšiřují předchozí metody pomocí vkládání znaků i pomocí jiných vstupních zařízení, než jen klávesnice. Například můžete zadávat znaky pomocí myši s využitím gest nebo zadávat japonské znaky pomocí klávesnice s latinkou.</p>
<p class="p">Pro výběr vstupní metody klikněte pravým tlačítkem na textový widget a v nabídce <span class="gui">Vstupní metoda</span> vyberte tu, kterou chcete používat. Není poskytována žádná výchozí, takže se podívejte do dokumentace ke vstupním metodám, jak se používají.</p>
</div></div>
