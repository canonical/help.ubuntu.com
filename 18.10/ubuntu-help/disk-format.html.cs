<div class="hgroup"><h1 class="title"><span class="title">Úplně vymazání výměnného média</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Když máte výměnný disk, jako je flasdisk nebo externí pevný disk, můžete si někdy přát na něm smazat úplně všechny soubory a složky. Můžete to provést pomocí <span class="em">formátování</span> disku – tím se na něm smažou všechny soubory a zůstane prázdný.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Formátování výměnného disku</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Z přehledu <span class="gui">Činnosti</span> otevřete <span class="app">Disky</span>.</p></li>
<li class="steps">
<p class="p">V seznamu úložných zařízení na levé straně vyberte disk, který chcete vymazat.</p>
<div class="note note-warning" title="Varování"><div class="inner"><div class="region"><div class="contents"><p class="p">Zkontrolujte si, že jste vybrali správný disk! Pokud vyberete nesprávný, vymažete si na něm svoje data!</p></div></div></div></div>
</li>
<li class="steps"><p class="p">Na nástrojové liště pod částí <span class="gui">Svazky</span> klikněte na nabídkové tlačítko. Pak klikněte na <span class="gui">Formátovat…</span>.</p></li>
<li class="steps">
<p class="p">V dialogovém okně, které se objeví, vyberte pro disk <span class="gui">Typ</span> souborového systému.</p>
<p class="p">Pokud budete disk používat mimo Linuxu i na počítačích s Windows a Mac OS, zvolte <span class="gui">FAT</span>. Pokud jej budete používat jen pod Windows, může být lepší volbou <span class="gui">NTFS</span>. Ve stručném popisu vám bude přiblíženo zaměření daného <span class="gui">typu souborového systému</span>.</p>
</li>
<li class="steps"><p class="p">Přidělte disku název a klikněte na <span class="gui">Formátovat…</span>. Zobrazí se okno s žádostí o potvrzení. Údaje podrobně zkontrolujte a kliknutím na <span class="gui">Formátovat</span> pak disk vymažete.</p></li>
<li class="steps"><p class="p">Až formátování skončí, klikněte na ikonu pro vysunutí, aby se disk bezpečně odebral. Nyní je prázdný a připravený k dalšímu použití.</p></li>
</ol></div>
</div></div>
<div class="note note-warning" title="Varování"><div class="inner">
<div class="title title-note"><h2><span class="title">Po zformátování disku nejsou soubory bezpečně odstraněné</span></h2></div>
<div class="region"><div class="contents"><p class="p">Formátování disku není stoprocentně bezpečný způsob, jak z disku vymazat data. Čerstvě naformátovaný disk se sice tváří, že na něm nejsou žádné soubory, ale speciálním softwarem je z něj lze získat. Pokud potřebujete soubory vymazat opravdu nevratně, musíte použít nějaký specializovaný nástroj, například <span class="app">shred</span> v příkazovém řádku.</p></div></div>
