<div class="hgroup"><h1 class="title"><span class="title">Nastavení jasu obrazovky</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">V závislosti na možnostech vašeho hardwaru, můžete snížit jas obrazovky, abyste šetřili energii, nebo jej naopak zvýšit, aby byla obrazovka čitelnější na jasném světle.</p>
<p class="p">Pro změnu jasu obrazovky otevřete <span class="gui"><a href="shell-introduction.html.cs#yourname" title="Vy a váš počítač">systémovou nabídku</a></span> na pravé straně horní lišty a posuňte táhlo jasu obrazovky na hodnotu, která vám vyhovuje. Nastavení by se mělo projevit okamžitě.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Řada notebooků má na klávesnici k úpravě jasu obrazovky speciální klávesy. Většinou je na nich symbol sluníčka. Tyto klávesy použijte za současného držení klávesy <span class="key"><kbd class="key-Fn">Fn</kbd></span>.</p></div></div></div></div>
<p class="p">Jas obrazovky můžete upravit také v panelu <span class="gui">Napájení</span>.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Nastavení jasu obrazovky pomocí panelu <span class="gui">Napájení</span>:</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">V postranním panelu klikněte na <span class="gui">Napájení</span>.</p></li>
<li class="steps"><p class="p">Posuňte táhlo <span class="gui">Jas obrazovky</span> na hodnotu, kterou chcete použít. Změny by se měly projevit okamžitě.</p></li>
</ol></div>
</div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud má počítač zabudovaný světelný senzor, bude se vám jas obrazovky automaticky přizpůsobovat okolním světelným podmínkám. Tuto automatickou funkci můžete zakázat v panelu <span class="gui">Napájení</span>.</p></div></div></div></div>
<p class="p">Když funguje nastavení jasu obrazovky, je také možné nechat automaticky ztlumit jas obrazovky kvůli úspoře energie. Další informace viz <span class="link"><a href="power-whydim.html.cs" title="Proč moje obrazovka po krátké době potemní?">Proč moje obrazovka po krátké době potemní?</a></span>.</p>
</div>
