<div class="hgroup"><h1 class="title"><span class="title">Tisk brožury</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Brožuru můžete vytisknout z PDF.</p>
<p class="p">Když chcete vytisknout brožuru z dokumentu <span class="app">LibreOffice</span>, nejprve ji vyexportujte do PDF pomocí <span class="guiseq"><span class="gui">Soubor</span> ▸ <span class="gui">Exportovat do PDF…</span></span>. Váš dokument musí mít počet stránek násobkem 4 (4, 8, 12, 16, …). Takže možná budete muset přidat až 3 prázdné stránky.</p>
<p class="p">Pokud počet stránek v dokumentu PDF není násobkem 4, měli byste přidat příslušný počet prázdných stránek (1, 2 nebo 3), aby násobkem 4 byl. Udělat to můžete takto:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Vytvořte dokument <span class="app">LibreOffice</span> s potřebným počtem (1 – 3) prázdných stránek.</p></li>
<li class="steps"><p class="p">Vyexportujte prázdné stránky do PDF pomocí <span class="guiseq"><span class="gui">Soubor</span> ▸ <span class="gui">Exportovat do PDF…</span></span></p></li>
<li class="steps"><p class="p">Slučte prázdné stránky s dokumentem PDF pomocí aplikace <span class="app">PDF-Shuffler</span> nebo <span class="app">PDF Mod</span> tak, by byly na konci.</p></li>
</ol></div></div></div>
