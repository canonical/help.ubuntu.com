<div class="region">
<div class="contents"></div>
<div id="reverse" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Opačné pořadí</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Tiskárny obvykle tisknou nejdříve první stránku a nakonec poslední stránku, takže pokud vyjíždí lícem nahoru, jsou seřazené ve špatném pořadí. V takovém případě můžete pořadí tisku obrátit.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h3><span class="title">Aby stránky vyjížděly v opačném pořadí:</span></h3></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Zmáčknutím <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>P</kbd></span></span> otevřete dialogové okno <span class="gui">Tisk</span>.</p></li>
<li class="steps"><p class="p">Na kartě <span class="gui">Obecné</span> v části <span class="em">Kopie</span> zaškrtněte <span class="gui">Obrátit</span>. Poslední stránka bude vytisknuta jako první atd.</p></li>
</ol></div>
</div></div>
</div></div>
</div></div>
<div id="collate" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Třídění</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Když tisknete více kopií dokumentu, je výchozím chováním seskupovat výtisky podle čísel stránek (tzn. vyjedou všechny kopie stránky jedna, pak všechny kopie stránky dva atd.). Volba <span class="em">Setřídit</span> zajistí, že vyjedou seskupené jednotlivé sady stránek ve správném pořadí.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h3><span class="title">Aby se stránky třídily:</span></h3></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Zmáčknutím <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>P</kbd></span></span> otevřete dialogové okno <span class="gui">Tisk</span>.</p></li>
<li class="steps"><p class="p">Na kartě <span class="gui">Obecné</span> v části <span class="em">Kopie</span> zaškrtněte <span class="gui">Setřídit</span>.</p></li>
</ol></div>
