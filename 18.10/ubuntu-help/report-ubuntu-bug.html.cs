<div class="hgroup"><h1 class="title"><span class="title">Nahlášení problému v Ubuntu</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud narazíte na problém s Ubuntu, máte možnost <span class="em">nahlásit bug</span>.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps">
<p class="p">Stiskněte <span class="keyseq"><span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>F2</kbd></span></span> a napište <span class="input">ubuntu-bug název_programu</span></p>
<p class="p">Jestliže se jedná o hardwarový problém nebo nevíte jméno programu, stačí napsat pouze <span class="input">ubuntu-bug</span></p>
</li>
<li class="steps"><p class="p">Po spuštění jednoho z výše uvedených příkazů Ubuntu sesbírá informace o problému. To může trvat až několik minut. Chcete-li, zkontrolujte sebrané informace. Klikněte na <span class="gui">Odeslat</span>.</p></li>
<li class="steps"><p class="p">Otevře se nový panel porhlížeče. Ubuntu používá aplikaci <span class="app">Launchpad</span> pro správu chyb. Pokud nemáte na Launchpadu účet, vytvořte si ho abyste mohl(a) dostávat informace o řešení problému. Účet si můžete založit po kliknutí na <span class="gui">Create a new account</span>.</p></li>
<li class="steps"><p class="p">Po přihlášení do systému Launchpad zapište do pole pro souhrn stručný popis problému.</p></li>
<li class="steps"><p class="p">Po kliknutí na <span class="gui">Next</span> se Launchpad pokusí najít podobné problémy a jejich řešení. Pokud byla chyba již zaznamenána, můžete svůj problém označit jako související kliknutím na "It also affect me". Pokud chyba dosud není zaznamenána, klikněte na <span class="gui">No, I need to report a new bug</span>.</p></li>
<li class="steps">
<p class="p">Vyplňte popis chyby s co možná nejvíce informacemi o problému. Důležité je vyplnit tři věci:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list" style="list-style-type:disc">
<li class="list"><p class="p">Co jste očekávali, že se stane</p></li>
<li class="list"><p class="p">Co se ve skutečnosti stalo</p></li>
<li class="list"><p class="p">Pokud možno, minimální postup potřebný k vyvolání chyby, začněte s "start the program".</p></li>
</ul></div></div></div>
</li>
<li class="steps"><p class="p">Váš popis dostane identifikační číslo (ID) a jeho stav se bude měnit tak jak bude chyba řešena. Děkujeme že pomáháte Ubuntu vylepšovat!</p></li>
</ol></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud se objeví chyba "This is not a genuine Ubuntu package", znamená to že se pokoušíte hlásit chybu kterou nezpůsobil program oficiálně nainstalovaný z repozitářů. Pro takové programy nelze tento nástroj použít.</p></div></div></div></div>
<p class="p">Pro více informací o hlášení chyb v Ubuntu, využijte prosím <span class="link"><a href="https://help.ubuntu.com/community/ReportingBugs" title="https://help.ubuntu.com/community/ReportingBugs">online dokumentaci</a></span>.</p>
</div>
