<div class="hgroup"><h2 class="title"><span class="title">Propojení kontaktů</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Můžete zkombinovat kontakty s místního adresáře a účtů on-line do jednoho společného záznamu v <span class="app">Kontaktech</span>. Díky tomu můžete udržovat adresář přehledně se všemi údaji na jednom místě.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Zapněte <span class="em">režim výběru</span> zmáčknutím tlačítka se zatrhnutím nad seznamem kontaktů.</p></li>
<li class="steps"><p class="p">U každého kontaktu se objeví zaškrtávací políčko. U kontaktů, které chcete sloučit, políčko zaškrtněte.</p></li>
<li class="steps"><p class="p">Zmáčknutím <span class="gui">Propojit</span> vybrané kontakty propojíte.</p></li>
</ol></div></div></div>
</div></div>
</div></div>
<div id="unlink-contacts" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Rozpojení kontaktů</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pokud omylem propojíte kontakty, které propojené být nemají, můžete je zase rozpojit.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">V seznamu kontaktů vyberte kontakt, který chcete rozpojit.</p></li>
<li class="steps"><p class="p">Zmáčkněte <span class="gui">Upravit</span> v pravém horním rohu aplikace <span class="app">Kontakty</span>.</p></li>
<li class="steps"><p class="p">Zmáčkněte <span class="gui">Propojené účty</span>.</p></li>
<li class="steps"><p class="p">Zmáčknutím <span class="gui">Rozpojit</span> záznam v kontaktech rozpojíte.</p></li>
<li class="steps"><p class="p">Po dokončení rozpojení okno zavřete.</p></li>
<li class="steps"><p class="p">Zmáčknutím <span class="gui">Hotovo</span> úpravy kontaktu dokončete.</p></li>
</ol></div></div></div>
