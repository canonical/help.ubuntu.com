<div class="hgroup"><h1 class="title"><span class="title">Sdílení souborů přes e-mail</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Soubory můžete snadno sdílet se svými kontakty jejich zasláním e-mailem přímo ze správce souborů.</p>
<div class="note note-important" title="Důležité"><div class="inner"><div class="region"><div class="contents"><p class="p">Než začnete, ujistěte se, že je ve vašem počítači nainstalován poštovní klient <span class="app">Evolution</span> nebo <span class="app">Geary</span> a v něm správně nastavený poštovní účet.</p></div></div></div></div>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Když chcete sdílet soubor přes e-mail:</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Z přehledu <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> otevřete aplikaci <span class="app">Soubory</span>.</p></li>
<li class="steps"><p class="p">Vyhledejte soubor, který chcete přenést.</p></li>
<li class="steps"><p class="p">Klikněte na soubor pravým tlačítkem a vyberte <span class="gui">Poslat e-mailem…</span> Objeví se okno editoru zprávy, ve které bude soubor nachystán jako příloha.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Komu</span>, abyste vybrali kontakt nebo ručně zadejte e-mailovou adresu, na kterou chcete soubor poslat. Podle potřeby vyplňte <span class="gui">Předmět</span> a tělo zprávy a klikněte na <span class="gui">Poslat</span>.</p></li>
</ol></div>
</div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Můžete poslat i více souborů naráz. Vyberte více souborů podržením zmáčknuté klávesy <span class="key"><kbd>Ctrl</kbd></span> během klikání, pravým tlačítkem pak můžete kliknout na libovolný z vybraných.</p></div></div></div></div>
