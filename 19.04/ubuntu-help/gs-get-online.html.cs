<div class="hgroup"><h1 class="title"><span class="title">Připojení k internetu</span></h1></div>
<div class="region">
<div class="contents"><div class="note note-important" title="Důležité"><div class="inner"><div class="region"><div class="contents"><p class="p">Stav svého síťového připojení můžete vidět na pravé straně horní lišty.</p></div></div></div></div></div>
<div id="going-online-wired" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Připojení k drátové síti</span></h2></div>
<div class="region"><div class="contents">
<div class="media media-image"><div class="inner"><img src="gs-go-online1.svg" width="100%" class="media media-block" alt=""></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps">
<p class="p">Ikona síťového připojení na pravé straně horní lišty zobrazuje, že jste odpojeni.</p>
<p class="p">Stav odpojení může být způsoben řadou různých důvodů: například může být vypojen síťový kabel, počítač může být přepnut do <span class="em">režimu „letadlo“</span> nebo ve vašem okolí nemusí být dostupné žádné sítě Wi-Fi.</p>
<p class="p">Jestli chcete použít drátové připojení, stačí do počítače zapojit síťový kabel. Počítač se pokusí síťové připojení nastavit automaticky.</p>
<p class="p">Během doby, co bude počítač síťové přípojení nastavovat, budou na ikoně síťovéh připojení zobrazeny tři tečky.</p>
</li>
<li class="steps"><p class="p">Až se nastavení síťového připojení úspěšně dokončí, změní se ikona síťového připojení na symbol počítače.</p></li>
</ol></div></div></div>
</div></div>
</div></div>
<div id="going-online-wifi" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Připojení k bezdrátové síti</span></h2></div>
<div class="region"><div class="contents">
<div class="media media-image"><div class="inner"><img src="gs-go-online2.svg" width="100%" class="media media-block" alt=""></div></div>
<div class="steps"><div class="inner">
<div class="title title-steps"><h3><span class="title">Když se chcete připojit k bezdrátové síti (Wi-Fi):</span></h3></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="gui"><a href="shell-introduction.html.cs#yourname" title="Vy a váš počítač">systémovou nabídku</a></span> v pravé části horní lišty.</p></li>
<li class="steps"><p class="p">Vyberte <span class="gui">Wi-Fi   Nepřipojeno</span>. Část nabídky Wi-Fi se rozbalí.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Vybrat síť</span>.</p></li>
</ol></div>
</div></div>
<div class="note note-important" title="Důležité"><div class="inner"><div class="region"><div class="contents"><p class="p">K síti Wi-Fi se můžete připojit, jen pokud k tomu má váš počítač patřičný hardware a jste v místě pokryté signálem Wi-Fi.</p></div></div></div></div>
<div class="media media-image"><div class="inner"><img src="gs-go-online3.svg" width="100%" class="media media-block" alt=""></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps" start="4"><li class="steps">
<p class="p">V seznamu dostupných sítí Wi-Fi vyberte síť, ke které se chcete připojit, a připojení potvrďte kliknutím na <span class="gui">Připojit</span>.</p>
<p class="p">V závislosti na nastavení sítě můžete být vyzváni ke kontrole svých pověření.</p>
</li></ol></div></div></div>
