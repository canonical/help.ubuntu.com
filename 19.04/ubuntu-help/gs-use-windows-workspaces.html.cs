<div id="use-workspaces-and-windows-maximize" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Maximalizace a zrušení maximalizace okna</span></h2></div>
<div class="region"><div class="contents">
<p class="p"></p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Pokud chcete okno maximalizovat tak, aby zabíralo celý prostor vaší pracovní plochy, chyťte jej za záhlaví a táhněte k horní části obrazovky.</p></li>
<li class="steps"><p class="p">Až se obrazovka zvýrazní, okno upusťte a ono se maximalizuje.</p></li>
<li class="steps"><p class="p">Když budete chtít obnovit původní nemaximalizovanou velikost okna, chyťte jej za záhlaví a táhněte pryč od okrajů obrazovky.</p></li>
</ol></div></div></div>
</div></div>
</div></div>
<div id="use-workspaces-and-windows-tile" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Dláždění oken</span></h2></div>
<div class="region"><div class="contents">
<p class="p"></p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Když chcete okno maximalizovat podél okraje obrazovky, uchopte jej za záhlaví a táhněte jej k levému nebo pravému okraji obrazovky.</p></li>
<li class="steps"><p class="p">Ve chvíli, kdy se zvýrazní polovina obrazovky, okno upusťte a ono se maximalizuje podél vybrané strany obrazovky.</p></li>
<li class="steps"><p class="p">Když chcete maximalizovat dvě okna vedle sebe, uchopte druhé okno za záhlaví a táhněte jej na opačnou stranu obrazovky, než to první.</p></li>
<li class="steps"><p class="p">Ve chvíli, kdy se zvýrazní polovina obrazovky, okno upusťte a ono se maximalizuje podél druhé strany obrazovky.</p></li>
</ol></div></div></div>
</div></div>
</div></div>
<div id="use-workspaces-and-windows-maximize-keyboard" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Maximalizace a zrušení maximalizace okna pomocí klávesnice</span></h2></div>
<div class="region"><div class="contents"><div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Pomocí klávesnice se okno maximalizuje podržením <span class="key"><a href="help:gnome-help/keyboard-key-super" title="help:gnome-help/keyboard-key-super"><kbd>Super</kbd></a></span> a zmáčknutím <span class="key"><kbd>↑</kbd></span>.</p></li>
<li class="steps"><p class="p">Pro zrušení maximalizace okna pomocí klávesnice podržte zmáčknutou klávesu <span class="key"><a href="help:gnome-help/keyboard-key-super" title="help:gnome-help/keyboard-key-super"><kbd>Super</kbd></a></span> a zmáčkněte <span class="key"><kbd>↓</kbd></span>.</p></li>
</ol></div></div></div></div></div>
</div></div>
<div id="use-workspaces-and-windows-tile-keyboard" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Dláždění oken pomocí klávesnice</span></h2></div>
<div class="region"><div class="contents"><div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Pro maximalizaci okna podél pravé strany obrazovky podržte zmáčknutou klávesu <span class="key"><a href="help:gnome-help/keyboard-key-super" title="help:gnome-help/keyboard-key-super"><kbd>Super</kbd></a></span> a zmáčkněte <span class="key"><kbd>→</kbd></span>.</p></li>
<li class="steps"><p class="p">Pro maximalizaci okna podél levé strany obrazovky podržte zmáčknutou klávesu <span class="key"><a href="help:gnome-help/keyboard-key-super" title="help:gnome-help/keyboard-key-super"><kbd>Super</kbd></a></span> a zmáčkněte <span class="key"><kbd>←</kbd></span>.</p></li>
</ol></div></div></div></div></div>
