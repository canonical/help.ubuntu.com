<div class="hgroup"><h1 class="title"><span class="title">Klikání a pohyb ukazatelem myši pomocí numerické klávesnice</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pokud je pro vás obtížné používat myš nebo jiné ukazovací zařízení, můžete ovládat ukazatel myši pomocí numerické části na klávesnici. Tato funkce se nazývá <span class="em">myš z klávesnice</span>.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps">
<p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Nastavení</span>.</p>
<p class="p">Do přehledu <span class="gui">Činností</span> se můžete dostat kliknutím na <span class="gui">Činnosti</span>, najetí ukazatelem myši do levého horního rohu obrazovky, použitím kombinace <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Alt</kbd></span>+<span class="key"><kbd>Tab</kbd></span></span> následované zmáčknutím <span class="key"><kbd>Enter</kbd></span> nebo použitím klávesy <span class="key"><a href="keyboard-key-super.html.cs" title="Co je klávesa Super?"><kbd>Super</kbd></a></span>.</p>
</li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Univerzální přístup</span> v postranním panelu otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Použijte klávesy <span class="key"><kbd>↑</kbd></span> a <span class="key"><kbd>↓</kbd></span> k výběru <span class="gui">Myš z klávesnice</span> v části <span class="gui">Ukazování a klikání</span> a zmáčkněte <span class="key"><kbd>Enter</kbd></span>, aby se <span class="gui">Myš z klávesnice</span> přepnula na <span class="gui">❙</span> (zapnuto).</p></li>
<li class="steps"><p class="p">Ujistěte se, že je vypnutý <span class="key"><kbd>Num Lock</kbd></span>. Od této chvíle máte možnost ovládat ukazatel myši pomocí klávesnice.</p></li>
</ol></div></div></div>
<p class="p">Numerická (číselná) klávesnice je blok kláves s čísly vpravo na klávesnici. Pokud máte klávesnici bez numerické části (například u notebooku), je potřeba obvykle držet zmáčknutou funkční klávesu (<span class="key"><kbd class="key-Fn">Fn</kbd></span>) a k tomu použít příslušné klávesy v jiné části klávesnice. V případě, že tuto funkci bude používat na takovéto klávesnici často, zvažte koupi externí numerické klávesnice připojené přes USB nebo Bluetooth.</p>
<p class="p">Každé číslo na numerické klávesnici odpovídá jednomu směru. Například zmáčknutí <span class="key"><kbd>8</kbd></span> posune ukazatel nahoru a zmáčknutí <span class="key"><kbd>2</kbd></span> dolů. Zmáčknutí <span class="key"><kbd>5</kbd></span> způsobí kliknutí a dvě zmáčknutí krátce po sobě způsobí dvojité kliknutí.</p>
<p class="p">Většina klávesnic má speciální klávesu, která umožňuje „kliknutí pravým tlačítkem“. Tato klávesa se někdy nazývá <span class="key"><a href="keyboard-key-menu.html.cs" title="Co je klávesa Menu?"><kbd>Menu</kbd></a></span> nebo <span class="key"><kbd>Context menu</kbd></span>. Ale pozor, vztahuje se k zaměření z klávesnice a ne k ukazateli myši. Podívejte se také na <span class="link"><a href="a11y-right-click.html.cs" title="Simulace kliknutí pravým tlačítkem">Simulace kliknutí pravým tlačítkem</a></span>, jak se dá kliknutí pravým tlačítkem provést podržením klávesy <span class="key"><kbd>5</kbd></span> nebo levého tlačítka myši.</p>
<p class="p">Když chcete použít numerickou klávesnici k zadávání čísel při současně zapnuté myši z klávesnice, zapněte <span class="key"><kbd>Num Lock</kbd></span>. Po dobu, co je <span class="key"><kbd>Num Lock</kbd></span> zapnutý, píše numerická klávesnice čísla a nelze ji použít k ovládání ukazatele.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Klávesy s čísly v horní části alfanumerické klávesnice k ovládání ukazatele myši neslouží a nadále píší čísla. K ovládání lze použít jen čísla na numerické klávesnici.</p></div></div></div></div>
