<div class="hgroup"><h1 class="title"><span class="title">Proč jsou v mých výtiscích šmouhy, prázdné linky nebo nesprávné barvy?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Když jsou ve vašem výtisku šmouhy, je vybledlý, jsou v něm čáry, kde nemají být nebo má nějak jinak zhoršenou kvalitu, může to být problém s tiskárnou nebo dochází v kazetě inkoust nebo toner.</p>
<div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Vybledlý text nebo obrázky</dt>
<dd class="terms"><p class="p">Možná vám dochází inkoust nebo toner. Zkontrolujte zásobník inkoustu nebo toneru a případně pořiďte novou kazetu.</p></dd>
<dt class="terms">Šmouhy a prázdné linky</dt>
<dd class="terms"><p class="p">Pokud máte inkoustovou tiskárnu, může být tisková hlava znečištěná nebo částečně ucpaná. Zkuste provést její vyčištění. Jak přesně na to, najdete v příručce k tiskárně.</p></dd>
<dt class="terms">Nesprávné barvy</dt>
<dd class="terms"><p class="p">Tiskárně může docházet jedna barva v inkoustové nebo tonerové kazetě. Zkontrolujte zásobu inkoustu nebo toneru a v případě potřeby vyměňte příslušnou kazetu za novou.</p></dd>
<dt class="terms">Zubaté nebo křivé čáry</dt>
<dd class="terms"><p class="p">Pokud jsou v tiskovém výstupu čáry, které by měly být rovné, zubaté, možná budete muset provést zarovnání hlav. Jak to přesně udělat, najdete v příručce k tiskárně.</p></dd>
</dl></div></div></div>
