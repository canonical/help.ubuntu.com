<div class="hgroup"><h1 class="title"><span class="title">Jiní lidé nemohou přehrát mnou vytvořená videa</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Když vytvoříte na svém počítači s Linuxem video a pošlete jej někomu, kdo používá Windows nebo Mac OS, můžete se setkat s tím, že bude mít problémy s jeho přehráním.</p>
<p class="p">Aby šlo video přehrát, musí mít osoba, které jej posíláte, nainstalovaný správný <span class="em">kodek</span>. Kodek je kus softwaru, který ví, jak video zpracovat a zobrazit jej na obrazovce. Existuje spousta různých formátů videa a každý tento formát vyžaduje jiný kodek, aby šel přehrát. O jaký formát se jedná u vašeho videa, můžete zjistit následovně:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Z přehledu <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> otevřete <span class="app">Soubory</span>.</p></li>
<li class="list"><p class="p">Klikněte pravým tlačítkem na videosoubor a vyberte <span class="gui">Vlastnosti</span>.</p></li>
<li class="list"><p class="p">Přejděte na kartu <span class="gui">Zvuk/video</span> nebo <span class="gui">Video</span> a podívejte se, jaký <span class="gui">Kodek</span> je uveden v části <span class="gui">Video</span> a <span class="gui">Zvuk</span> (pokud je video ozvučené).</p></li>
</ul></div></div></div>
<p class="p">Zeptejte se osoby, která má problém s přehráním, jestli má nainstalovaný správný kodek. Může ji pomoci, když si na Internetu vyhledá název kodeku spolu s název aplikace, kterou používá k přehrávání. Například, když vaše video používá formát <span class="em">Theora</span> a váš kamarád používá Windows Media Player, ať zkusí hledat „theora windows media player“. Většinou je možnost si správný kodek zdarma stáhnou a nainstalovat.</p>
<p class="p">Pokud správný kodek nenajde, může zkusit <span class="link"><a href="http://www.videolan.org/vlc/" title="http://www.videolan.org/vlc/">multimediální přehrávač VLC</a></span>. Funguje pod Windows i Mac OS, stejně jako v Linuxu, a podporuje spoustu různých formátů videa. Když i toto selže, zkuste převést své video do jiného formátu. To umí většina editorů videa a jsou k dispozici i specializované aplikace. Podívejte se do své aplikace pro instalaci softwaru, co máte k dispozici.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Existuje i pár dalších problémů, které mohou někomu bránit v přehrání vašeho videa. Video se může poškodit, když je pošlete (obvykle velké soubory se někdy mohou zkopírovat s chybou), nebo dotyčný může mít problémy se svým přehrávačem videí, nebo video nemusí být správně vytvořené (třeba mohla vzniknout nějaká chyba při ukládání videa).</p></div></div></div></div>
