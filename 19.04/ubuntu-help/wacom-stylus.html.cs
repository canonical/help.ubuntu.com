<div class="hgroup"><h1 class="title"><span class="title">Nastavení stylusu</span></h1></div>
<div class="region">
<div class="contents">
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">V postranním panelu klikněte na <span class="gui">Zařízení</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Grafický tablet</span> v postranním panelu otevřete příslušný panel.</p></li>
<li class="steps">
<p class="p">Klikněte na tlačítko <span class="gui">Stylus</span> v hlavičkové liště.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud není zjištěn žádný stylus, jste vyzváni <span class="gui">Přesuňte prosím stylus do blízkosti tabletu, aby jej bylo možné nastavit</span>.</p></div></div></div></div>
</li>
<li class="steps">
<p class="p">Panel obsahuje podrobnosti a nastavení týkající se stylusu. Po levé straně je název zařízení (třída stylusu) a náčrtek. V nastaveních si můžete přizpůsobit:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p"><span class="gui">Pocit z přítlaku gumy:</span> použijte táhlo k přizpůsobení „pocitu“ (jak se fyzický přítlak převádí na digitální hodnoty) od <span class="gui">Měkkého</span> po <span class="gui">Tvrdý</span>.</p></li>
<li class="list"><p class="p">Nastavení <span class="gui">Tlačítka/Kolečka</span> (tyto změny ovlivní stylus). Klikněte na nabídku vedle popisku a vyberte jednu z následujících funkcí: Žádná činnost, Kliknutí levým tlačítkem myši, Kliknutí prostředním tlačítkem myši, Kliknutí pravým tlačítkem myši, Posouvání nahoru, Posouvání dolů, Posouvání doleva, Posouvání doprava, Zpět, Vpřed.</p></li>
<li class="list"><p class="p"><span class="gui">Pocit z přítlaku hrotu:</span> použijte táhlo k přizpůsobení „pocitu“ od <span class="gui">Měkkého</span> po <span class="gui">Tvrdý</span>.</p></li>
</ul></div></div></div>
</li>
</ol></div></div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud máte stylusů více, použijte šipky vedle názvu stylusu k výběru, který stylus nastavujete.</p></div></div></div></div>
</div>
