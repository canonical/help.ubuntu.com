<div class="hgroup pagewide"><h1 class="title"><span class="title">Mazání souborů a složek</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Pokud některý soubor nebo složku již nechcete, můžete je smazat. Když položku vymažete, přesune se do složky <span class="gui">Koš</span>, ve které bude uchována, dokud koš nevysypete. V případě, že zjistíte, že je přece jen potřebujete, nebo je smažete omylem, můžete ze složky <span class="gui">Koš</span> <span class="link"><a href="files-recover.html.cs" title="Obnovení souboru z koše">položky obnovit</a></span> na jejich původní umístění.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Když chcete soubor vyhodit do koše:</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Jedním kliknutím vyberte položku, kterou chcete vyhodit do koše.</p></li>
<li class="steps"><p class="p">Zmáčkněte klávesu <span class="key"><kbd>Delete</kbd></span>. Případně můžete položku přetáhnout do <span class="gui">Koše</span> na postranní liště.</p></li>
</ol></div>
</div></div>
<p class="p">Soubor bude přesunut do koše a vám bude nabídnuta možnost smazání <span class="gui">Vrátit</span>. Tlačítko <span class="gui">Vrátit</span> se objeví jen na pár vteřin. Pokud jej použijete, soubor se obnoví na své původní místo.</p>
<p class="p">Abyste soubory vymazali trvale a uvolnili místo na disku, musíte koš vysypat. Dělá se to tak, že se klikne pravým tlačítkem na <span class="gui">Koš</span> v postranním panelu a vybere se <span class="gui">Vyprázdnit koš</span>.</p>
</div>
<section id="permanent"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Trvalé smazání souboru</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Soubor můžete vymazat trvale také hned, bez mezikroku s jeho posíláním do koše.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h3><span class="title">Když chcete soubor trvale smazat:</span></h3></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Vyberte položku, kterou chcete smazat.</p></li>
<li class="steps"><p class="p">Zmáčkněte a držte klávesu <span class="key"><kbd>Shift</kbd></span> a pak zmáčkněte klávesu <span class="key"><kbd>Delete</kbd></span>.</p></li>
<li class="steps"><p class="p">Protože tuto operaci nelze vrátit zpět, budete dotázáni na potvrzení, že soubor nebo složku opravdu chcete smazat.</p></li>
</ol></div>
</div></div>
