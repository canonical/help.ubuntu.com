<div class="hgroup pagewide"><h1 class="title"><span class="title">Obnovení souboru z koše</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Když ve správci souborů smažete soubor, normálně se přesune do <span class="gui">Koše</span> a mělo by být možné ho obnovit.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Když chcete obnovit soubor z koše:</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="app">Soubory</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="app">Soubory</span> otevřete správce souborů.</p></li>
<li class="steps"><p class="p">V postranním panelu klikněte na <span class="gui">Koš</span>. Pokud postranní panel nevidíte, zmáčkněte tlačítko nabídky v pravém horním rohu okna a vyberte <span class="gui">Postranní panel</span>.</p></li>
<li class="steps"><p class="p">Pokud zde je smazaný soubor, klikněte na něj a vyberte <span class="gui">Obnovit</span>. Obnoven bude do složky, ze které byl vymazán.</p></li>
</ol></div>
</div></div>
<p class="p">V případě, že smažete soubor zmáčknutím <span class="keyseq"><span class="key"><kbd>Shift</kbd></span>+<span class="key"><kbd>Delete</kbd></span></span> nebo pomocí příkazové řádky, soubor bude smazán trvale. Soubory smazané trvale nelze obnovit z <span class="gui">Koše</span>.</p>
<p class="p">Existuje řada nástrojů pro obnovu, které někdy dokáží obnovit i soubory, které byly smazané trvale. Obecně tyto nástroje ale nejsou jednoduché na používání. Pokud si omylem soubor smažete trvale, je asi nejlepší zeptat se na radu na diskuzním fóru podpory, jestli půjde obnovit.</p>
