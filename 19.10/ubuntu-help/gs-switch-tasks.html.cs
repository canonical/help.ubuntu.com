<section id="switch-tasks-overview"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Přepínání úloh</span></h2></div>
<div class="region"><div class="contents pagewide"><div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Přejeďte ukazatelem myši do rohu <span class="gui">Činnosti</span> v levém horním rohu obrazovky a zobrazí se vám <span class="gui">Přehled činností</span>, ve kterém můžete vidět právě běžící úlohy v podobě náhledů oken.</p></li>
<li class="steps"><p class="p">Kliknutím na okno se přepnete na příslušnou úlohu.</p></li>
</ol></div></div></div></div></div>
</div></section><section id="switching-tasks-tiling"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Dláždění oken</span></h2></div>
<div class="region"><div class="contents pagewide"><div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Když chcete okno maximalizovat podél okraje obrazovky, uchopte jej za záhlaví a táhněte jej k levému nebo pravému okraji obrazovky.</p></li>
<li class="steps"><p class="p">Ve chvíli, kdy se zvýrazní polovina obrazovky, okno upusťte a ono se maximalizuje podél vybrané strany obrazovky.</p></li>
<li class="steps"><p class="p">Když chcete maximalizovat dvě okna vedle sebe, uchopte druhé okno za záhlaví a táhněte jej na opačnou stranu obrazovky, než to první.</p></li>
<li class="steps"><p class="p">Ve chvíli, kdy se zvýrazní polovina obrazovky, okno upusťte a ono se maximalizuje podél druhé strany obrazovky.</p></li>
</ol></div></div></div></div></div>
</div></section><section id="switch-tasks-windows"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Přepínání mezi okny</span></h2></div>
<div class="region"><div class="contents pagewide"><div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Zmáčkněte <span class="keyseq"><span class="key"><a href="help:gnome-help/keyboard-key-super" title="help:gnome-help/keyboard-key-super"><kbd>Super</kbd></a></span>+<span class="key"><kbd>Tab</kbd></span></span> pro zobrazení <span class="gui">přepínače oken</span>, ve kterém jsou vypsána aktuálně otevřená okna.</p></li>
<li class="steps"><p class="p">Uvolněním klávesy <span class="key"><a href="help:gnome-help/keyboard-key-super" title="help:gnome-help/keyboard-key-super"><kbd>Super</kbd></a></span> vyberete následující zvýrazněné okno v <span class="gui">přepínači oken</span>.</p></li>
<li class="steps"><p class="p">Jestli chcete seznam otevřených oken postupně procházet, tak klávesu <span class="key"><a href="help:gnome-help/keyboard-key-super" title="help:gnome-help/keyboard-key-super"><kbd>Super</kbd></a></span> nepouštějte, ale stále ji držte a mačkejte <span class="key"><kbd>Tab</kbd></span>.</p></li>
</ol></div></div></div></div></div>
</div></section><section id="switch-tasks-search"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Použití hledání k přepínání aplikací</span></h2></div>
<div class="region"><div class="contents pagewide"><div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Zmáčknutím klávesy <span class="key"><a href="help:gnome-help/keyboard-key-super" title="help:gnome-help/keyboard-key-super"><kbd>Super</kbd></a></span> zobrazte <span class="gui">Přehled činností</span>.</p></li>
<li class="steps"><p class="p">Začněte psát název aplikace, na kterou se chcete přepnout. Aplikace vyhovující tomu, co píšete, se začnou objevovat průběžně.</p></li>
<li class="steps"><p class="p">Až se aplikace, na kterou se chcete přepnout, objeví ve výsledcích jako první, zmáčkněte <span class="key"><kbd>Enter</kbd></span> a tím se na ni přepnete.</p></li>
</ol></div></div></div></div></div>
