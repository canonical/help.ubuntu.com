<div class="hgroup pagewide"><h1 class="title"><span class="title">Proč se můj počítač nechce znovu zapnout po jeho uspání?</span></h1></div>
<div class="region">
<div class="contents pagewide"><p class="p">Když svůj počítač <span class="link"><a href="power-suspend.html.cs" title="Co se stane, když svůj počítač uspím?">uspíte do paměti</a></span>, a pak jej zkusíte probudit, můžete zjistit, že nepracuje, jak by měl. To může být způsobené tím, že příslušný režim uspání váš hardware správně nepodporuje.</p></div>
<section id="resume"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Můj počítač se uspí a už se neprobudí</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Když počítač uspíte do paměti a pak zmáčknete klávesu nebo kliknete myší, měl by se probudit a zobrazit obrazovku s dotazem na heslo. Pokud se tak nestane, zkuste zmáčknout tlačítko napájení (nedržte jej, jen krátce zmáčkněte).</p>
<p class="p">Pokud to stále nepomůže, ujistěte se, že je zapnutý monitor a zkuste znovu zmáčknout klávesu na klávesnici.</p>
<p class="p">Jako poslední možnosti vypněte počítač podržením napájecího tlačítka po dobu 5 až 10 vteřin, ale počítejte s tím, že při tom přijdete o rozdělanou práci. Pak byste měli být schopni počítač normálně zapnout.</p>
<p class="p">Jestliže tato situace nastane pokaždé, když počítač uspíte do paměti, nejspíše funkce uspání s vaším hardwarem nefunguje správně.</p>
