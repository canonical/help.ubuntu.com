<div class="hgroup pagewide"><h1 class="title"><span class="title">Kliknutí prostředním tlačítkem</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Řada myší a touchpadů má prostřední tlačítko. Na myši s kolečkem můžete obvykle zmáčknout přímo rolovací kolečko jako prostřední tlačítko. V případě, že prostřední není přítomno, můžete zmáčknout naráz levé a pravé tlačítko, a bude to považováno za prostřední tlačítko.</p>
<p class="p">Na vícedotykových touchpadech můžete dosáhnout kliknutí prostředním tlačítkem pomocí klepnutí třemi prsty naráz. Aby to fungovalo, musíte mít v nastaveních touchpadu <span class="link"><a href="mouse-touchpad-click.html.cs" title="Klikání, přetahování a posuv na touchpadu">povolené kliknutí klepnutím</a></span>.</p>
<p class="p">Řada aplikací používá kliknutí prostředním tlačítkem pro pokročilé klikací zkratky.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">V aplikacích s posuvníky kliknutí levým tlačítkem na volné místo na liště posuvníku provede posun přímo na dané místo. Kliknutí prostředním tlačítkem provede posun o jednu stránku v daném směru.</p></li>
<li class="list"><p class="p">V přehledu <span class="gui">Činností</span> můžete rychle otevřít nové okno pro aplikaci pomocí kliknutí prostředním tlačítkem na ikonu aplikaci buď nalevo v oblíbených nebo v přehledu aplikací. Přehled aplikací se zobrazí pomocí tlačítky s mřížkou, které najdete dole v oblíbených.</p></li>
<li class="list"><p class="p">Většina webových prohlížečů umožňuje rychle otevřít odkazy v kartách pomocí prostředního tlačítka myši. Stačí na libovolný odkaz kliknout prostředním tlačítkem a ten se otevře v nové kartě.</p></li>
<li class="list"><p class="p">Ve správci souborů hraje prostřední kliknutí dvě role. Když kliknete prostředním tlačítkem na složku, otevře se v nové kartě. Tím se napodobuje chování oblíbených webových prohlížečů. Když kliknete prostředním tlačítkem na soubor, otevře se soubor stejným způsobem, jako byste na něj klikli dvojitě.</p></li>
</ul></div></div></div>
<p class="p">Některé specializované aplikace umí využít prostřední tlačítko myši pro další funkce. Hledejte v nápovědě k takovéto aplikaci výraz <span class="em">prostřední kliknutí</span> nebo <span class="em">kliknutí prostředním tlačítkem</span>.</p>
