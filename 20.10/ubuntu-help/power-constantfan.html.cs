<div class="hgroup pagewide"><h1 class="title"><span class="title">Ventilátor notebooku pořád běží</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Pokud větráček ve vašem notebooku stále běží naplno, může to být tím, že hardware ovládající chlazení ve vašem notebooku není v Linuxu úplně dobře podporován. Některé notebooky potřebují dodatečný software, aby mohly správně řídit větráčky u chladičů, ale tento software nemusí být nainstalován (nebo pro Linux vůbec dostupný), a tak větráčky běží stále na plný výkon.</p>
<p class="p">V takovémto případě můžete buď změnit některá nastavení nebo nainstalovat dodatečný software, který umožní plnou kontrolu nad větráčky. Například pro řízení větráčků u notebooků Sony VAIO můžete nainstalovat <span class="link"><a href="http://vaio-utils.org/fan/" title="http://vaio-utils.org/fan/">vaiofand</a></span>. Instalace takovéhoto softwaru je technická záležitost značně závislá na výrobci a modelu vašeho notebooku, takže je dobré se podívat přímo na konkrétní rady pro váš notebook.</p>
<p class="p">Je také možné, že váš notebook prostě jen hodně hřeje. To nemusí nutně znamenat, že se přehřívá, ale jen prostě potřebuje, aby ventilátor běžel pořád na nejvyšší otáčky, aby počítač zůstal dostatečně chladný. Pokud se jedná o tento případ, nemáte moc na výběr, než nechat ventilátor běžet pořád naplno. Někdy může pomoci zakoupit přídavné chlazení pro notebook, tzv. chladicí podložku.</p>
