<div class="hgroup pagewide"><h1 class="title"><span class="title">Řazení souborů a složek</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Soubory ve složce můžete řadit různými způsoby, například podle data nebo velikosti. Seznamem běžných způsobů řazení souborů viz <span class="link"><a href="#ways" title="Způsoby řazení souborů">Způsoby řazení souborů</a></span> níže. Jak změnit výchozí způsob řazení, viz <span class="link"><a href="nautilus-views.html.cs" title="Předvolby zobrazení v Souborech">Předvolby zobrazení v <span class="app">Souborech</span></a></span>.</p>
<p class="p">Způsob, jakým můžete řadit soubory závisí na <span class="em">zobrazení složky</span>, které používáte. Aktuální zobrazení můžete změnit pomocí tlačítek pro seznamové nebo ikonové zobrazení na nástrojové liště.</p>
</div>
<section id="icon-view"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Ikonové zobrazení</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Abyste změnili řazení souborů, klikněte na tlačítko zobrazení na nástrojové liště a zvolte <span class="gui">Podle názvu</span>, <span class="gui">Podle velikosti</span>, <span class="gui">Podle typu</span>, <span class="gui">Podle data změny</span> nebo <span class="gui">Podle data přístupu</span>.</p>
<p class="p">Například, když vyberete <span class="gui">Podle názvu</span>, soubory budou řazeny podle svých názvů v abecedním pořadí. Další možnosti viz <span class="link"><a href="#ways" title="Způsoby řazení souborů">Způsoby řazení souborů</a></span>.</p>
<p class="p">Můžete řadit i v opačném pořadí pomocí výběru <span class="gui">Obrátit pořadí</span> v nabídce.</p>
</div></div>
</div></section><section id="list-view"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Seznamové zobrazení</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Abyste změnili řazení souborů, klikněte na jedno ze záhlaví sloupců ve správci souborů. Například klikněte na <span class="gui">Typ</span>, aby se řadilo podle typu souboru. Když kliknete na záhlaví stejného sloupce ještě jednou, změní se směr řazení.</p>
<p class="p">V seznamovém zobrazení můžete zobrazit sloupce s dalšími vlastnostmi a řadit podle nich. Klikněte na tlačítko zobrazení na nástrojové liště, zvolte <span class="gui">Viditelné sloupce…</span> a vyberte sloupce, které chcete, aby byly viditelné. Pak budete moci řadit podle těchto sloupců. Popis jednotlivých sloupců viz <span class="link"><a href="nautilus-list.html.cs" title="Předvolby sloupců v seznamu souborů">Předvolby sloupců v seznamu souborů</a></span>.</p>
</div></div>
</div></section><section id="ways"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Způsoby řazení souborů</span></h2></div>
<div class="region"><div class="contents pagewide"><div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Název</dt>
<dd class="terms"><p class="p">Řadí abecedně podle názvu souboru.</p></dd>
<dt class="terms">Velikost</dt>
<dd class="terms"><p class="p">Řadí podle velikosti souboru (tj. kolik místa zabírá na disku). Jako výchozí se řadí od nejmenších po největší.</p></dd>
<dt class="terms">Typ</dt>
<dd class="terms"><p class="p">Řadí abecedně podle typu souboru. Soubory stejného typu jsou seskupeny dohromady a následně seřazeny podle názvu.</p></dd>
<dt class="terms">Naposledy změněno</dt>
<dd class="terms"><p class="p">Řadí podle data a času, kdy byl soubor naposledy změněn. Jako výchozí řadí od nejstarších po nejnovější.</p></dd>
</dl></div></div></div></div></div>
