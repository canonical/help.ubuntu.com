<section id="use-search-inside-applications"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Vyhledávání v aplikacích</span></h2></div>
<div class="region"><div class="contents pagewide"><p class="p">Vyhledávání v systému sdružuje výsledky z různorodých aplikací. Po levé straně výsledků hledání můžete vidět ikony aplikací, které výsledky poskytly. Kliknutím na některou z těchto ikon spustíte opětovné hledání přímo v aplikaci patřící k dané ikoně. Protože v <span class="gui">Přehledu činností</span> se zobrazují jen nepřesnější shody, může vám hledání přímo v aplikaci přinést lepší výsledky.</p></div></div>
