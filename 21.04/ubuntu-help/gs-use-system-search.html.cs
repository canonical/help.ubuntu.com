<li class="steps"><p class="p">Klikněte na <span class="gui">Hledání</span> v panelu vlevo.</p></li>
<li class="steps"><p class="p">V seznamu prohledávaných míst klikněte na přepínač vedle zdroje hledání, který chcete povolit či zakázat.</p></li>
