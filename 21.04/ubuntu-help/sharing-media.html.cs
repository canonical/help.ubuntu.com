<div class="hgroup pagewide"><h1 class="title"><span class="title">Sdílení vaší hudby, fotografií a videí</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Můžete procházet, vyhledávat a přehrávat multimédia na svém počítači pomocí zařízení podporující <span class="sys">UPnP</span> nebo <span class="sys">DLNA</span>, jako jsou chytré telefony, televize nebo herní konzole. Nastavte <span class="gui">Sdílení multimédií</span>, abyste umožnili těmto zařízením přístup k složkám obsahujícím hudbu, fotografie a videa.</p>
<div class="note note-package" title="Balíček">
<svg width="24" height="24" version="1.1">
 <path class="yelp-svg-fill" d="m3 10v7.429l9 5.571 9-5.571v-7.429l-9 5z"></path>
 <path class="yelp-svg-fill" d="m3 7v2l9 5 9-5v-2l-9-5z"></path>
</svg><div class="inner"><div class="region"><div class="contents">
<p class="p">Aby bylo <span class="app">Sdílení multimédií</span> vidět, musíte mít nainstalovaný balíček <span class="gui">Rygel</span>.</p>
<p class="p"><span class="link-button link"><a href="apt:rygel" title="apt:rygel">Nainstalovat Rygel</a></span></p>
</div></div></div>
</div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Sdílení</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Sdílení</span> otevřete příslušný panel.</p></li>
<li class="steps">
<p class="p">Jestli je vypínač <span class="gui">Sdílení</span> v pravé horní části okna v poloze vypnuto, přepněte jej na zapnuto.</p>
<div class="note" title="Upozornění">
<svg width="24" height="24" version="1.1">
 <path class="yelp-svg-fill" d="m4 3h16c0.554 0 1 0.446 1 1v11h-6v6h-11c-0.554 0-1-0.446-1-1v-16c0-0.554 0.446-1 1-1z"></path>
 <path class="yelp-svg-fill" d="m17 16h4l-5 5v-4c0-0.554 0.446-1 1-1z"></path>
</svg><div class="inner"><div class="region"><div class="contents"><p class="p">Text pod popiskem <span class="gui">Název počítače</span> je možné upravit a <span class="link"><a href="sharing-displayname.html.cs" title="Nastavení zobrazovaného názvu pro váš počítač">změnit</a></span> tak název svého počítače, kterým se prezentuje v síti.</p></div></div></div>
</div>
</li>
<li class="steps"><p class="p">Vyberte <span class="gui">Sdílení médií</span>.</p></li>
<li class="steps"><p class="p">Vypínač <span class="gui">Sdílení médií</span> přepněte do polohy zapnuto.</p></li>
<li class="steps"><p class="p">Ve výchozím stavu jsou sdíleny složky <span class="file">Hudba</span>, <span class="file">Obrázky</span> a <span class="file">Videa</span>. Pokud některou z nich chcete odebrat, klikněte na <span class="gui">×</span> vedle názvu složky.</p></li>
<li class="steps"><p class="p">Pokud chcete přidat jinou složku, klikněte na <span class="gui">+</span>, aby se otevřelo okno <span class="gui">Výběr složky</span>. Přejděte <span class="em">do</span> požadované složky a klikněte na <span class="gui">Otevřít</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">×</span>. Nyní budete moci pomocí externích zařízení procházet nebo přehrávat multimédia v složkách, které jste vybrali.</p></li>
</ol></div></div></div>
