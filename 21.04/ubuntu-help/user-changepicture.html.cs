<div class="hgroup pagewide"><h1 class="title"><span class="title">Změna vaší fotografie na přihlašovací obrazovce</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Když se přihlašujete nebo přepínáte uživatele, vidíte seznam uživatelů s jejich fotografiemi. Svoji fotografii si můžete změnit, buď na standardní obrázek nebo svůj vlastní. Můžete si také pořídit svoji vlastní fotografii pomocí webové kamery.</p>
<p class="p">Abyste mohli měnit jiný, než svůj, uživatelský účet, potřebujete <span class="link"><a href="user-admin-explain.html.cs" title="Jak fungují oprávnění správce?">oprávnění správce</a></span>.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Uživatelé</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Uživatelé</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Pokud chcete upravovat jiný účet, než svůj, zmáčkněte <span class="gui">Odemknout</span> v pravém horním rohu a po vyzvání napište své heslo.</p></li>
<li class="steps">
<p class="p">Klikněte na obrázek vedle svého jména. Rozbalí se galerie se standardními přihlašovacími obrázky. Jestli pro sebe chcete některý z nich, stačí na něj kliknout.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Pokud byste raději použili obrázek, který již máte v počítači, klikněte na <span class="gui">Vybrat soubor…</span></p></li>
<li class="list"><p class="p">Pokud máte webovou kameru, můžete si ihned pořídit svoji fotografii kliknutím na <span class="gui">Pořídit fotografii…</span> Pořídí se snímek, na kterém potom na požadované místo posuňte obrys čtverce a upravte jeho velikost, abyste vyřízli část, kterou potřebujete. V případě, že se vám pořízený snímek nelíbí, proveďte buď nový pokus kliknutím na <span class="gui">Pořídit jiný snímek</span> nebo to kliknutím na <span class="gui">Zrušit</span> skončete.</p></li>
</ul></div></div></div>
</li>
</ol></div></div></div>
