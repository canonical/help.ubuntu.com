<div class="hgroup pagewide"><h1 class="title"><span class="title">Hledání souborů</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Přímo ve správci souborů můžete hledat soubory podle jejich názvu nebo typu.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Hledání</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Z přehledu <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> otevřete aplikaci <span class="app">Soubory</span>.</p></li>
<li class="steps"><p class="p">V případě, že víte, ve které konkrétní složce se požadované soubory nachází, přejděte do ní.</p></li>
<li class="steps">
<p class="p">Napište slovo nebo slova, o kterých víte, že se v názvu souboru nachází, a ty se objeví ve vyhledávací liště. Například, když vaše soubory s fakturami začínají slovem „Faktura“, napište <span class="input">faktura</span>. Slova se porovnávají bez ohledu na velikost písmen.</p>
<div class="note" title="Upozornění">
<svg width="24" height="24" version="1.1">
 <path class="yelp-svg-fill" d="m4 3h16c0.554 0 1 0.446 1 1v11h-6v6h-11c-0.554 0-1-0.446-1-1v-16c0-0.554 0.446-1 1-1z"></path>
 <path class="yelp-svg-fill" d="m17 16h4l-5 5v-4c0-0.554 0.446-1 1-1z"></path>
</svg><div class="inner"><div class="region"><div class="contents"><p class="p">Místo abyste začali přímo psát, můžete si vyhledávací lištu zobrazit předem kliknutím na tlačítko <span class="media"><span class="media media-image"><img src="figures/edit-find-symbolic.svg" class="media media-inline" alt="Search key symbol"></span></span> na nástrojové liště nebo zmáčknutím <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>F</kbd></span></span>.</p></div></div></div>
</div>
</li>
<li class="steps">
<p class="p">Výsledky hledání můžete zúžit zadáním data, typu souboru a rozlišením, jestli se hledá v celém textu obsaženém v souborech nebo jen v názvech souborů.</p>
<p class="p">Když chcete použít filtry, zmáčkněte tlačítko rozbalovací nabídky vedle ikony <span class="media"><span class="media media-image"><img src="figures/edit-find-symbolic.svg" class="media media-inline" alt="Search key symbol"></span></span> správce souborů a vyberte si z nabízených filtrů:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p"><span class="gui">Kdy</span>: Jak daleko do minulosti hledat?</p></li>
<li class="list"><p class="p"><span class="gui">Co</span>: Jaký typ položky?</p></li>
<li class="list"><p class="p">Má hledání zahrnovat i celý text obsažený v souborech, nebo jen názvy souborů?</p></li>
</ul></div></div></div>
</li>
<li class="steps"><p class="p">Když chcete filtr odstranit, zmáčkněte <span class="gui">×</span>, který se vedle něj nachází.</p></li>
<li class="steps"><p class="p">Soubory ve výsledcích hledání můžete otevírat, kopírovat, mazat a i jinak s nimi pracovat stejným způsobem, jako byste byli v běžné složce ve správci souborů.</p></li>
<li class="steps"><p class="p">Opětovným kliknutím na tlačítko <span class="media"><span class="media media-image"><img src="figures/edit-find-symbolic.svg" class="media media-inline" alt="Search key symbol"></span></span> na nástrojové liště hledání opustíte a vrátíte se do složky.</p></li>
</ol></div>
</div></div>
</div>
<section id="customize-files-search"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Přizpůsobení hledání souborů</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Můžete chtít, aby některé složky byly v hledání v aplikaci <span class="app">Soubory</span> zahrnuté a jiné naopak vyjmuté. Když si chcete přizpůsobit, které složky se mají prohledávat:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Hledání</span>.</p></li>
<li class="steps"><p class="p">Ve výsledcích vyberte <span class="guiseq"><span class="gui">Nastavení</span> ▸ <span class="gui">Hledání</span></span>. Tím se otevře panel <span class="gui">Nastavení hledání</span>.</p></li>
<li class="steps"><p class="p">Klikněte na tlačítko <span class="gui">Prohledávaná umístění</span> v hlavičkové liště.</p></li>
</ol></div></div></div>
<p class="p">Otevře se samostatný panel nastavení, ve kterém můžete zapínat a vypínat prohledávání složek. K dispozici jsou tři karty:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p"><span class="gui">Místa</span>: seznam běžných složek v domovské složce</p></li>
<li class="list"><p class="p"><span class="gui">Záložky</span>: seznam složek, které máte v záložkách aplikace <span class="app">Soubory</span></p></li>
<li class="list"><p class="p"><span class="gui">Ostatní</span>: seznam složek, které jste přidali tlačítkem <span class="gui">+</span>.</p></li>
</ul></div></div></div>
