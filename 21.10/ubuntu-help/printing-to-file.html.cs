<div class="hgroup pagewide"><h1 class="title"><span class="title">Tisk do souboru</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Místo abyste dokument vytiskli na tiskárně, můžete jej vytisknout do souboru. Tím se vytvoří soubor <span class="sys">PDF</span>, <span class="sys">PostScript</span> nebo <span class="sys">SVG</span> obsahující dokument. To se může hodit, když chcete dokument přesunout na jiný počítač nebo jej s někým sdílet.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Když chcete tisknout do souboru:</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete dialogové okno tisku zmáčknátím <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>P</kbd></span></span>.</p></li>
<li class="steps"><p class="p">V <span class="gui">Tiskárnách</span> na kartě <span class="gui">Obecné</span> vyberte <span class="gui">Tisknout do souboru</span>.</p></li>
<li class="steps"><p class="p">Abyste mohli změnit výchozí název souboru a místo, kam se má uložit, klikněte na název souboru pod výběrem tiskárny. Po dokončení volby klikněte na <span class="gui">Vybrat</span>.</p></li>
<li class="steps"><p class="p">Výchozím typem souboru pro dokument je <span class="sys">PDF</span>. Když chcete použít jiný <span class="gui">Výstupní formát</span>, vyberte <span class="sys">PostSkript</span> nebo <span class="sys">SVG</span>.</p></li>
<li class="steps"><p class="p">Nastavte si další předvolby stránky.</p></li>
<li class="steps"><p class="p">Zmáčknutím <span class="gui">Tisk</span> soubor uložíte.</p></li>
</ol></div>
