<div class="hgroup pagewide"><h1 class="title"><span class="title">Co se stane, když svůj počítač uspím?</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Když počítač <span class="em">uspíte do paměti</span>, zůstanou všechny vaše aplikace a dokumenty otevřené, ale obrazovka a všechny ostatní části počítače se vypnout, aby se šetřila energie. Počítač sice zůstane zapnutý a pude potřebovat napájení, ale spotřeba bude velmi malá. Probudit jej můžete zmáčknutím klávesy nebo kliknutím myší. Pokud to nepůjde, zkuste zmáčknout tlačítko napájení.</p>
<p class="p">Některé počítače mají problémy s hardwarovou podporou uspávání, což znamená, že <span class="link"><a href="power-suspendfail.html.cs" title="Proč se můj počítač nechce znovu zapnout po jeho uspání?">nemohou být správně uspány do paměti</a></span>. Je dobré si uspávání počítače napřed vyzkoušet, abyste věděli, jestli jej můžete používat.</p>
