<div class="hgroup pagewide"><h1 class="title"><span class="title">Můj počítač se nezapne</span></h1></div>
<div class="region">
<div class="contents pagewide"><p class="p">Existuje řada důvodů, proč se počítač nechce zapnout. Toto téma přináší stručný přehled některých možných příčin.</p></div>
<section id="nopower"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Počítač není připojen k napájení, má vybitou baterii nebo povysunutý napájecí kabel</span></h2></div>
<div class="region"><div class="contents pagewide"><p class="p">Ujistěte se, kabel napájení počítače je pořádně zastrčený ve zdířce v počítači a v elektrické zásuvce, a že jde elektřina. Rovněž se ujistěte, že je připojený a zapnutý monitor. Jestliže máte notebook, připojte nabíječku (pro případ, že by byly vybité baterie). Můžete také zkontrolovat, že baterie, pokud jsou výměnné, jsou správně zasunuté (podívejte se ze spodní strany notebooku).</p></div></div>
</div></section><section id="hardwareproblem"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Problémy s hardwarem počítače</span></h2></div>
<div class="region"><div class="contents pagewide"><p class="p">Některá součást v počítači může být poškozená nebo nefunkční. V takovém případě bude počítač potřebovat opravu. Mezi nejčastější závady patří vadný napájecí zdroj, nesprávně zasunuté součásti (např. paměťový modul nebo karta ve sběrnici), vadný disk a vadná základní deska.</p></div></div>
</div></section><section id="beeps"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Počítač zapípá a vypne se</span></h2></div>
<div class="region"><div class="contents pagewide"><p class="p">V případě, že počítač po zapnutí několikrát pípne a pak se vypne (nebo selže spuštění), může to signalizovat objevení nějakého problému. Tato pípnutí se nazývají <span class="em">beep codes</span> a jejich počet a způsob říkají, o jaký problém se jedná. Bohužel každý výrobce používá jiný vzor pípání, takže je potřeba se podívat do příručky k základní desce nebo dát počítač na opravu.</p></div></div>
</div></section><section id="fans"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Ventilátor počítače běží, ale na obrazovce se nic neobjeví</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Nejprve zkontrolujte, že je váš monitor připojený k počítači a zapnutý.</p>
<p class="p">Problém může být způsoben také selháním hardwaru. Ventilátory se mohou po zmáčknutí vypínače napájení roztočit, ale u ostatních podstatných částí počítače může spuštění selhat. V takovém případě váš počítač potřebuje opravu.</p>
</div></div>
