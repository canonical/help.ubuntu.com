<div class="hgroup pagewide"><h1 class="title"><span class="title">Používání gest na touchpadu a dotykové obrazovce</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Vícedotyková gesta se dají používat na touchpadu nebo dotykové obrazovce jak pro ovládání systému, tak i aplikací.</p>
<p class="p">Gesta využívá řada aplikací. V <span class="app">Prohlížeči dokumentů</span> se dá pomocí gest měnit přiblížení dokumentu a listovat v něm a <span class="app">Prohlížeč obrázků</span> umožňuje gesty měnit přiblížení, otáčet a procházet obrázky.</p>
</div>
