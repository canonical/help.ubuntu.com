<div class="hgroup pagewide"><h1 class="title"><span class="title">Kopírování a přesun souborů a složek</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Soubory a složky můžete kopírovat nebo přesouvat na nové místo pomocí tažení a upuštění myší, použitím příkazů kopírovat a vložit nebo pomocí klávesových zkratek.</p>
<p class="p">Například můžete chtít zkopírovat prezentaci na flešdisk, když si ji potřebujete vzít na pracovní cestu. Nebo můžete chtít udělat kopii dokumentu, než v něm provedete nějaké změny (a původní kopii pak použít, když se změny ukážou nesprávné).</p>
<p class="p">Stejný postup se používá pro soubory i složky. Soubory i složky se kopírují a přesouvají tím samým způsobem.</p>
<div class="steps ui-expander">
<div class="yelp-data yelp-data-ui-expander" dir="ltr" data-yelp-expanded="false"></div>
<div class="inner">
<div class="title title-steps"><h2><span class="title">Kopírování a vkládání souborů</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Jedním kliknutím vyberte soubor, který chcete kopírovat.</p></li>
<li class="steps"><p class="p">Right-click and select <span class="gui">Copy</span>, or press
 <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>C</kbd></span></span>.</p></li>
<li class="steps"><p class="p">Přejděte do jiné složky, kam chcete soubor zkopírovat.</p></li>
<li class="steps"><p class="p">Right-click and select <span class="gui">Paste</span> to finish copying the
 file, or press <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>V</kbd></span></span>. There
 will now be a copy of the file in the original folder and the other
 folder.</p></li>
</ol></div>
</div>
</div>
<div class="steps ui-expander">
<div class="yelp-data yelp-data-ui-expander" dir="ltr" data-yelp-expanded="false"></div>
<div class="inner">
<div class="title title-steps"><h2><span class="title">Vyjmutí a vložení souboru, aby se přesunul</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Jedním kliknutím vyberte soubor, který chcete přesunout.</p></li>
<li class="steps"><p class="p">Right-click and select <span class="gui">Cut</span>, or press
 <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>X</kbd></span></span>.</p></li>
<li class="steps"><p class="p">Přejděte do jiné složky, kam chcete soubor přesunout.</p></li>
<li class="steps"><p class="p">Right-click and select <span class="gui">Paste</span> to
 finish moving the file, or press <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>V</kbd></span></span>.
 The file will be taken out of its original folder and moved to the other
 folder.</p></li>
</ol></div>
</div>
</div>
<div class="steps ui-expander">
<div class="yelp-data yelp-data-ui-expander" dir="ltr" data-yelp-expanded="false"></div>
<div class="inner">
<div class="title title-steps"><h2><span class="title">Kopírování a přesun souborů přetažením</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete správce souborů a přejděte do složky, která obsahuje soubor, který chcete kopírovat.</p></li>
<li class="steps"><p class="p">Press the menu button in the top-right corner of the window and select
 <span class="gui">New Window</span> (or
 press <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>N</kbd></span></span>) to open a second window. In
 the new window, navigate to the folder where you want to move or copy the file.
 </p></li>
<li class="steps">
<p class="p">Klikněte na soubor a přetáhněte jej do jiného okna. Pokud je cílová složka na <span class="em">stejném</span> zařízení, soubor se <span class="em">přesune</span>, pokud je na <span class="em">jiném</span> zařízení, soubor se <span class="em">zkopíruje</span>.</p>
<p class="p">Například, když přetáhnete soubor z flešdisku do své domovské složky, soubor se zkopíruje, protože se jedná o dvě různá zařízení.</p>
<p class="p">Během přetahování můžete vynutit, aby byl soubor zkopírován, pomocí držení klávesy <span class="key"><kbd>Ctrl</kbd></span>, nebo aby byl přesunut, pomocí držení klávesy <span class="key"><kbd>Shift</kbd></span>.</p>
</li>
</ol></div>
</div>
