<div class="hgroup pagewide"><h1 class="title"><span class="title">Otevření souborů pomocí jiných aplikací</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Když ve správci souborů kliknete dvojitě (nebo prostředním tlačítkem) na soubor, ten se otevře ve výchozí aplikaci pro daný typ souboru. Můžete jej také otevřít v jiné aplikaci, vyhledat pro něj aplikaci on-line nebo nastavit výchozí aplikaci pro všechny soubory stejného typu.</p>
<p class="p">Jestli chcete otevřít soubor jinou aplikací, než je výchozí, klikněte na soubor pravým tlačítkem a vyberte v horní části nabídky aplikaci, kterou chcete. Pokud ji tam nevidíte, vyberte <span class="gui">Otevřít jinou aplikací</span>. Ve výchozím stavu zobrazuje správce souborů jen aplikace, o kterých ví, že umí se souborem zacházet. Abyste viděli všechny aplikace v počítači, klikněte na <span class="gui">Zobrazit všechny aplikace</span>.</p>
<p class="p">Pokud stále nemůžete najít aplikaci, kterou chcete, můžete vyhledat další aplikace kliknutím na <span class="gui">Vyhledat nové aplikace</span>. Správce souborů vyhledá on-line balíčky obsahující aplikace, o kterých je známo, že umí pracovat se souborem příslušného typu.</p>
</div>
<section id="default"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Změna výchozí aplikace</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Můžete změnit výchozí aplikaci, která se používá k otevření souborů daného typu. Umožní vám to otevřít vaši upřednostňovanou aplikaci, když otevřete soubor dvojitým kliknutím. Například můžete chtít, aby se otevřel váš oblíbený hudební přehrávač, když dvojitě kliknete na soubor MP3.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Vyberte soubor typu, pro který chcete výchozí aplikaci změnit. Například, když chcete změnit, která aplikace bude otevírat soubory MP3, vyberte soubor <span class="file">.mp3</span>.</p></li>
<li class="steps"><p class="p">Klikněte na soubor pravým tlačítkem a vyberte <span class="gui">Vlastnosti</span>.</p></li>
<li class="steps"><p class="p">Vyberte kartu <span class="gui">Otevřít pomocí</span>.</p></li>
<li class="steps">
<p class="p">Vyberte aplikaci, kterou chcete a klikněte na <span class="gui">Nastavit jako výchozí</span>.</p>
<p class="p">Pokud <span class="gui">Další aplikace</span> obsahují aplikaci, kterou občas chcete použít k otevření, ale nechcete ji nastavit jako výchozí, vyberte ji a klikněte na <span class="gui">Přidat</span>. Bude přidána do <span class="gui">Doporučených aplikací</span>. Po té ji budete mít možnost použít vybráním ze seznamu po kliknutí pravým tlačítkem na soubor.</p>
</li>
</ol></div></div></div>
<p class="p">Tyto změny výchozí aplikaci neplatí jen pro vybraný soubor, ale obecně pro všechny soubory stejného typu.</p>
