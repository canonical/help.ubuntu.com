</svg><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud pojmenujete soubor s <span class="file">.</span> (tečka) jako prvním znakem a zkusíte se na něj podívat ve správci souborů, zjistíte, že je <span class="link"><a href="files-hidden.html.cs" title="Skrytí souboru">skrytý</a></span>.</p></div></div></div>
