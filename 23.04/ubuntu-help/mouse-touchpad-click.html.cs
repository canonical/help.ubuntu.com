<div class="hgroup pagewide"><h2 class="title"><span class="title">Posuv dvěma prsty</span></h2></div>
<div class="region"><div class="contents pagewide">
<div class="media media-image floatend"><div class="inner"><img src="figures/touch-scroll.svg" class="media media-block" alt=""></div></div>
<p class="p">Posouvat můžete pomocí touchpadu za použití dvou prstů.</p>
<p class="p">Když je tato volba vybrána, klepání a přetahování jedním prstem nepracuje normálně, ale místo toho se posouvá, když táhnete dvěma prsty po libovolné části touchpadu. Táhněte prsty mezi horní a dolní částí touchpadu, abyste prováděli svislý posuv, nebo jimi táhněte na šířku napříč touchpadem, abyste prováděli vodorovný posuv. Dávejte pozor na to, abyste prsty nechali mírně od sebe. Když byste je měli přitisknuté příliš k sobě, jevily by se touchpadu jako jeden prst.</p>
<div class="note" title="Upozornění">
<svg width="24" height="24" version="1.1">
 <path class="yelp-svg-fill" d="m4 3h16c0.554 0 1 0.446 1 1v11h-6v6h-11c-0.554 0-1-0.446-1-1v-16c0-0.554 0.446-1 1-1z"></path>
 <path class="yelp-svg-fill" d="m17 16h4l-5 5v-4c0-0.554 0.446-1 1-1z"></path>
</svg><div class="inner"><div class="region"><div class="contents"><p class="p">Posuv dvěma prsty nemusí fungovat na všech touchpadech.</p></div></div></div>
</div>
