<div class="hgroup pagewide"><h1 class="title"><span class="title">Nemohu přehrát písničky, které jsem koupil on-line v obchodě s hudbou</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Když si stáhnete nějakou hudbu z obchodu on-line, můžete narazit na to, že nepůjde na počítači přehrát, zvláště pokud jste ji koupili ve Windows nebo Mac OS a pak ji překopírovali.</p>
<p class="p">To může být tím, že je hudba ve formátu, který váš počítač nerozpozná. Abyste mohli písničku přehrát, potřebujete mít nainstalovanou podporu příslušného zvukového formátu – například, když chcete přehrát soubor MP3, musíte mít nainstalovanou podporu pro MP3. Pokud podporu pro daný formát zvuku nainstalovanou nemáte, měli byste uvidět zprávu, která vás na to upozorní. Tato zpráva by vám měla poskytnout i instrukce, jak podporu pro tento formát nainstalovat, aby přehrávání fungovalo.</p>
<p class="p">Pokud máte podporu zvukového formátu, ve kterém je skladba uložená, nainstalovanou a stále skladbu nemůžete přehrát, může mít skladba <span class="em">ochranu proti kopírování</span> (známou také jako <span class="em">omezení pomocí DRM</span>). DRM je způsob, jak omezit, kdo může skladbu přehrát a na jakém zařízení ji může přehrát. A pravidla určuje vydavatelská společnost, ne vy. V případě, že má hudební soubor omezení pomocí DRM, nebudete jej nejspíše moci přehrát – obecně potřebujete specializovaný software od konkrétního výrobce, abyste mohli přehrávat soubory s omezením pomocí DRM, ale tento software často není na Linuxu podporován.</p>
<p class="p">O DRM se můžete více dozvědět od <span class="link"><a href="https://www.eff.org/issues/drm" title="https://www.eff.org/issues/drm">Electronic Frontier Foundation</a></span> (odkaz je v angličtině).</p>
