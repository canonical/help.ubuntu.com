<div class="region">
<div class="contents pagewide"></div>
<section id="what"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Co je to upozornění?</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Když chce některá aplikace nebo systémová komponenta získat vaši pozornost, zobrazí se upozornění u horní části obrazovky nebo na uzamknuté obrazovce.</p>
<p class="p">Například, když dostanete novou zprávu v diskuzi nebo nový e-mail, budo vás o tom upozornění informovat. Upozornění z diskuzí jsou pojata trochu jinak a jsou podána podle jednotlivých kontaktů, které vám zprávy poslaly.</p>
<p class="p">Jiná upozornění mohou mít tlačítka s volbami. Abyste takovéto upozornění zavřeli bez použití některé z nabízených voleb, stačí kliknout na zavírací tlačítko.</p>
<p class="p">Kliknutí na zavírací tlačítko u některých upozornění způsobí jejích zmizení. Jiné, jako jsou od přehrávače Rhythmbox nebo od diskuzních aplikací, zůstanou skryté v seznamu upozornění.</p>
</div></div>
</div></section><section id="notificationlist"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Seznam upozornění</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Seznam upozornění vám poskytuje způsob, jak se vrátit ke svým upozorněním, až se vám to bude hodit. Objeví se po kliknutí na hodiny nebo po zmáčknutí <span class="keyseq"><span class="key"><a href="keyboard-key-super.html.cs" title="Co je klávesa Super?"><kbd>Super</kbd></a></span>+<span class="key"><kbd>V</kbd></span></span>. Obsahuje všechna upozornění, na která jste zatím nereagovali nebo která jsou v něm umístěna trvale.</p>
<p class="p">Upozoronění si můžete zobrazit kliknutím na něj v seznamu. Zavřít seznam upozornění můžete zmáčknutím <span class="keyseq"><span class="key"><kbd>Super</kbd></span>+<span class="key"><kbd>V</kbd></span></span> nebo klávesy <span class="key"><kbd>Esc</kbd></span>.</p>
<p class="p">Použijte tlačítko <span class="gui">Vymazat vše</span> k vyčištění seznamu upozornění.</p>
</div></div>
</div></section><section id="hidenotifications"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Skrývání upozornění</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Když na něčem pracujete a nechcete být obtěžováni, můžete upozornění vypnout.</p>
<p class="p">Můžete skrýt všechna upozornění tak, že otevřete seznam upozornění a v dolní části přepnete <span class="gui">Nerušit</span> do polohy zapnuto. Jiný možný postup je:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Upozornění</span> v postranním panelu otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Přepněte vypínač <span class="gui">Nerušit</span> do polohy vypnuto.</p></li>
</ol></div></div></div>
<p class="p">Když jsou upozornění vypnutá, většina se jich nebude v horní části obrazovky objevovat. Všechna upozornění budou ale pořád k dispozici v seznamu upozornění, když si jej zobrazíte (kliknutím na hodiny nebo zmáčknutím <span class="keyseq"><span class="key"><kbd>Super</kbd></span>+<span class="key"><kbd>V</kbd></span></span>) a začnou se znovu objevovat, jakmile přepnete vypínač zpátky do polohy zapnuto.</p>
<p class="p">Můžete také zakázat nebo znovu povolit upozornění pro jednotlivé aplikace v panelu <span class="gui">Upozornění</span>.</p>
</div></div>
</div></section><section id="lock-screen-notifications"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Skrývání upozornění na uzamknuté obrazovce</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Když je obrazovka uzamknutá, objevují se na ní upozornění. Můžete ji nastavit tak, aby tato upozornění zůstala z důvodu soukromí skrytá.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h3><span class="title">Když chcete vypnout upozornění po dobu uzamknutí obrazovky:</span></h3></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Upozornění</span> v postranním panelu otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Přepněte vypínač <span class="gui">Upozornění na uzamknuté obrazovce</span> do polohy vypnuto.</p></li>
</ol></div>
</div></div>
