<div class="region">
<div class="contents pagewide">
<p class="p">Obdobně, jako v jiných uživatelských prostředích, používá systém k zobrazení běžících aplikací okna. Pomocí přehledu <span class="link"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">činností</a></span> a <span class="em">oblíbených</span> můžete spustit nové aplikace a ovládat aktivní okna.</p>
<p class="p">Své aplikace můžete seskupovat pomocí pracovních ploch. Podívejte se níže do témat o oknech a pracovních plochách, abyste lépe pochopili, jak to funguje.</p>
