<div class="hgroup pagewide"><h1 class="title"><span class="title">Co je to pracovní plocha a co mi přinese?</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Pracovní plocha představuje skupinu oken na vaší ploše. Můžete vytvořit více pracovních ploch, které budou fungovat jako virtuální plochy. Pracovní plochy zvětšují přehlednost a usnadňují orientaci.</p>
<p class="p">Pracovní plochy můžete používat k zorganizování své práce. Například můžete mít všechna své aplikace pro komunikaci, jako je e-mail nebo rychlá komunikace, na jedné ploše a práci, na které zrovna děláte, na další ploše. Aplikace přehrávající hudbu může být na třetí ploše.</p>
<p class="p">Používání pracovních ploch:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">V přehledu <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činností</a></span> se můžete vodorovně pohybovat mezi pracovními plochami.</p></li>
<li class="list"><p class="p">Pokud je použito více než jedna pracovní plocha, je mezi vyhledávacím polem a seznamem oken zobrazený <span class="link"><a href="shell-workspaces.html.cs" title="Co je to pracovní plocha a co mi přinese?">přepínač pracovních ploch</a></span>. Zobrazuje právě používané pracovní plochy plus jednu prázdnou pracovní plochu navíc.</p></li>
<li class="list"><p class="p">Když chcete přidat další pracovní plochu, přetáhněte okno ze stávající pracovní plochy na prázdnou v přepínači pracovních ploch. Tato pracovní plocha nyní bude obsahovat okno, které jste přetáhli, a vedle ní se objeví nová prázdná pracovní plocha.</p></li>
<li class="list"><p class="p">Když chcete odebrat pracovní plochu, jednoduše zavřete všechna okna na ní nebo je přesuňte na jinou pracovní plochu.</p></li>
</ul></div></div></div>
<p class="p">Vždy existuje aspoň jedna pracovní plocha.</p>
