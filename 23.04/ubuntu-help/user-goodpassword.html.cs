<div class="hgroup pagewide"><h1 class="title"><span class="title">Volba bezpečného hesla</span></h1></div>
<div class="region">
<div class="contents pagewide">
<div class="note note-important" title="Důležité">
<svg width="24" height="24" version="1.1">
 <path class="yelp-svg-fill" d="m12.5 2a9.5 9.5 0 0 0-9.5 9.5 9.5 9.5 0 0 0 9.5 9.5 9.5 9.5 0 0 0 9.5-9.5 9.5 9.5 0 0 0-9.5-9.5zm0 3a1.5 1.5 0 0 1 1.5 1.5v6a1.5 1.5 0 0 1-1.5 1.5 1.5 1.5 0 0 1-1.5-1.5v-6a1.5 1.5 0 0 1 1.5-1.5zm0 10.5a1.5 1.5 0 0 1 1.5 1.5 1.5 1.5 0 0 1-1.5 1.5 1.5 1.5 0 0 1-1.5-1.5 1.5 1.5 0 0 1 1.5-1.5z"></path>
</svg><div class="inner"><div class="region"><div class="contents"><p class="p">Vytvořte si takové heslo, které si snadno zapamatujete, ale pro ostatní (včetně počítačových programů) bude velmi obtížné jej uhodnout.</p></div></div></div>
</div>
<p class="p">Volba dobrého hesla pomáhá udržet váš počítač v bezpečí. Když jde heslo snadno uhodnout, může na něj někdo přijít a získat přístup k vašim osobním datům.</p>
<p class="p">Někdo by také mohl použít počítače k systematickému hádání vašeho hesla (tzv. útok hrubou silou), takže i když by bylo těžce uhodnutelné pro člověka, může být snadno rozlousknutelné počítačem. Zde je pár rad, jak si zvolit dobré heslo:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list">
<p class="p">Použijte v hesle směs malých a velkých písmen, číslic, symbolů a mezer. Tím se stane těžko uhodnutelným, protože více různých znaků znamená více možných kombinací, které je potřeba při hádání hesla vyzkoušet.</p>
<div class="note" title="Upozornění">
<svg width="24" height="24" version="1.1">
 <path class="yelp-svg-fill" d="m4 3h16c0.554 0 1 0.446 1 1v11h-6v6h-11c-0.554 0-1-0.446-1-1v-16c0-0.554 0.446-1 1-1z"></path>
 <path class="yelp-svg-fill" d="m17 16h4l-5 5v-4c0-0.554 0.446-1 1-1z"></path>
</svg><div class="inner"><div class="region"><div class="contents"><p class="p">Dobrou metodou pro výběr kvalitního hesla je vzít první znaky slov z nějaké fráze, kterou si dobře pamatujete. Třeba úryvek z nějaké písně, básně nebo hlášky z filmu, knihy. Například „Kams to dostal Václave? Ale do křídla, člověče.“  by mohlo dát „KtdV? Adk, c.“ nebo „KtdV?Adkc.“ nebo „KtdVAdkc“.</p></div></div></div>
</div>
</li>
<li class="list"><p class="p">Vytvořte heslo co nejdelší. Čím více znaků obsahuje, tím déle by trvalo člověku nebo počítači jej uhodnout.</p></li>
<li class="list"><p class="p">Nepoužívejte běžná slova, která se objevují v slovnících libovolného jazyka. Takováto slova zkouší útočníci jako první. Nejběžnějším heslem je „heslo“ (respektive v angličtině „password“) – heslo tohoto typu lze uhodnout velmi rychle!</p></li>
<li class="list"><p class="p">Nepoužívejte osobní údaje, jako datum narození, registrační značka, jméno člena rodiny.</p></li>
<li class="list"><p class="p">Nepoužívejte běžná slova.</p></li>
<li class="list">
<p class="p">Volte hesla, která dokážete napsat rychle, abyste snížili šance vysledovat co píšete osobou, které se poštěstilo vás při psaní hesla pozorovat.</p>
