</div>
</li>
<li class="list"><p class="p">Pro různé věci používejte různá hesla.</p></li>
<li class="list">
<p class="p">Pro různé účty používejte různá hesla.</p>
<p class="p">Pokud použijete stejné heslo ke všem svým účtům, může se kdokoliv, kdo heslo zjistí, dostat ihned do všech vašich účtů.</p>
<p class="p">Může být obtížné zapamatovat si spoustu hesel. I když nejbezpečnější je používat pro každou věc jiné heslo, můžete si to usnadnit tím, že použijete to stejné heslo pro nekritické věci (jako třeba pro některé weby) a jiná hesla pro věci důležité (jako internetové bankovnictví a váš e-mail).</p>
</li>
<li class="list"><p class="p">Svá hesla pravidelně měňte.</p></li>
</ul></div></div></div>
