<div class="hgroup pagewide"><h1 class="title"><span class="title">Kalendářové události</span></h1></div>
<div class="region">
<div class="contents pagewide">
<div class="note" title="Upozornění">
<svg width="24" height="24" version="1.1">
 <path class="yelp-svg-fill" d="m4 3h16c0.554 0 1 0.446 1 1v11h-6v6h-11c-0.554 0-1-0.446-1-1v-16c0-0.554 0.446-1 1-1z"></path>
 <path class="yelp-svg-fill" d="m17 16h4l-5 5v-4c0-0.554 0.446-1 1-1z"></path>
</svg><div class="inner"><div class="region"><div class="contents">
<p class="p">Tato funkcionalita vyžaduje použití kalendáře za aplikace <span class="app">Evolution</span> nebo <span class="app">Kalendář</span>, nebo abyste měli nastavený nějaký účet on-line, který podporuje <span class="gui">Kalendář</span>.</p>
<p class="p">Většina distribucí má ve výchozím stavu alespoň jeden z těchto programů předinstalovaný. Jestliže vaše ne, budete jej možná muset nainstalovat pomocí správy balíčků ve své distribuci.</p>
</div></div></div>
</div>
<p class="p">Kdy si chcete zobrazit své události:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na hodiny na horní liště.</p></li>
<li class="steps">
<p class="p">Klikněte na datum, pro které chcete vidět své události z kalendáře.</p>
<div class="note" title="Upozornění">
<svg width="24" height="24" version="1.1">
 <path class="yelp-svg-fill" d="m4 3h16c0.554 0 1 0.446 1 1v11h-6v6h-11c-0.554 0-1-0.446-1-1v-16c0-0.554 0.446-1 1-1z"></path>
 <path class="yelp-svg-fill" d="m17 16h4l-5 5v-4c0-0.554 0.446-1 1-1z"></path>
</svg><div class="inner"><div class="region"><div class="contents"><p class="p">Pod každým datem, které má nějakou událost, je tečka.</p></div></div></div>
</div>
<p class="p">Stávající události se zobrazují v kalendáři nalevo. Jakmile je událost přidána do kalendáře v aplikaci <span class="app">Evolution</span> nebo <span class="app">Kalendář</span>, objeví se v hodinách v seznamu událostí.</p>
</li>
</ol></div></div></div>
