<div class="hgroup pagewide"><h1 class="title"><span class="title">Uzpůsobení rychlosti dvojitého kliknutí</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Dvojité kliknutí nastane, když zmáčknete tlačítko myši dvakrát krátce po sobě. Pokud následuje druhé kliknutí příliš dlouho po prvním, je to bráno jako dvě oddělená kliknutí, ne dvojité. V případě, že je pro vás obtížné zmáčknout tlačítka tak rychle, můžete si časový limit zvýšit.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Přístupnost</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Přístupnost</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Select the <span class="gui">Pointing &amp; Clicking</span> section to open it.</p></li>
<li class="steps"><p class="p">Adjust the <span class="gui">Double-Click Delay</span> slider to a value you find comfortable.</p></li>
</ol></div></div></div>
<p class="p">V případě, že vaše myš dělá dvojitá kliknutí, když chcete jednoduchá, a to i přesto, že jste zvýšili prodlevu pro dvojité kliknutí, může být myš vadná. Zkuste ke svému počítači připojit jinou myš a otestovat, jestli ta funguje správně. Případně naopak svoji myš připojte k jinému počítači a sledujte, jestli má stejný problém i tam.</p>
