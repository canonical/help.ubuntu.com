<div class="hgroup pagewide"><h2 class="title"><span class="title">Uspání</span></h2></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Abyste ušetřili energii, uspěte svůj počítač, když jej zrovna nepoužíváte. Pokud používáte notebook, standardně jej systém uspí automaticky při zavření víka. Při uspání se pouze uchová stav v paměti počítač a většina součástí počítače se vypne. Během uspání je zapotřebí napájení, ale odběr je velmi malý.</p>
<p class="p">To suspend your computer manually, click the system menu on the right side
  of the top bar, click the
    <span class="media"><span class="media media-image"><img src="figures/system-shutdown-symbolic.svg" class="media media-inline" alt="Shutdown"></span></span>
  button, and select <span class="gui">Suspend</span>.</p>
