</div></section><section id="lock-screen"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Zamknutí obrazovky</span></h2></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Když odcházíte od svého počítače jen na krátkou chvíli, měli byste zamknout obrazovku, abyste zabránili ostatním lidem v přístupu ke svým souborům a běžícím aplikacím. Až se vrátíte, uvidíte <span class="link"><a href="shell-lockscreen.html.cs" title="Uzamknutá obrazovka">uzamknutou obrazovku</a></span>. Zadejte své heslo, abyste se přihlásili zpět. Pokud obrazovku nezamknete, stane se tak automaticky po uplynutí určené doby.</p>
<p class="p">To lock your screen, click the system menu on the right side of the top
  bar and click the
    <span class="media"><span class="media media-image"><img src="figures/system-lock-screen-symbolic.svg" class="media media-inline" alt="Lock"></span></span>
  button.</p>
<p class="p">Když je obrazovka zamknutá, mohou se ostatní uživatelé přihlásit ke svým vlastním účtům kliknutím na <span class="gui">Přihlásit se jako jiný uživatel</span> v pravém dolním rohu přihlašovací obrazovky. Až skončí, můžete se přepnout zpět na svoji pracovní plochu.</p>
</div>
