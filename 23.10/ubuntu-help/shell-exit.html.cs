<div class="region">
<div class="contents pagewide"><p class="p">Když skončíte u počítače, můžete jej vypnout, uspat (kvůli úspoře energie) nebo jej ponechat zapnutý a odhlásit se.</p></div>
<section id="logout"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Odhlášení nebo přepnutí uživatelů</span></h2></div>
<div class="region">
<div class="contents pagewide">
<div class="media media-image floatend if-if if__not-target-mobile"><div class="inner"><img src="figures/shell-exit-expanded.png" width="250" class="media media-block" alt="Nabídka uživatele"></div></div>
<p class="p">Abyste umožnili ostatním uživatelům použít počítač, můžete se buď odhlásit nebo nebo zůstat přihlášeni a jen přepnout uživatele. Po přepnutí uživatelů zůstanou vaše aplikace běžet a když se přihlásíte zpátky, bude vše tam, kde jste to opustili.</p>
<p class="p">To <span class="gui">Log Out</span> or <span class="gui">Switch User</span>, click the
  <span class="link"><a href="shell-introduction.html.cs#systemmenu" title="Nabídka systému">system menu</a></span> on the right
  side of the top bar, click the
    <span class="media"><span class="media media-image"><img src="figures/system-shutdown-symbolic.svg" class="media media-inline" alt="Shutdown"></span></span>
  button, and select the correct option.</p>
