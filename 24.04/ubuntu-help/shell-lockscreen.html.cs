<div class="hgroup pagewide"><h1 class="title"><span class="title">Uzamknutá obrazovka</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Zamknutá obrazovka znamená, že můžete vidět, co se děje po dobu, co je počítač zamknutý a máte možnost získat souhrnné informace o tom, co se událo, když jste byli pryč. Uzamknutá obrazovka poskytuje užitečné informace:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">datum a čas a některá upozornění</p></li>
<li class="list"><p class="p">stav baterie a sítě</p></li>
</ul></div></div></div>
<p class="p">Když chcete počítač odemknout, klikněte jednou myší nebo touchpadem, nebo zmáčkněte <span class="key"><kbd>Esc</kbd></span> nebo <span class="key"><kbd>Enter</kbd></span>. Tím se odhalí přihlašovací obrazovka, na které můžete zadat své heslo a počítač tím odemknout. Případně stačí začít heslo rovnou psát a přihlašovací obrazovka se během psaní sama objeví. Pokud je počítač nastaven pro používání více uživateli, máte také možnost přepnout se na jiného uživatele vpravo dole na přihlašovací obrazovce.</p>
<p class="p">Jak skrýt upozornění na uzamknuté obrazovce najdete v kapitole <span class="link"><a href="shell-notifications.html.cs#lock-screen-notifications" title="Skrývání upozornění na uzamknuté obrazovce">Skrývání upozornění na uzamknuté obrazovce</a></span>.</p>
