<div class="hgroup pagewide"><h1 class="title"><span class="title">Změna vašeho hesla</span></h1></div>
<div class="region">
<div class="contents pagewide">
<p class="p">Je dobrou zvyklostí čas od času změnit své heslo, hlavně když máte podezření, že jej někdo jiný může znát.</p>
<p class="p">Abyste mohli měnit jiný, než svůj, uživatelský účet, potřebujete <span class="link"><a href="user-admin-explain.html.cs" title="Jak fungují oprávnění správce?">oprávnění správce</a></span>.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Open the <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Activities</a></span> overview and
      start typing <span class="gui">System</span>.</p></li>
<li class="steps"><p class="p">Select <span class="guiseq"><span class="gui">Settings</span> ▸ <span class="gui">System</span></span> from the
      results. This will open the <span class="gui">System</span> panel.</p></li>
<li class="steps"><p class="p">Select <span class="gui">Users</span> to open the panel.</p></li>
<li class="steps"><p class="p">Click the label <span class="gui">·····</span> next to <span class="gui">Password</span>. If you
      are changing the password for a different user, you will first need to
      <span class="gui">Unlock</span> the panel and select the account under
      <span class="gui">Other Users</span>.</p></li>
<li class="steps">
<p class="p">Zadejte své současné heslo a pak nové. Nové zadejte ještě jednou do pole <span class="gui">Ověřit nové heslo</span>.</p>
<p class="p">Můžete zmáčknout ikonu <span class="gui"><span class="media"><span class="media media-image"><img src="figures/system-run-symbolic.svg" height="16" width="16" class="media media-inline" alt="generovat heslo"></span></span></span>, aby se vám automaticky vygenerovalo náhodné heslo.</p>
</li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Změnit</span>.</p></li>
</ol></div></div></div>
<p class="p">Snažte se <span class="link"><a href="user-goodpassword.html.cs" title="Volba bezpečného hesla">zvolit dobré heslo</a></span>. To vám pomůže uchovat váš uživatelský účet v bezpečí.</p>
<div class="note" title="Upozornění">
<svg width="24" height="24" version="1.1">
 <path class="yelp-svg-fill" d="m4 3h16c0.554 0 1 0.446 1 1v11h-6v6h-11c-0.554 0-1-0.446-1-1v-16c0-0.554 0.446-1 1-1z"></path>
 <path class="yelp-svg-fill" d="m17 16h4l-5 5v-4c0-0.554 0.446-1 1-1z"></path>
</svg><div class="inner"><div class="region"><div class="contents"><p class="p">Když změníte své přihlašovací heslo, automaticky se zaktualizuje i heslo klíčenky, aby bylo stejné, jako to přihlašovací.</p></div></div></div>
</div>
<p class="p">V případě, kdy zapomenete své heslo, může vám jej kdokoliv s oprávněním správce změnit.</p>
