<section id="characters"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Znaky</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Aplikace <span class="app">Znaky</span> umožňuje vyhledávat a vkládat méně obvyklé znaky, včetně emodži, a to buď ručním vyhledáním v kategoriích nebo vyhledáním na základě zadaného slova.</p>
<p class="p"><span class="app">Znaky</span> můžete spustit z přehledu <span class="gui">Činností</span>.</p>
</div></div>
</div></section><section id="emoji"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Emodži</span></h2></div>
<div class="region"><div class="contents pagewide"><div class="steps"><div class="inner">
<div class="title title-steps"><h3><span class="title">Vkládání emodži</span></h3></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Zmáčkněte <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>;</kbd></span></span>.</p></li>
<li class="steps"><p class="p">Projděte si kategorie v dolní části dialogového okna nebo začněte ve vyhledávacím poli psát popis emodži.</p></li>
<li class="steps"><p class="p">Vyberte emodži, kterou chcete vložit.</p></li>
</ol></div>
</div></div></div></div>
</div></section><section id="compose"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Klávesa Compose</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Klávesa <span class="key"><kbd>Compose</kbd></span> je speciální klávesa, která umožňuje zmáčknout více kláves po sobě, abyste získali speciální znak. Například pro napsání dlouhého <span class="em">é</span> zmáčkněte <span class="key"><kbd>Compose</kbd></span>, pak <span class="key"><kbd>'</kbd></span> a pak <span class="key"><kbd>e</kbd></span>.</p>
<p class="p">Drtivá většina klávesnic nemá zvlášť klávesu <span class="key"><kbd>Compose</kbd></span>. Můžete si ji ale namapovat na některou jinou existující klávesu na své klávesnici.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h3><span class="title">Nadefinování klávesy Compose</span></h3></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Nastavení</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Klávesnice</span> v postranním panelu otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">V části <span class="gui">Psaní speciálních znaků</span> klikněte na <span class="gui">Klávesa Compose</span>.</p></li>
<li class="steps"><p class="p">Zapněte přepínač <span class="gui">Klávesa Compose</span>.</p></li>
<li class="steps"><p class="p">Zaškrtněte políčko u klávesy, kterou chcete nastavit jako klávesu Compose.</p></li>
<li class="steps"><p class="p">Zavřete dialogové okno.</p></li>
</ol></div>
</div></div>
<p class="p">Pomocí klávesy <span class="key"><kbd>Compose</kbd></span> můžete psát řadu běžných znaků, například:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Zmáčkněte <span class="key"><kbd>Compose</kbd></span>, pak <span class="key"><kbd>'</kbd></span> a pak písmeno, nad které se přidá čárka, jako třeba <span class="em">é</span>.</p></li>
<li class="list"><p class="p">Zmáčkněte <span class="key"><kbd>Compose</kbd></span>, pak <span class="key"><kbd>`</kbd></span> a pak písmeno, nad které se přidá obrácená čárka (těžký akcent), jako třeba <span class="em">è</span>.</p></li>
<li class="list"><p class="p">Zmáčkněte <span class="key"><kbd>Compose</kbd></span>, pak <span class="key"><kbd>"</kbd></span> a pak písmeno, nad které se přidá přehláska, jako třeba <span class="em">ë</span>.</p></li>
<li class="list"><p class="p">Zmáčkněte <span class="key"><kbd>Compose</kbd></span>, pak <span class="key"><kbd>-</kbd></span> a pak písmeno, nad které se přidá vodorovná čárka, jako třeba <span class="em">ē</span>.</p></li>
</ul></div></div></div>
<p class="p">Na další možnosti kombinací s klávesou <span class="key"><kbd>Compose</kbd></span> se podívejte na <span class="link"><a href="https://cs.wikipedia.org/wiki/Compose##Nejb.C4.9B.C5.BEn.C4.9Bj.C5.A1.C3.AD_kombinace" title="https://cs.wikipedia.org/wiki/Compose##Nejb.C4.9B.C5.BEn.C4.9Bj.C5.A1.C3.AD_kombinace">Wikipedii na stránku o klávese compose</a></span>.</p>
</div></div>
</div></section><section id="ctrlshiftu"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Kódy znaků</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Znaky Unikódu můžete zadávat čistě pomocí klávesnice použitím číselného kódu znaku. Každý znak je představován čtyřmístným kódem znaku v šestnáctkové soustavě. Když si chcete tento kód zjistit, najděte si znak v aplikaci <span class="app">Znaky</span>. Kód tvoří čtyři znaky (šestnáctkové číslice) za <span class="gui">U+</span>.</p>
<p class="p">Když chcete zadat znak pomocí jeho kódu, zmáčkněte <span class="key"><kbd>Ctrl</kbd></span><span class="key"><kbd>Shift</kbd></span><span class="key"><kbd>U</kbd></span>, následně napište čtyři znaky kódu a pak zmáčkněte <span class="key"><kbd>mezerník</kbd></span> nebo <span class="key"><kbd>Enter</kbd></span>. Pokud často používáte nějaké znaky, které nejsou snadno dostupné jinými metodami, může stát za to, naučit se zpaměti kódy těchto znaků, abyste je mohli rychle zadávat.</p>
</div></div>
</div></section><section id="layout"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Rozložení klávesnice</span></h2></div>
<div class="region"><div class="contents pagewide"><p class="p">Můžete přimět svoji klávesnici, aby se chovala jako klávesnice pro jiný jazyk, než pro který má na klávesách popisky. Mezi různými rozvrženími klávesnice se můžete přepínat pomocí ikony na horní liště. Jak na to viz <span class="link"><a href="keyboard-layouts.html.cs" title="Používání alternativního rozložení klávesnice">Používání alternativního rozložení klávesnice</a></span>.</p></div></div>
</div></section><section id="im"><div class="inner">
<div class="hgroup pagewide"><h2 class="title"><span class="title">Vstupní metody</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Vstupní metody rozšiřují předchozí metody pomocí vkládání znaků i pomocí jiných vstupních zařízení, než jen klávesnice. Například můžete zadávat znaky pomocí myši s využitím gest nebo zadávat japonské znaky pomocí klávesnice s latinkou.</p>
<p class="p">Pro výběr vstupní metody klikněte pravým tlačítkem na textový widget a v nabídce <span class="gui">Vstupní metoda</span> vyberte tu, kterou chcete používat. Není poskytována žádná výchozí, takže se podívejte do dokumentace ke vstupním metodám, jak se používají.</p>
</div></div>
