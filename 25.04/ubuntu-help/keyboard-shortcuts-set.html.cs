<div class="hgroup pagewide"><h2 class="title"><span class="title">Vlastní klávesové zkratky</span></h2></div>
<div class="region"><div class="contents pagewide">
<p class="p">Když si chcete nadefinovat své vlastní klávesové zkratky v nastavení <span class="gui">Klávesové zkratky</span>:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Vyberte <span class="gui">Vlastní zkratky</span>.</p></li>
<li class="steps"><p class="p">Jestliže zatím neexistuje žádná vlastní klávesová zkratka, klikněte na tlačítko <span class="gui">Přidat klávesovou zkratku</span>, jinak klikněte na tlačítko <span class="gui">+</span>. Objeví se okno <span class="gui">Přidání vlastní klávesové zkratky</span>.</p></li>
<li class="steps"><p class="p">Napište <span class="gui">Název</span>, podle kterého se klávesová zkratka pozná, a <span class="gui">Příkaz</span>, který spustí aplikaci. Například, když chcete klávesovou zkratku, která spustí <span class="app">Rhythmbox</span>, můžete ji pojmenovat <span class="input">Hudba</span> a použít příkaz <span class="input">rhythmbox</span>.</p></li>
<li class="steps"><p class="p">Klikněte na tlačítko <span class="gui">Přidat klávesovou zkratku…</span>. V okně <span class="gui">Přidání vlastní klávesové zkratky</span> podržte zmáčknoutou požadovanou kombinaci kláves.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Přidat</span>.</p></li>
</ol></div></div></div>
<p class="p">Název příkazu, který jste zadali, by měl být platný systémový příkaz. Jestli funguje, si můžete ověřit po otevření Terminálu a jeho zadání v něm. Příkaz, který otevírá aplikace, nemusí mít nutně stejný název, jako ona aplikace.</p>
<p class="p">Když chcete změnit příkaz, který je přiřazený vlastní klávesové zkratce, klikněte na řádek s klávesovou zkratkou. Objeví se okno <span class="gui">Nastavení vlastní klávesové zkratky</span>, ve kterém můžete příkaz upravit.</p>
