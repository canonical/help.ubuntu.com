<div class="hgroup"><h1 class="title"><span class="title">Řízení, který účet služby on-line může být použit k přístupu</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Někteří poskytovatelé účtů on-line umožňují přístup k několika službám s tím samým uživatelským účtem. Například účet Google poskytuje přístup ke kalendáři, e-mailu, kontaktům a diskuzím. Možná ale budete chtít účet používat jen pro jednu službu a pro ostatní ne. Například můžete chtít použít účet Google pro e-mail, ale ne pro diskuze, protože k tomu máte jiný účet jinde.</p>
<p class="p">Můžete zakázat některé služby, které jednotlivé účty on-line poskytují:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Účty on-line</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Účty on-line</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">V seznamu nalevo vyberte účet, ve kterém chcete provést změny.</p></li>
<li class="steps"><p class="p">Seznam služeb, které jsou dostupné u tohoto účtu bude zobrazen u <span class="gui">Použít pro</span>. Podívejte se na <span class="link"><a href="accounts-which-application.html" title="Služby a aplikace on-line">Služby a aplikace on-line</a></span>, které aplikace přistupují ke kterým účtům.</p></li>
<li class="steps"><p class="p">Vypněte libovolné služby, které nechcete používat.</p></li>
</ol></div></div></div>
<p class="p">Jakmile je služba u účtu zakázaná, aplikace ji již nemohou po připojení k účtu použít.</p>
<p class="p">Abyste zapnuli službu, kterou jste zakázali, stačí se vrátit do okna <span class="gui">Účty on-line</span> a zapnout ji.</p>
</div>
