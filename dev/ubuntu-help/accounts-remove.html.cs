<div class="hgroup"><h1 class="title"><span class="title">Odebrání účtu</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Účet on-line, který si již nadále nepřejete používat, můžete z nastavení odebrat.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents">
<p class="p">Řada služeb on-line poskytuje ověřovací tiket, který si GNOME uchovává namísto hesla. Když odeberete některý z účtů, měli byste rovněž odvolat tento certifikát. Tím se zajistí, že se žádná jiná aplikace nebo webová stránka nemohou připojit k této službě s pomocí ověření používaného pro GNOME.</p>
<p class="p">Způsob, jak ověření odvolat záleží na poskytovateli služby. Podívejte se do svého nastavení na webu poskytovatele po něčem jako ověřené nebo připojené aplikace nebo počítače. Najděte aplikaci s názvem „GNOME“ a odeberte ji.</p>
</div></div></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Účty on-line</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Účty on-line</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Vyberte účet, který si přejete odebrat.</p></li>
<li class="steps"><p class="p">Klikněte na tlačítko <span class="gui">-</span> v levém dolním rohu okna.</p></li>
<li class="steps"><p class="p">V potvrzovacím dialogovém okně klikněte na <span class="gui">Odebrat</span>.</p></li>
</ol></div></div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Místo úplného vymazání účtu je možné jen <span class="link"><a href="accounts-disable-service.html.cs" title="Řízení, který účet služby on-line může být použit k přístupu">omezit služby</a></span>, ke kterým bude z uživatelského prostředí přístup.</p></div></div></div></div>
