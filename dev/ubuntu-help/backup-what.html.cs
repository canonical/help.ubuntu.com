<div class="hgroup"><h1 class="title"><span class="title">Co zálohovat</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Vaší prioritou by měla být záloha vašich <span class="link"><a href="backup-thinkabout.html.cs" title="Kde mohu najít soubory, které chci zálohovat?">nejdůležitějších souborů</a></span> a souborů, které by bylo náročné vytvořit znovu. Zde jsou příklady seřazené od nejdůležitějších po méně důležité:</p>
<div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Vaše osobní soubory</dt>
<dd class="terms"><p class="p">Mohou to být dokumenty, tabulky, e-maily, kalendářová data, finanční údaje, rodinné fotografie nebo jiné osobní soubory, které považujete za nenahraditelné.</p></dd>
<dt class="terms">Vaše osobní nastavení</dt>
<dd class="terms"><p class="p">Do tohoto spadají změny, které jste provedli v barvách, pozadí, rozlišení obrazovky a nastavení myši ve vašem uživatelském prostředí. Rovněž sem patří předvolby aplikací, jako jsou <span class="app">LibreOffice</span>, hudební přehrávač nebo poštovní program. To vše lze sice vytvořit znovu, ale zabere to čas.</p></dd>
<dt class="terms">Systémová nastavení</dt>
<dd class="terms"><p class="p">Většina lidí nikdy nemění systémová nastavení, která se vytvořila při instalaci. Pokud jste si ale tato nastavení z nějakého důvodu přizpůsobili nebo se jedná o server, můžete chtít zálohovat i tato nastavení.</p></dd>
<dt class="terms">Nainstalovaný software</dt>
<dd class="terms"><p class="p">Software, který používáte, lze za normálních okolností po nějakých problémech s počítačem docela rychle znovu nainstalovat.</p></dd>
</dl></div></div></div>
<p class="p">Obecně řečeno, chcete zálohovat soubory, které jsou nenahraditelné a soubory, u kterých je třeba věnovat velké množství času k jejich obnovení bez zálohy. Na druhou stranu, pokud jsou snadno nahraditelné, nemusíte chtít, aby vám zabírali místo v úložišti se zálohou.</p>
</div>
