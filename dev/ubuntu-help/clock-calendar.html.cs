<div class="hgroup"><h1 class="title"><span class="title">Kalendářové události</span></h1></div>
<div class="region">
<div class="contents">
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents">
<p class="p">Tato funkcionalita vyžaduje použití kalendáře <span class="app">Evolution</span>, nebo abyste měli nastavený nějaký účet on-line, který podporuje <span class="gui">Kalendář</span>.</p>
<p class="p">Většina distribucí má <span class="app">Evolution</span> předinstalován ve výchozím stavu. Jestliže vaše ne, budete jej možná muset nainstalovat pomocí správy balíčků ve své distribuci.</p>
</div></div></div></div>
<p class="p">Kdy si chcete zobrazit své události:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na hodiny na horní liště.</p></li>
<li class="steps">
<p class="p">Klikněte na datum, pro které chcete vidět své události z kalendáře.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Pod každým datem, které má nějakou událost, je tečka.</p></div></div></div></div>
<p class="p">Stávající události se zobrazují v kalendáři nalevo. Jakmile je událost přidána do kalendáře v aplikaci <span class="app">Evolution</span>, objeví se v hodinách v seznamu událostí.</p>
</li>
</ol></div></div></div>
