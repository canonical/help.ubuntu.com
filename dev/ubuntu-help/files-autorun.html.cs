<div class="hgroup"><h1 class="title"><span class="title">Otevření aplikace pro zařízení nebo disk</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete mít aplikaci, která se automaticky spustí, kdy je připojeno zařízení nebo vložen disk nebo paměťová karta. Například můžete chtít, aby se při připojení digitálního fotoaparátu spustil správce fotografií. Tuto funkci ale můžete i vypnout, takže po připojení se nestane nic.</p>
<p class="p">Když chcete rozhodnout, které aplikace by se měly spouštět při připojení různých zařízení:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Podrobnosti</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Podrobnosti</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Výměnná média</span>.</p></li>
<li class="steps">
<p class="p">Najděte požadované zařízení nebo typ média a vyberte pro něj aplikaci nebo činnost. Níže jsou uvedeny popisy pro různé typy zařízení a médií.</p>
<p class="p">Místo spuštění aplikace můžete také pomocí volby <span class="gui">Otevřít složku</span> nastavit, aby se zařízení zobrazilo ve správci souborů. Když se tak stane, budete dotázáni, co se má udělat a do té doby se automaticky nic nestane.</p>
</li>
<li class="steps"><p class="p">Jestliže v seznamu nevidíte zařízení nebo médium, které chcete změnit (třeba disk Blu-ray nebo čtečku e-knih), klikněte na <span class="gui">Ostatní média…</span>, aby se zobrazil podrobnější seznam zařízení. Vyberte typ zařízení nebo média z rozbalovacího seznamu <span class="gui">Typ</span> a aplikaci nebo činnost z rozbalovacího seznamu <span class="gui">Činnost</span>.</p></li>
</ol></div></div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud nechcete, aby se žádná aplikace otevřela automaticky, ať už připojíte nebo vložíte cokoliv, zaškrtněte v dolní části dialogového okna <span class="gui">Podrobnosti</span> volbu <span class="gui">Nikdy se nedotazovat nebo spouštět programy na vloženém médiu</span>.</p></div></div></div></div>
</div>
<div id="files-types-of-devices" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Typy zařízení a médií</span></h2></div>
<div class="region"><div class="contents"><div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Zvukové disky</dt>
<dd class="terms"><p class="p">Vyberte svoji oblíbenou hudební aplikaci nebo aplikaci na extrahování zvukových stop z CD. Pokud používáte zvuková DVD (DVD-A), vyberte, jak se mají otevírat, v <span class="gui">Ostatní média…</span>. Když otevřete zvukový disk ve správci souborů, objeví se zvukové stopy jako soubory WAV, které můžete přehrát v libovolné aplikaci pro přehrávání zvuků.</p></dd>
<dt class="terms">Videodisky</dt>
<dd class="terms"><p class="p">Vyberte svoji oblíbenou aplikaci pro video z disku DVD. K nastavení aplikace pro Blu-ray, HD DVD, video CD (VCD) a super video CD (SVCD) použijte tlačítko <span class="gui">Ostatní média…</span>. Pokud DVD nebo jiné videodisky po vložení nefungují správně, podívejte se na <span class="link"><a href="video-dvd.html" title="Proč se nechtějí DVD přehrávat?">Proč se nechtějí DVD přehrávat?</a></span>.</p></dd>
<dt class="terms">Prázdné disky</dt>
<dd class="terms"><p class="p">Použijte tlačítko <span class="gui">Ostatní média…</span> k výběru aplikace pro zápis na prázdné disky CD, DVD, Blu-ray a HD DVD.</p></dd>
<dt class="terms">Fotoaparáty a fotografie</dt>
<dd class="terms">
<p class="p">Použijte rozbalovací seznam <span class="gui">Fotografie</span> k výběru aplikace pro správu fotografií, která se má spustit, když připojíte svůj digitální fotoaparát nebo vložíte paměťovou kartu, jako je CF, SD, MMC nebo MS, s fotografiemi. Případně můžete své fotografie obyčejně procházet ve správci souborů.</p>
<p class="p">Pomocí tlačítka <span class="gui">Ostatní média…</span> můžete vybrat aplikaci, která má otevírat Kodak Picture CD, jaké vám udělají ve fotolabu. Ve skutečnosti se jedná o běžné CD s obrázky JPEG uloženými ve složce nazvané <span class="file">PICTURES</span>.</p>
</dd>
<dt class="terms">Hudební přehrávače</dt>
<dd class="terms"><p class="p">Vyberte aplikaci pro správu hudební knihovny na přenosném hudebním přehrávači, případně si soubory můžete ručně spravovat ve správci souborů.</p></dd>
<dt class="terms">Čtečky elektronických knih</dt>
<dd class="terms"><p class="p">Pomocí tlačítka <span class="gui">Ostatní média…</span> si vyberte aplikaci pro správu knih na své čtečce e-knih, případně je můžete spravovat ručně ve správci souborů.</p></dd>
<dt class="terms">Software</dt>
<dd class="terms">
<p class="p">Některé disky a výměnná média obsahují software, u kterého se předpokládá, že bude spuštěn automaticky po vložení média. K nastavení toho, co se má stát po vložení média s automaticky spustitelným softwarem použijte volbu <span class="gui">Software</span>. Před spuštěním softwaru budete vždy vyzváni k potvrzení.</p>
<div class="note note-warning" title="Varování"><div class="inner"><div class="region"><div class="contents"><p class="p">Nikdy nespouštějte software z médií, kterým nemůžete plně důvěřovat.</p></div></div></div></div>
</dd>
</dl></div></div></div></div></div>
