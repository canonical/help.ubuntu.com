<div class="hgroup"><h1 class="title"><span class="title">Přejmenování souboru nebo složky</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Stejně jako jiné správce souborů, i <span class="app">Soubory</span> můžete použít ke změně názvu souboru nebo složky.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Když chcete přejmenovat soubor nebo složku:</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na položku pravým tlačítkem a vyberte <span class="gui">Přejmenovat</span> nebo vyberte soubor a zmáčkněte <span class="key"><kbd>F2</kbd></span>.</p></li>
<li class="steps"><p class="p">Napište nový název a zmáčkněte <span class="key"><kbd>Enter</kbd></span> nebo klikněte na <span class="gui">Přejmenovat</span>.</p></li>
</ol></div>
</div></div>
<p class="p">Soubor můžete přejmenovat také v okně <span class="link"><a href="nautilus-file-properties-basic.html" title="Vlastnosti souboru">vlastností</a></span>.</p>
<p class="p">Když se pustíte do přejmenování souboru, bude označena jen jeho první část a přípona ne (tj. část za poslední <span class="file">.</span>). Přípona normálně naznačuje, o jaký typ souboru se jedná (například <span class="file">soubor.pdf</span> je dokument PDF), a normálně ji nepotřebujete měnit. Pokud potřebujete změnit i příponu, označte celý název a změňte jej.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Když přejmenujete nesprávný soubor nebo zadáte nesprávný název, můžete přejmenování vrátit zpět. Vrácení proveďte ihned kliknutím na tlačítko nabídky v nástrojové liště a volbou <span class="gui">Vrátit přejmenování</span>, nebo zmáčknutím <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>Z</kbd></span></span>. Soubor dostane svůj dřívější název.</p></div></div></div></div>
</div>
<div id="valid-chars" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Platné znaky pro názvy souborů</span></h2></div>
<div class="region"><div class="contents">
<p class="p">V názvu souboru můžete použít libovolný znak, vyjma <span class="file">/</span> (lomítko). Některá zařízení ale používají <span class="em">souborové systémy</span>, které mají rozsáhlejší omezení na názvy souborů. Proto je dobrým zvykem vyhnout se v názvech souborů následujícím znakům: <span class="file">|</span>, <span class="file">\</span>, <span class="file">?</span>, <span class="file">*</span>, <span class="file">&lt;</span>, <span class="file">"</span>, <span class="file">:</span>, <span class="file">&gt;</span> a <span class="file">/</span>.</p>
<div class="note note-warning" title="Varování"><div class="inner"><div class="region"><div class="contents"><p class="p">Pokud pojmenujete soubor s <span class="file">.</span> (tečka) jako prvním znakem a zkusíte se na něj podívat ve správci souborů, zjistíte, že je <span class="link"><a href="files-hidden.html" title="Skrytí souboru">skrytý</a></span>.</p></div></div></div></div>
</div></div>
</div></div>
<div id="common-probs" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Běžné problémy</span></h2></div>
<div class="region"><div class="contents"><div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Název souboru je již použit</dt>
<dd class="terms">
<p class="p">Nemůžete mít dva soubory nebo složky se stejným názvem v jedné složce. Když se pokusíte přejmenovat soubor na název, který již ve složce existuje, správce souborů vám to nedovolí.</p>
<p class="p">U názvů souborů a složek se rozlišuje velikost písmen, takže název <span class="file">Soubor.txt</span> není to stejné co <span class="file">SOUBOR.txt</span>. Použití názvů lišících se jen velikostí písmen, jako v tomto příkladu, je sice možné, ale nedoporučuje se.</p>
</dd>
<dt class="terms">Název souboru je příliš dlouhý</dt>
<dd class="terms"><p class="p">Na některých souborových systémech nemohou mít názvy souborů více než 255 znaků. Toto omezení zahrnuje jak vlastní název souboru, tak cestu k němu (například <span class="file">/home/venda/Dokumenty/práce/rozjednané zakázky/…</span>), takže byste se měli vyhnout dlouhým názvům souborů a složek, pokud je to možné.</p></dd>
<dt class="terms">Volba pro přejmenování je zašedlá</dt>
<dd class="terms"><p class="p">Jestliže je <span class="gui">Přejmenovat</span> zašedlé, nemáte oprávnění soubor přejmenovat. Měli byste být obezřetní ohledně přejmenování takovýchto souboru, protože přejmenování některých chráněných souborů může způsobit nestabilitu systému. Více podrobností viz <span class="link"><a href="nautilus-file-properties-permissions.html" title="Nastavení oprávnění souboru">Nastavení oprávnění souboru</a></span>.</p></dd>
</dl></div></div></div></div></div>
