<div class="hgroup"><h1 class="title"><span class="title">Hledání souborů</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Přímo ve správci souborů můžete hledat soubory podle jejich názvu nebo typu.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Hledání</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Z přehledu <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> otevřete aplikaci <span class="app">Soubory</span>.</p></li>
<li class="steps"><p class="p">V případě, že víte, ve které konkrétní složce se požadované soubory nachází, přejděte do ní.</p></li>
<li class="steps">
<p class="p">Napište slovo nebo slova, o kterých víte, že se v názvu souboru nachází, a ty se objeví ve vyhledávací liště. Například, když vaše soubory s fakturami začínají slovem „Faktura“, napište <span class="input">faktura</span>. Slova se porovnávají bez ohledu na velikost písmen.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Místo abyste začali přímo psát, můžete si vyhledávací lištu zobrazit předem kliknutím na tlačítko s lupou na nástrojové liště nebo zmáčknutím <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>F</kbd></span></span>.</p></div></div></div></div>
</li>
<li class="steps">
<p class="p">Výsledky můžete zúžit určením umístění a typu souboru.</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">Kliknutím na <span class="gui">Home</span> omezíte výsledky hledání na svoji složku <span class="file">Domů</span> nebo klikněte na <span class="gui">Všechny soubory</span>, aby se hledalo všude.</p></li>
<li class="list"><p class="p">Klikněte na tlačítko <span class="gui">+</span> a v rozbalovacím seznamu vyberte <span class="gui">Typ souboru</span>, abyste výsledky hledání zúžili na konkrétní typ souboru. Kliknutím na tlačítko <span class="gui">×</span> tuto volbu odeberete a výsledky hledání zase rozšíříte.</p></li>
</ul></div></div></div>
</li>
<li class="steps"><p class="p">Soubory ve výsledcích hledání můžete otevírat, kopírovat, mazat a i jinak s nimi pracovat stejným způsobem, jako byste byli v běžné složce ve správci souborů.</p></li>
<li class="steps"><p class="p">Opětovným kliknutím na tlačítko s lupou na nástrojové liště hledání opustíte a vrátíte se do složky.</p></li>
</ol></div>
