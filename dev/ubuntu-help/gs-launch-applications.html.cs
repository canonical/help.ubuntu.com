<div id="launch-apps-mouse" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Spouštění aplikací pomocí myši</span></h2></div>
<div class="region"><div class="contents"><div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Přejeďte ukazatelem myši do rohu <span class="gui">Činnosti</span> v levém horním rohu obrazovky a zobrazí se vám <span class="gui">Přehled činností</span>.</p></li>
<li class="steps"><p class="p">Klikněte na ikonu <span class="gui">Zobrazit aplikace</span>, kterou najdete v dolní části lišty na levé straně obrazovky.</p></li>
<li class="steps"><p class="p">Zobrazí se seznam aplikací. Klikněte na aplikaci, kterou chcete spustit, například Nápověda.</p></li>
</ol></div></div></div></div></div>
</div></div>
<div id="launch-app-keyboard" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Spouštění aplikací pomocí klávesnice</span></h2></div>
<div class="region"><div class="contents"><div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete <span class="gui">Přehled činností</span> zmáčknutím klávesy <span class="key"><a href="help:gnome-help/keyboard-key-super" title="help:gnome-help/keyboard-key-super"><kbd>Super</kbd></a></span>.</p></li>
<li class="steps"><p class="p">Začněte psát název aplikace, kterou chcete spustit. Aplikace se ihned začne vyhledávat.</p></li>
<li class="steps"><p class="p">Až se vám zobrazí ikona požadované aplikace, vyberte ji a zmáčknutím <span class="key"><kbd>Enter</kbd></span> aplikaci spusťte.</p></li>
</ol></div></div></div></div></div>
