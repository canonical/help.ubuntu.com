<div class="hgroup"><h1 class="title"><span class="title">Vyhledávání v systému</span></h1></div>
<div class="region">
<div class="contents">
<div class="media media-image"><div class="inner"><img src="gs-search1.svg" width="100%" class="media media-block" alt=""></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps"><li class="steps">
<p class="p">Otevřete <span class="gui">Přehled činností</span> zmáčknutím klávesy <span class="key"><a href="help:gnome-help/keyboard-key-super" title="help:gnome-help/keyboard-key-super"><kbd>Super</kbd></a></span> a začněte hledat tím, že začnete psát.</p>
<p class="p">Výsledky vyhovující tomu, co jste napsali, se budou objevovat průběžně. První výsledek je vždy zvýrazněný a zobrazený nahoře.</p>
<p class="p">Zmáčknutím <span class="key"><kbd>Enter</kbd></span> se na tento první zvýrazněný výsledek přepnete.</p>
</li></ol></div></div></div>
<div class="media media-image"><div class="inner"><img src="gs-search2.svg" width="100%" class="media media-block" alt=""></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps" start="2">
<li class="steps">
<p class="p">Mezi položky, které se mohou objevit ve výsledcích hledání patří:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">vyhovující aplikace (zobrazí se ve výsledcích jako první)</p></li>
<li class="list"><p class="p">vyhovující nastavení</p></li>
<li class="list"><p class="p">vyhovující kontakty</p></li>
<li class="list"><p class="p">vyhovující dokumenty</p></li>
</ul></div></div></div>
</li>
<li class="steps">
<p class="p">Kliknutím na položku ve výsledcích hledání se na ni přepnete.</p>
<p class="p">Případně položku vyberte pomocí klávesových šipek a zmáčkněte <span class="key"><kbd>Enter</kbd></span>.</p>
</li>
</ol></div></div></div>
</div>
<div id="use-search-inside-applications" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Vyhledávání v aplikacích</span></h2></div>
<div class="region"><div class="contents"><p class="p">Vyhledávání v systému sdružuje výsledky z různorodých aplikací. Po levé straně výsledků hledání můžete vidět ikony aplikací, které výsledky poskytly. Kliknutím na některou z těchto ikon spustíte opětovné hledání přímo v aplikaci patřící k dané ikoně. Protože v <span class="gui">Přehledu činností</span> se zobrazují jen nepřesnější shody, může vám hledání přímo v aplikaci přinést lepší výsledky.</p></div></div>
</div></div>
<div id="use-search-customize" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Přizpůsobení výsledků hledání</span></h2></div>
<div class="region"><div class="contents">
<div class="media media-image"><div class="inner"><img src="gs-search-settings.svg" width="100%" class="media media-block" alt=""></div></div>
<div class="note note-important" title="Důležité"><div class="inner"><div class="region"><div class="contents"><p class="p">GNOME vám umožňuje si přizpůsobit, co chcete zobrazit ve výsledcích hledání v <span class="gui">Přehledu činností</span>. Například si můžete zvolit, jestli chcete zobrazovat výsledky pro webové stránky, fotografie nebo hudbu.</p></div></div></div></div>
<div class="steps"><div class="inner">
<div class="title title-steps"><h3><span class="title">Když si chcete přizpůsobit, co se má zobrazovat ve výsledcích hledání:</span></h3></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na <span class="gui"><a href="shell-introduction.html#yourname" title="Vy a váš počítač">systémovou nabídku</a></span> v pravé části horní lišty.</p></li>
<li class="steps"><p class="p">Klikněte na tlačítko nastavení v levé dolní části nabídky.</p></li>
<li class="steps"><p class="p">Klikněte na panel <span class="gui">Hledání</span>.</p></li>
<li class="steps"><p class="p">V seznamu prohledávaných míst klikněte na přepínač <span class="gui">❙/○</span> vedle prohledávaného místa, které chcete povolit či zakázat.</p></li>
</ol></div>
</div></div>
