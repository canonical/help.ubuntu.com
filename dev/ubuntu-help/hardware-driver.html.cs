<div class="hgroup"><h1 class="title"><span class="title">Co je to ovladač?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Zařízení jsou fyzické „části“ počítače. Mohou být <span class="em">externí</span>, jako třeba tiskárna nebo monitor, nebo <span class="em">interní</span>, jako třeba grafická karta nebo zvuková karta.</p>
<p class="p">Aby systém v počítači dokázal tato zařízení používat, potřebuje vědět, jak s nimi komunikovat. Dělá to pomocí softwarových „částí“, nazývaných <span class="em">ovladače zařízení</span>.</p>
<p class="p">Když připojíte zařízení k počítači, musíte mít nainstalovaný správný ovladač, aby fungovalo. Například, když připojíte tiskárnu, ale nemáte k ní správný ovladač, nebudete ji moci používat. Normálně každý model zařízení používá jiný ovladač, který není zaměnitelný s ostatními.</p>
<p class="p">V Linuxu je většina ovladačů ve výchozím stavu nainstalovaná, takže by mělo vše po připojení fungovat. Ale mohou nastat případy, kdy je ovladač potřeba nainstalovat ručně nebo kdy není k dispozici.</p>
<p class="p">Navíc, některé existující ovladače jsou neúplné nebo částečně nefunkční. Například můžete zjistit, že některé tiskárně nejde tisknou oboustranně, ale vše ostatní funguje.</p>
