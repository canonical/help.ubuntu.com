<div class="hgroup"><h2 class="title"><span class="title">Vlastní klávesové zkratky</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Když si chcete nadefinovat své vlastní klávesové zkratky v nastavení <span class="gui">Klávesnice</span>:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Klikněte na tlačítko <span class="gui">+</span>. Objeví se okno <span class="gui">Vlastní zkratka</span>.</p></li>
<li class="steps"><p class="p">Napište <span class="gui">Název</span>, podle kterého se klávesová zkratka pozná, a <span class="gui">Příkaz</span>, který spustí aplikaci, a nakonec klikněte na <span class="gui">Přidat</span>. Například, když chcete klávesovou zkratku, která spustí <span class="app">Rhythmbox</span>, můžete ji pojmenovat <span class="input">Hudba</span> a použít příkaz <span class="input">rhythmbox</span>.</p></li>
<li class="steps"><p class="p">V řádku, který jste právě přidali, klikněte na <span class="gui">Vypnuto</span>. V tu chvíli se změní na <span class="gui">Nová klávesová zkratka…</span> a vy můžete zmáčknout požadovanou kombinaci kláves.</p></li>
</ol></div></div></div>
<p class="p">Název příkazu, který jste zadali, by měl být platný systémový příkaz. Jestli funguje, si můžete ověřit po otevření Terminálu a jeho zadání v něm. Příkaz, který otevírá aplikace, nemusí mít nutně stejný název, jako ona aplikace.</p>
<p class="p">Když chcete změnit příkaz, který je přiřazený vlastní klávesové zkratce, klikněte dvojitě na <span class="em">název</span> klávesové zkratky. Objeví se okno <span class="gui">Vlastní klávesová zkratka</span>, ve kterém můžete příkaz upravit.</p>
