<div class="hgroup"><h1 class="title"><span class="title">Změna rozlišení nebo otočení obrazovky</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pomocí změny <span class="em">rozlišení obrazovky</span> můžete změnit, jak velké (nebo jak podrobně vykreslené) mají věci na obrazovce vypadat. Pomocí změny <span class="em">otočení</span> můžete změnit, aby věci byly hlavou vzhůru (např. pokud máte otočný displej).</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Displeje</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Displeje</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Pokud máte více displejů a ty nejsou zrcadlené, můžete mít u každého jiné nastavení. Vyberte displej v části s náhledy.</p></li>
<li class="steps"><p class="p">Vyberte rozlišení a otočení.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Použít</span>. Nové nastavení se použije po dobu 20 vteřin, než je automaticky vráceno zpět. Díky tomu, když s novým nastavením nebude nic vidět, dostanete se zpět k původnímu nastavení. V případě, že je nové nastavení v pořádku a jste s ním spokojení, klikněte na <span class="gui">Zachovat změny</span>.</p></li>
</ol></div></div></div>
</div>
<div id="resolution" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Rozlišení</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Rozlišení je počet pixelů (obrazových bodů), které lze v jednotlivém směru zobrazit. Rozlišení má <span class="em">poměr stran</span>, což je poměr šířky a výšky. Širokoúhlé displeje používají poměr 16∶9, zatímco tradiční displeje 4∶3. Pokud zvolíte rozlišení, které neodpovídá poměrům stran vašeho displeje, bude obrazovka ořezána přidáním černých pruhů nahoru a dolů nebo po obou stranách, aby se zabránilo zkreslení.</p>
<p class="p">V rozbalovacím seznamu <span class="gui">Rozlišení</span> si můžete zvolit rozlišení, které vám vyhovuje. Pokud ale zvolíte takové, které není pro vaši obrazovku správné, bude obraz <span class="link"><a href="look-display-fuzzy.html" title="Proč vše na mojí obrazovce vypadá rozmazané/kostičkované?">vypadat rozmazaně nebo kostičkovaně</a></span>.</p>
</div></div>
</div></div>
<div id="rotation" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Otočení</span></h2></div>
<div class="region"><div class="contents"><p class="p">U některých notebooků (tzv. konvertibilní notebooky) je možné obrazovku fyzicky otočit do několika směrů. Rovněž některé modely monitorů mají možnost otočit displej o 90 stupňů (tzv. pivot). V takových případech se hodí možnost změnit softwarové otočení displeje. Můžete to udělat pomocí tlačítek se šipkami.</p></div></div>
