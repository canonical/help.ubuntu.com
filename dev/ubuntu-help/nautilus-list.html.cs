<div class="hgroup"><h1 class="title"><span class="title">Předvolby sloupců v seznamu souborů</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">K dispozici je jedenáct různých sloupců s informace, které si můžete zobrazit ve sloupcovém zobrazení v <span class="gui">Souborech</span>. Klikněte na <span class="gui">Soubory</span> v horní liště, zvolte <span class="gui">Předvolby</span> a vyberte kartu <span class="gui">Sloupce seznamu</span>, abyste mohli vybrat, které sloupce budou viditelné.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Můžete použít tlačítka <span class="gui">∧</span> a <span class="gui">∨</span> ke změně pořadí, v jakém se vybrané sloupce objeví. Když potřebujete změny vrátit zpět a používat výchozí sloupce, klikněte na <span class="gui">Nastavit na výchozí</span>.</p></div></div></div></div>
<div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms"><span class="gui">Název</span></dt>
<dd class="terms">
<p class="p">Název souboru nebo složky.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Sloupec <span class="gui">Název</span> nelze skrýt.</p></div></div></div></div>
</dd>
<dt class="terms"><span class="gui">Velikost</span></dt>
<dd class="terms"><p class="p">Velikost složky je dána počtem položek, které obsahuje. Velikost souboru je uvedena v bajtech, kB nebo MB.</p></dd>
<dt class="terms"><span class="gui">Typ</span></dt>
<dd class="terms"><p class="p">Zobrazeno buď jako složka nebo jako typ souboru, jako je dokument PDF, obrázek JPEG, zvuk MP3 apod.</p></dd>
<dt class="terms"><span class="gui">Změněno</span></dt>
<dd class="terms"><p class="p">Uvádí datum z časového údaje, kdy byl soubor naposledy změněn.</p></dd>
<dt class="terms"><span class="gui">Vlastník</span></dt>
<dd class="terms"><p class="p">Jméno uživatele, který složku nebo soubor vlastní.</p></dd>
<dt class="terms"><span class="gui">Skupina</span></dt>
<dd class="terms"><p class="p">Skupina, která soubor vlastní. Normálně je každý uživatel ve své vlastní skupině, ale může být ve skupině i více uživatelů. Například ve firemním prostředí může mít svoji skupinu každé oddělení.</p></dd>
<dt class="terms"><span class="gui">Oprávnění</span></dt>
<dd class="terms">
<p class="p">Zobrazuje oprávnění přístupu k souboru. Například <span class="gui">drwxrw-r--</span></p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p">První znak představuje typ souboru. <span class="gui">-</span> znamená běžný soubor a <span class="gui">d</span> složku. Ve zřídkavých případech můžete narazit i na jiná písmena.</p></li>
<li class="list"><p class="p">Následující tři znaky <span class="gui">rwx</span> určují oprávnění pro uživatele, který soubor vlastní.</p></li>
<li class="list"><p class="p">Následující tři znaky <span class="gui">rw-</span> určují oprávnění pro všechny členy skupiny, která soubor vlastní.</p></li>
<li class="list"><p class="p">Poslední tři znaky <span class="gui">r--</span> určují oprávnění pro všechny ostatní uživatele v systému.</p></li>
</ul></div></div></div>
<p class="p">Jednotlivá oprávnění mají následující význam:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list"><p class="p"><span class="gui">r</span>: čtení (Read) znamená, že můžete soubor nebo složku otevřít</p></li>
<li class="list"><p class="p"><span class="gui">w</span>: zápis (Write) znamená, že můžete ukládat změny</p></li>
<li class="list"><p class="p"><span class="gui">x</span>: spouštění (eXxecute) znamená, že pokud se jedná o program nebo skript, můžete jej spustit, nebo u složky, že máte přístup k podsložkám a souborům v ní</p></li>
<li class="list"><p class="p"><span class="gui">-</span>: žádné oprávnění není nastaveno</p></li>
</ul></div></div></div>
</dd>
<dt class="terms"><span class="gui">Typ MIME</span></dt>
<dd class="terms"><p class="p">Zobrazuje typ MIME položky.</p></dd>
<dt class="terms"><span class="gui">Umístění</span></dt>
<dd class="terms"><p class="p">Cesta k umístění souboru.</p></dd>
<dt class="terms"><span class="gui">Změněno — čas</span></dt>
<dd class="terms"><p class="p">Uvádí datum a čas, kdy byl soubor naposledy změněn.</p></dd>
<dt class="terms"><span class="gui">Použito</span></dt>
<dd class="terms"><p class="p">Uvádí datum nebo čas, kdy byl soubor naposledy změněn.</p></dd>
</dl></div></div></div>
