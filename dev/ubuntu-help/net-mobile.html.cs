<div class="hgroup"><h1 class="title"><span class="title">Mobilní širokopásmové připojení</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Nastavte si připojení k mobilní síti (3G) pomocí modemu 3G vestavěného ve vašem počítači, mobilního telefonu nebo externího modemu.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Pokud nemáte vestavěný modem 3G, připojte svůj telefon nebo modem do portu USB na počítači.</p></li>
<li class="steps"><p class="p">Otevřete <span class="gui"><a href="shell-introduction.html#yourname" title="Vy a váš počítač">systémovou nabídku</a></span> vpravo na horní liště.</p></li>
<li class="steps">
<p class="p">Vyberte <span class="gui"><span class="media"><span class="media media-image"><img src="figures/network-cellular-signal-excellent-symbolic.svg" height="16" width="16" class="media media-inline" alt=""></span></span> Širokopásmové připojení   Vypnuto</span>. Oddíl <span class="gui">Širokopásmové připojení</span> v nabídce se rozbalí.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Když se ve stavové nabídce systému neobjeví <span class="gui">Mobilní širokopásmové</span>, ujistěte se, že není zařízení nastavené, aby se připojovalo jako „Mass Storage“.</p></div></div></div></div>
</li>
<li class="steps"><p class="p">Vyberte <span class="gui">Připojit</span>. Když se připojujete poprvé, spustí se průvodce <span class="gui">Nastavení mobilního širokopásmového připojení</span>. Na otevřené obrazovce je zobrazen seznam požadovaných informací. Klikněte na <span class="gui">Další</span>.</p></li>
<li class="steps"><p class="p">V seznamu vyberte zemi nebo region svého poskytovatele. Klikněte na <span class="gui">Následující</span>.</p></li>
<li class="steps"><p class="p">V seznamu vyberte svého poskytovatele. Klikněte na <span class="gui">Následující</span>.</p></li>
<li class="steps"><p class="p">Vyberte plán odpovídající typu zařízení, které připojujete. Tím se určí APN (Acces Point Name – název přístupového bodu). Klikněte na <span class="gui">Další</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na tlačítko <span class="gui">Použít</span> potvrďte nastavení, které jste vybrali. Průvodce se zavře a panel <span class="gui">Síť</span> zobrazí vlastnosti vašeho připojení.</p></li>
</ol></div></div></div>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Některé telefony nabízejí sdílené připojení přes USB (<span class="em">USB tethering</span>), které nevyžaduje na počítači žádné nastavení. Když je toto nastavení na telefonu aktivováno, objeví se v systémové nabídce <span class="gui">Ethernet je připojeno</span> a v panelu <span class="gui">Síť</span> ovládacího centra položka <span class="gui">USB Ethernet</span>.</p></div></div></div></div>
</div>
