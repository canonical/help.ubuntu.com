<div class="hgroup"><h1 class="title"><span class="title">Nastavení automatického uspávání</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete počítač nastavit, aby se při nečinnosti automaticky uspával do paměti. Lze určit jiný čas pro běh z baterie a jiný pro běh z elektrické sítě.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Napájení</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Napájení</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">V části <span class="gui">Uspávání a vypínání</span> klikněte na <span class="gui">Automaticky uspat</span>.</p></li>
<li class="steps">
<p class="p">Zvolte <span class="gui">Při napájení z baterie</span> nebo <span class="gui">Při napájení ze sítě</span>, přepněte vypínač na <span class="gui">❙</span> (zapnuto) a vyberte čas <span class="gui">Po uplynutí</span>. Nastavit můžete obě volby.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">U stolního počítače je jedna volba označená jako <span class="gui">Při nečinnosti</span>.</p></div></div></div></div>
</li>
</ol></div></div></div>
