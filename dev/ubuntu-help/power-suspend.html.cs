<div class="hgroup"><h1 class="title"><span class="title">Co se stane, když svůj počítač uspím?</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Když počítač <span class="em">uspíte do paměti</span>, zůstanou všechny vaše aplikace a dokumenty otevřené, ale obrazovka a všechny ostatní části počítače se vypnout, aby se šetřila energie. Počítač sice zůstane zapnutý a pude potřebovat napájení, ale spotřeba bude velmi malá. Probudit jej můžete zmáčknutím klávesy nebo kliknutím myší. Pokud to nepůjde, zkuste zmáčknout tlačítko napájení.</p>
<p class="p">Některé počítače mají problémy s hardwarovou podporou uspávání, což znamená, že <span class="link"><a href="power-suspendfail.html" title="Proč se můj počítač nechce znovu zapnout po jeho uspání?">nemohou být správně uspány do paměti</a></span>. Je dobré si uspávání počítače napřed vyzkoušet, abyste věděli, jestli jej můžete používat.</p>
<div class="note note-important" title="Důležité"><div class="inner">
<div class="title title-note"><h2><span class="title">Před uspáním vždy uložte svoji práci</span></h2></div>
<div class="region"><div class="contents"><p class="p">Před uspáním počítače byste si měli všechnu svoji práci uložit. Může se totiž stát, že se něco pokazí a při opětovném probuzení počítače nepůjdou obnovit otevřené aplikace a dokumenty.</p></div></div>
