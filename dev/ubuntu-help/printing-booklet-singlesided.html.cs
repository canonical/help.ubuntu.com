<div class="hgroup"><h1 class="title"><span class="title">Tisk brožury na jednostranné tiskárně</span></h1></div>
<div class="region">
<div class="contents">
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents">
<p class="p">Tyto instrukce jsou pro tisk brožury z dokumentu PDF.</p>
<p class="p">Když chcete vytisknout brožuru z dokumentu <span class="app">LibreOffice</span>, nejprve ji vyexportujte do PDF pomocí <span class="guiseq"><span class="gui">Soubor</span> ▸ <span class="gui">Exportovat do PDF…</span></span>. Váš dokument musí mít počet stránek násobkem 4 (4, 8, 12, 16, …). Takže možná budete muset přidat až 3 prázdné stránky.</p>
</div></div></div></div>
<p class="p">Tisk proveďte následovně:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete dialogové okno tisku. Normálně se to dělá přes nabídku <span class="gui">Tisk</span> nebo pomocí klávesové zkratky <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>P</kbd></span></span>.</p></li>
<li class="steps">
<p class="p">Klikněte na tlačítko <span class="gui">Vlastnosti…</span></p>
<p class="p">Ujistěte se, že v rozbalovacím seznamu <span class="gui">Orientace</span> je vybráno <span class="gui">Na šířku</span>.</p>
<p class="p">Kliknutím na <span class="gui">Budiž</span> se vraťte do dialogového okna tisku.</p>
</li>
<li class="steps">
<p class="p">V části <span class="gui">Rozsah a kopie</span> zvolte <span class="gui">Stránky</span>.</p>
<p class="p">Napište čísla stránek v tomto pořadí (n je celkový počet stránek a je násobkem 4):</p>
<p class="p">n, 1, 2, n-1, n-2, 3, 4, n-3, n-4, 5, 6, n-5, n-6, 7, 8, n-7, n-8, 9, 10, n-9, n-10, 11, 12, n-11…</p>
<p class="p">…dokud nevypíšete všechny stránky.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents">
<p class="p">Příklad:</p>
<p class="p">4stránková brožura: Napište <span class="input">4,1,2,3</span></p>
<p class="p">8stránková brožura: Napište <span class="input">8,1,2,7,6,3,4,5</span></p>
<p class="p">12stránková brožura: Napište <span class="input">12,1,2,11,10,3,4,9,8,5,6,7</span></p>
<p class="p">16stránková brožura: Napište <span class="input">16,1,2,15,14,3,4,13,12,5,6,11,10,7,8,9</span></p>
<p class="p">20stránková brožura: Napište  <span class="input">20,1,2,19,18,3,4,17,16,5,6,15,14,7,8,13,12,9,10,11</span></p>
</div></div></div></div>
</li>
<li class="steps">
<p class="p">Zvolte kartu <span class="gui">Rozvržení stránek</span>.</p>
<p class="p">V části <span class="gui">Rozvržení</span> zvolte <span class="gui">Příručka</span>.</p>
<p class="p">V <span class="gui">Strany stránek</span> v rozbalovacím seznamu <span class="gui">Zahrnout</span> vyberte <span class="gui">Přední strany/pravé strany</span>.</p>
</li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Tisk</span>.</p></li>
<li class="steps"><p class="p">Až se vytisknou všechny stránky, otočte je vzhůru nohama a vraťte zpět do podavače tiskárny.</p></li>
<li class="steps"><p class="p">Otevřete dialogové okno tisku. Normálně se to dělá přes nabídku <span class="gui">Tisk</span> nebo pomocí klávesové zkratky <span class="keyseq"><span class="key"><kbd>Ctrl</kbd></span>+<span class="key"><kbd>P</kbd></span></span>.</p></li>
<li class="steps">
<p class="p">Zvolte kartu <span class="gui">Rozvržení stránek</span>.</p>
<p class="p">V <span class="gui">Strany stránek</span> v rozbalovacím seznamu <span class="gui">Zahrnout</span> vyberte <span class="gui">Zadní strany/levé strany</span>.</p>
</li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Tisk</span>.</p></li>
</ol></div></div></div>
