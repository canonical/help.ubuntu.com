<div class="hgroup"><h1 class="title"><span class="title">Vypněte nebo omezte sledování historie souborů</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Sledování nedávno použitých souborů a složek může usnadnit vyhledání položek, se kterými jste pracovali ve správci souborů a dialogových oknech souborů v aplikacích. Na druhou stranu si můžete přát, aby se tyto položky uchovaly v soukromí, nebo aby se sledovala jen úplně nejnovější historie.</p>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Vypnutí sledování historie souborů</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Soukromí</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Soukromí</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Vyberte <span class="gui">Použití a historie</span>.</p></li>
<li class="steps">
<p class="p">Vypínač <span class="gui">Nedávno použité</span> přepněte na <span class="gui">○</span> (vypnuto).</p>
<p class="p">Pro opětovné povolení této funkce přepněte vypínač <span class="gui">Nedávno použité</span> na <span class="gui">❙</span> (zapnuto).</p>
</li>
<li class="steps"><p class="p">Použijte tlačítko <span class="gui">Vyčistit nedávnou historii</span> k okamžitému zahození historie.</p></li>
</ol></div>
</div></div>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Toto nastavení neovlivní, jak uchovává webový prohlížeč informace o webových stránkách, které jste navštívili.</p></div></div></div></div>
<div class="steps"><div class="inner">
<div class="title title-steps"><h2><span class="title">Omezení doby, po kterou je soubor s historií schraňován</span></h2></div>
<div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Soukromí</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Soukromí</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Vyberte <span class="gui">Použití a historie</span>.</p></li>
<li class="steps"><p class="p">Ujistěte se, že je vypínač <span class="gui">Nedávno použité</span> přepnutý na <span class="gui">❙</span> (zapnuto).</p></li>
<li class="steps"><p class="p">U položky <span class="gui">Zachovat historii</span> vyberte dobu uchování. Na výběr je <span class="gui">1 den</span>, <span class="gui">7 dní</span>, <span class="gui">30 dní</span> nebo <span class="gui">Napořád</span>.</p></li>
<li class="steps"><p class="p">Použijte tlačítko <span class="gui">Vyčistit nedávnou historii</span> k okamžitému zahození historie.</p></li>
</ol></div>
</div></div>
