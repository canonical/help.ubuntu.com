<div class="hgroup"><h1 class="title"><span class="title">Přihlašování pomocí otisku prstu</span></h1></div>
<div class="region">
<div class="contents"><p class="p">V případě, že váš systém podporuje snímač otisků prstů, můžete nahrát svůj otisk prstu a použít jej k přihlašování.</p></div>
<div id="record" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zaznamenání otisku prstu</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Dříve, než se můžete přihlašovat svým otiskem prstu, potřebujete jej nahrát, aby jej systém mohl používat k vaší identifikaci.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">V případě, že máte příliš suchou pokožku na prstech, můžete mít problém s registrací vašeho otisku prstu. V takové situaci si prsty lehce navlhčete, osušte čistým hadříkem, který nepouští chlupy, a zkuste to znovu.</p></div></div></div></div>
<p class="p">Abyste mohli měnit jiný, než svůj, uživatelský účet, potřebujete <span class="link"><a href="user-admin-explain.html.cs" title="Jak fungují oprávnění správce?">oprávnění správce</a></span>.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Uživatelé</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Uživatelé</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Zmáčkněte <span class="gui">Zakázáno</span> vedle <span class="gui">Přihlášení otiskem prstu</span>, abyste přidali otisk prstu pro vybraný účet. V případě, že přidáváte otisk pro jiného uživatele, musíte panel nejprve <span class="gui">Odemknout</span>.</p></li>
<li class="steps"><p class="p">Vyberte prst, který chcete pro otisk použít, a zmáčkněte <span class="gui">Následující</span>.</p></li>
<li class="steps"><p class="p">Postupujte podle pokynů v dialogovém okně a přejeďte prstem <span class="em">pomalu a plynule</span> přes čtečku otisků prstů. Jakmile bude mít počítač váš otisk prstu správně zaznamenán, uvidíte zprávu <span class="gui">Hotovo!</span>.</p></li>
<li class="steps"><p class="p">Vyberte <span class="gui">Další</span>. Uvidíte potvrzovací zprávu, že se váš otisk prstu úspěšně uložil. Pro dokončení vyberte <span class="gui">Zavřít</span>.</p></li>
</ol></div></div></div>
</div></div>
</div></div>
<div id="verify" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Kontrola, že otisky prstů fungují</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Nyní zkontrolujte, jestli přihlášením s vaším novým otiskem prstu funguje. I když máte zaregistrovaný otisk prstu, stále máte na výběr přihlášení pomocí svého hesla.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Uložte případnou rozdělanou práci a pak se <span class="link"><a href="shell-exit.html.cs#logout" title="Odhlášení nebo přepnutí uživatelů">odhlaste</a></span>.</p></li>
<li class="steps"><p class="p">Na přihlašovací obrazovce vyberte v seznamu své jméno. Objeví se pole pro zadání hesla.</p></li>
<li class="steps"><p class="p">Místo zadávání hesla by vám mělo fungovat přejetí prstem přes čtečku otisků prstů.</p></li>
</ol></div></div></div>
</div></div>
