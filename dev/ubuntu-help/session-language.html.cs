<div class="hgroup"><h1 class="title"><span class="title">Změna jazyka, který používáte</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Aplikace a celé uživatelské prostředí můžete používat v tuctech národních jazyků, které jsem poskytovány skrze jazykové balíčky nainstalované v počítači.</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Region a jazyk</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Region a jazyk</span> otevřete příslušný panel.</p></li>
<li class="steps"><p class="p">Klikněte na <span class="gui">Jazyk</span>.</p></li>
<li class="steps"><p class="p">Vyberte svůj požadovaný region a jazyk. Pokud váš region a jazyk nejsou na seznamu, klikněte na <span class="gui"><span class="media"><span class="media media-image"><img src="figures/view-more-symbolic.svg" class="media media-inline" alt="…"></span></span></span> ve spodní části seznamu, abyste si mohli vybrat ze všech dostupných regionů a jazyků.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Hotovo</span> nastavení uložíte.</p></li>
<li class="steps"><p class="p">Na výzvu <span class="gui">Aby se změny projevily, musí být sezení restartováno</span> zareagujte kliknutím na <span class="gui">Restartovat nyní</span> nebo kliknutím na <span class="gui">×</span> odložte restart na později.</p></li>
</ol></div></div></div>
<p class="p">Některé překlady mohou být neúplné a některé aplikace nemusí podporovat váš jazyk vůbec. Nepřeložený text se pak objeví v jazyce, ve kterém byl software původně vyvíjen, obvykle americké angličtině.</p>
<p class="p">Existuje pár speciálních složek ve vaší domovské složce, do kterých mohou aplikace ukládat takové věci, jak jsou hudba, obrázky a dokumenty. Tyto složky používají standardní názvy ve vašem jazyce. Když se znovu přihlásíte, budete dotázáni, jestli chcete tyto složky přejmenovat na standardní názvy ve vašem jazyce. Pokud plánujete používat nový jazyk dlouhodobě, měli byste názvy složek nechat aktualizovat.</p>
