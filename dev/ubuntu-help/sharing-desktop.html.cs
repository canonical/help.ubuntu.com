<div class="hgroup"><h1 class="title"><span class="title">Sdílení vaší plochy</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete dovolit ostatním lidem zobrazit si a ovládat vaše uživatelské prostředí z jiného počítače pomocí příslušné aplikace. Povolení přístupu a předvolby zabezpečení najdete v nastavení <span class="gui">Sdílení obrazovky</span>.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Aby bylo viditelné <span class="app">Sdílení obrazovky</span>, musíte mít nainstalovaný balíček <span class="gui">Vino</span>.</p></div></div></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Sdílení</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Sdílení</span> otevřete příslušný panel.</p></li>
<li class="steps">
<p class="p">Pokud je vypínač <span class="gui">Sdílení</span> v poloze <span class="gui">○</span> (vypnuto), přepněte jej na <span class="gui">❙</span> (zapnuto).</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Text pod popiskem <span class="gui">Název počítače</span> je možné upravit a <span class="link"><a href="sharing-displayname.html" title="Nastavení zobrazovaného názvu pro váš počítač">změnit</a></span> tak název svého počítače, kterým se prezentuje v síti.</p></div></div></div></div>
</li>
<li class="steps"><p class="p">Vyberte <span class="gui">Sdílení obrazovky</span>.</p></li>
<li class="steps"><p class="p">Abyste ostatním umožnili zobrazit si vaši pracovní plochu, přepněte vypínač <span class="gui">Sdílení obrazovky</span> na <span class="gui">❙</span> (zapnuto). Bude to znamenat, že ostatní lidé se mohou připojit k vašemu počítači a zobrazit si, co máte na obrazovce.</p></li>
<li class="steps">
<p class="p">Abyste ostatním umožnili pracovat ve vašem uživatelském prostředí, přepněte <span class="gui">Umožnit vzdálené ovládání</span> na <span class="gui">❙</span> (zapnuto). Díky tomu budou moci ostatní pohybovat vaší myší, spouštět aplikace a procházet soubory ve vašem počítači, v závislosti na bezpečnostním nastavení, které se právě používá.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Když je <span class="gui">Sdílení obrazovky</span> v poloze <span class="gui">❙</span> (zapnuto), je tato volba ve výchozím stavu zapnutá.</p></div></div></div></div>
</li>
</ol></div></div></div>
</div>
<div id="security" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zabezpeční</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Je důležité, abyste zvážili všechny dosahy jednotlivých bezpečnostních voleb, než je změníte.</p>
<div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Nové připojení se musí dotázat o svolení</dt>
<dd class="terms">
<p class="p">Jestli chcete mít možnost volit, jestli někomu povolit přístup ke svému uživatelskému prostředí či ne, zapněte <span class="gui">Nové připojení se musí dotázat o svolení</span>. Když tuto volbu vypnete, nebudete na svolení, že se může někdo připojit k vašemu počítači, dotazováni.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Tato volba je ve výchozím stavu zapnutá.</p></div></div></div></div>
</dd>
<dt class="terms">Vyžadovat heslo</dt>
<dd class="terms">
<p class="p">Aby bylo po ostatních lidech požadováno použití hesla při připojení k vaší pracovní ploše, zapněte <span class="gui">Vyžadovat heslo</span>. Když tuto volbu nepoužijete, bude si moci vaši pracovní plochu zobrazit kdokoliv.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Tato volba je ve výchozím stavu vypnutá, ale měli byste ji zapnout a nastavit bezpečné heslo.</p></div></div></div></div>
</dd>
</dl></div></div></div>
</div></div>
</div></div>
<div id="networks" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Sítě</span></h2></div>
<div class="region"><div class="contents"><p class="p">V části <span class="gui">Sítě</span> jsou uvedeny sítě, ke kterým jste právě připojeni. Použijte vypínač vedle jednotlivých sítí k volbě, kde může být vaše pracovní plocha sdílena.</p></div></div>
</div></div>
<div id="notification-icon" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zastavení sdílení obrazovky</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Pomocí <span class="gui">ikony upozornění</span> v pořadači zpráv můžete kohokoliv, kdo si prohlíží vaši pracovní plochu, odpojit. Uděláte to následovně:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete pořadač zpráv zmáčknutím <span class="keyseq"><span class="key"><kbd>Super</kbd></span>+<span class="key"><kbd>M</kbd></span></span> nebo přesunutím ukazatele myši do úplně spodní části obrazovky.</p></li>
<li class="steps"><p class="p">V <span class="gui">pořadači zpráv</span> klikněte na ikonu <span class="gui">Plochy</span>. Tím se otevře panel <span class="app">Sdílení</span>.</p></li>
<li class="steps"><p class="p">Vyberte <span class="gui">Sdílení obrazovky</span>.</p></li>
<li class="steps"><p class="p">Vypínač <span class="gui">Sdílení obrazovky</span> přepněte do polohy <span class="gui">○</span> (vypnuto).</p></li>
</ol></div></div></div>
