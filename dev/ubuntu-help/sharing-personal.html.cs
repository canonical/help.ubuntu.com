<div class="hgroup"><h1 class="title"><span class="title">Sdílení vašich osobních souborů</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Můžete umožnit přístup z jiných počítačů v síti k složce <span class="file">Veřejné</span> ve vaší <span class="file">Domovské</span> složce. Nastavte <span class="gui">Sdílení osobních souborů</span>, abyste ostatním umožnili přístup k obsahu této složky.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Aby bylo <span class="app">Sdílení multimédií</span> vidět, musíte mít nainstalovaný balíček <span class="gui">gnome-user-share</span>.</p></div></div></div></div>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html.cs#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Sdílení</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Sdílení</span> otevřete příslušný panel.</p></li>
<li class="steps">
<p class="p">Pokud je vypínač <span class="gui">Sdílení</span> v poloze <span class="gui">○</span> (vypnuto), přepněte jej na <span class="gui">❙</span> (zapnuto).</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Text pod popiskem <span class="gui">Název počítače</span> je možné upravit a <span class="link"><a href="sharing-displayname.html.cs" title="Nastavení zobrazovaného názvu pro váš počítač">změnit</a></span> tak název svého počítače, kterým se prezentuje v síti.</p></div></div></div></div>
</li>
<li class="steps"><p class="p">Vyberte <span class="gui">Sdílení osobních souborů</span>.</p></li>
<li class="steps">
<p class="p">Přepněte vypínač <span class="gui">Sdílení osobních souborů</span> na <span class="gui">❙</span> (zapnuto). Znamená to, že ostatní lidé ve vaší aktuální síti se budou moci připojit k vašemu počítači a přistupovat k souborům ve složce <span class="file">Veřejné</span>.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Zobrazená adresa <span class="em">URI</span> je tak, přes kterou se mohou ostatní v síti dostat k vaší složce <span class="file">Veřejné</span> na vašem počítači.</p></div></div></div></div>
</li>
</ol></div></div></div>
</div>
<div id="security" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Zabezpeční</span></h2></div>
<div class="region"><div class="contents"><div class="terms"><div class="inner"><div class="region"><dl class="terms">
<dt class="terms">Vyžadovat heslo</dt>
<dd class="terms">
<p class="p">Aby bylo po ostatních lidech požadováno použití hesla při přístupu k vaší složce <span class="file">Veřejné</span>, přepněte <span class="gui">Vyžadovat heslo</span> na <span class="gui">❙</span> (zapnuto). Když tuto volbu nepoužijete, bude si moci vaši složku <span class="file">Veřejné</span> zobrazit kdokoliv.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Tato volba je ve výchozím stavu vypnutá, ale měli byste ji zapnout a nastavit bezpečné heslo.</p></div></div></div></div>
</dd>
</dl></div></div></div></div></div>
</div></div>
<div id="networks" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Sítě</span></h2></div>
<div class="region"><div class="contents"><p class="p">V části <span class="gui">Sítě</span> jsou uvedeny sítě, ke kterým jste právě připojeni. Použijte vypínač vedle jednotlivých sítí k volbě, kde mohou být vaše osobní soubory sdíleny.</p></div></div>
