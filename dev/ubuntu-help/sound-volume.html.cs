<div class="hgroup"><h1 class="title"><span class="title">Změna hlasitosti</span></h1></div>
<div class="region">
<div class="contents">
<p class="p">Pro změnu hlasitosti zvuku otevřete <span class="gui"><a href="shell-introduction.html#yourname" title="Vy a váš počítač">systémovou nabídku</a></span> na pravé straně horní lišty a posuňte táhlo hlasitosti doleva nebo doprava. Posunutím úplně doleva zvuk zcela vypnete (ztišíte).</p>
<p class="p">Některé klávesnice mají klávesy, které umožňují ovládat hlasitost. Obvykle na sobě mají symbol stylizovaného reproduktoru, ze kterého vychází zvukové vlny. Obvykle se nachází někde u funkčních kláves nahoře, u notebooků to jsou často přímo funkční klávesy v kombinaci se speciální klávesou <span class="key"><kbd class="key-Fn">Fn</kbd></span>.</p>
<p class="p">Pokud máte externí reproduktory, můžete ovládat hlasitost také přímo na nich ovládacím kolečkem. Rovněž některá sluchátka mají ovládání hlasitosti.</p>
</div>
<div id="apps" class="sect"><div class="inner">
<div class="hgroup"><h2 class="title"><span class="title">Změna hlasitosti zvuku pro jednotlivé aplikace</span></h2></div>
<div class="region"><div class="contents">
<p class="p">Můžete změnit hlasitost u jedné aplikace a pro ostatní ji ponechat bez změny. To se hodí například když posloucháte hudbu a u toho procházíte webové stránky. Při tom můžete chtít ve webovém prohlížeči zvuk vypnout, aby vám nenarušoval poslech hudby.</p>
<p class="p">Některé aplikace mají ovládání hlasitosti ve svém hlavním okně. Pokud tomu tak je, použijte toto ovládání hlasitosti. Pokud tomu tak není:</p>
<div class="steps"><div class="inner"><div class="region"><ol class="steps">
<li class="steps"><p class="p">Otevřete přehled <span class="gui"><a href="shell-introduction.html#activities" title="Přehled Činností">Činnosti</a></span> a začněte psát <span class="gui">Zvuk</span>.</p></li>
<li class="steps"><p class="p">Kliknutím na <span class="gui">Zvuk</span> otevřete příslušný panel.</p></li>
<li class="steps">
<p class="p">Přejděte na kartu <span class="gui">Aplikace</span> a změňte hlasitost poslouchané aplikace zde.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Uvedeny jsou jen aplikace, které přehrávají zvuk. V případě, že aplikace přehrává zvuk a není zde uvedena, možná nepodporuje funkci, která umožňuje ovládat hlasitost tímto způsobem. Potom hlasitost měnit nemůžete.</p></div></div></div></div>
</li>
</ol></div></div></div>
