<div class="hgroup"><h1 class="title"><span class="title">Volba bezpečného hesla</span></h1></div>
<div class="region">
<div class="contents">
<div class="note note-important" title="Důležité"><div class="inner"><div class="region"><div class="contents"><p class="p">Vytvořte si takové heslo, které si snadno zapamatujete, ale pro ostatní (včetně počítačových programů) bude velmi obtížné jej uhodnout.</p></div></div></div></div>
<p class="p">Volba dobrého hesla pomáhá udržet váš počítač v bezpečí. Když jde heslo snadno uhodnout, může na něj někdo přijít a získat přístup k vašim osobním datům.</p>
<p class="p">Někdo by také mohl použít počítače k systematickému hádání vašeho hesla (tzv. útok hrubou silou), takže i když by bylo těžce uhodnutelné pro člověka, může být snadno rozlousknutelné počítačem. Zde je pár rad, jak si zvolit dobré heslo:</p>
<div class="list"><div class="inner"><div class="region"><ul class="list">
<li class="list">
<p class="p">Použijte v hesle směs malých a velkých písmen, číslic, symbolů a mezer. Tím se stane těžko uhodnutelným, protože více různých znaků znamená více možných kombinací, které je potřeba při hádání hesla vyzkoušet.</p>
<div class="note" title="Upozornění"><div class="inner"><div class="region"><div class="contents"><p class="p">Dobrou metodou pro výběr kvalitního hesla je vzít první znaky slov z nějaké fráze, kterou si dobře pamatujete. Třeba úryvek z nějaké písně, básně nebo hlášky z filmu, knihy. Například „Kams to dostal Václave? Ale do křídla, člověče.“  by mohlo dát „KtdV? Adk, c.“ nebo „KtdV?Adkc.“ nebo „KtdVAdkc“.</p></div></div></div></div>
</li>
<li class="list"><p class="p">Vytvořte heslo co nejdelší. Čím více znaků obsahuje, tím déle by trvalo člověku nebo počítači jej uhodnout.</p></li>
<li class="list"><p class="p">Nepoužívejte běžná slova, která se objevují v slovnících libovolného jazyka. Takováto slova zkouší útočníci jako první. Nejběžnějším heslem je „heslo“ (respektive v angličtině „password“) – heslo tohoto typu lze uhodnout velmi rychle!</p></li>
<li class="list"><p class="p">Nepoužívejte osobní údaje, jako datum narození, registrační značka, jméno člena rodiny.</p></li>
<li class="list"><p class="p">Nepoužívejte běžná slova.</p></li>
<li class="list">
<p class="p">Volte hesla, která dokážete napsat rychle, abyste snížili šance vysledovat co píšete osobou, které se poštěstilo vás při psaní hesla pozorovat.</p>
<div class="note note-tip" title="Tip"><div class="inner"><div class="region"><div class="contents"><p class="p">Hesla si nikdy nikam nezapisujte. Někdo by je mohl snadno najít.</p></div></div></div></div>
</li>
<li class="list"><p class="p">Pro různé věci používejte různá hesla.</p></li>
<li class="list">
<p class="p">Pro různé účty používejte různá hesla.</p>
<p class="p">Pokud použijete stejné heslo ke všem svým účtům, může se kdokoliv, kdo heslo zjistí, dostat ihned do všech vašich účtů.</p>
<p class="p">Může být obtížné zapamatovat si spoustu hesel. I když nejbezpečnější je používat pro každou věc jiné heslo, můžete si to usnadnit tím, že použijete to stejné heslo pro nekritické věci (jako třeba pro některé weby) a jiná hesla pro věci důležité (jako internetové bankovnictví a váš e-mail).</p>
</li>
<li class="list"><p class="p">Svá hesla pravidelně měňte.</p></li>
</ul></div></div></div>
